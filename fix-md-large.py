def extract_prod_structure_enhanced(container: Tag) -> dict:
    """Enhanced prod structure extraction used by the large-guide fixer."""
    structure = {
        "notes": [],
        "images": [],
        "list_items": [],
        "li_paragraphs": [],
        "tables": [],
        "headings": [],
        "paragraphs": [],
    }

    admon_classes = {"note", "warning", "important", "information", "tip", "danger"}

    for div in container.find_all("div", class_=True):
        classes = set(div.get("class", []))
        if "note" not in classes and not admon_classes & classes:
            continue
        li_parent = div.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        text = div.get_text(strip=True)[:80]
        para_starts = []
        direct_p_count = 0
        for p in div.find_all("p"):
            list_ancestor = p.find_parent(["ul", "ol"])
            if list_ancestor and div in list_ancestor.parents:
                continue
            p_text = re.sub(r"\s+", " ", _html_to_md_text(p).strip())
            if p_text:
                para_starts.append(p_text)
                direct_p_count += 1
        structure["notes"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "text_preview": text,
            "text": re.sub(r"\s+", " ", _html_to_md_text(div).strip()),
            "paragraphs": direct_p_count,
            "para_starts": para_starts,
        })

    for img in container.find_all("img"):
        src = img.get("src", "")
        if img.find_parent(lambda tag: tag.name == "div"
                           and admon_classes & set(tag.get("class") or [])):
            continue
        block = img.parent
        while block and block.name not in ("p", "li", "td", "div", "body"):
            block = block.parent
        if block and block.name in ("p", "li"):
            sibling_text = ""
            for sib in block.children:
                if sib is img:
                    continue
                if isinstance(sib, NavigableString):
                    sibling_text += str(sib).strip()
                elif hasattr(sib, "get_text") and sib.name not in ("style", "script"):
                    if not sib.find("img"):
                        sibling_text += sib.get_text(strip=True)
            if len(sibling_text) > 5:
                continue
        li_parent = img.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        structure["images"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "src": src,
        })

    for li in container.find_all("li"):
        depth = _li_depth(li)
        has_note = any(
            div for div in li.find_all("div", class_=True, recursive=False)
            if {"note"} & set(div.get("class", []))
            or admon_classes & set(div.get("class", []))
        )
        for info_div in li.find_all("div", class_="itemgroup"):
            if any(
                div for div in info_div.find_all("div", class_=True, recursive=False)
                if admon_classes & set(div.get("class", []))
            ):
                has_note = True
        structure["list_items"].append({
            "depth": depth,
            "text": _li_text(li),
            "has_note": has_note,
        })

    for li in container.find_all("li"):
        li_first_text = ""
        for child in li.children:
            if isinstance(child, NavigableString) and child.strip():
                li_first_text = child.strip()[:80]
                break
            if isinstance(child, Tag) and child.name in ("span", "p", "a", "strong", "em", "b"):
                li_first_text = child.get_text(strip=True)[:80]
                break

        for child in li.children:
            if not isinstance(child, Tag):
                continue
            if child.name in ("ul", "ol"):
                continue
            cls = child.get("class") if child.attrs else None
            if cls and admon_classes & set(cls or []):
                continue
            if child.name in ("div", "p") and child.get_text(strip=True):
                child_text = re.sub(r"\s+", " ", _html_to_md_text(child).strip())
                if child_text[:60] != li_first_text[:60]:
                    structure["li_paragraphs"].append({
                        "li_head": li_first_text,
                        "text": child_text[:120],
                        "li_depth": _li_depth(li),
                    })
            elif child.name == "div":
                for seg in _extract_inline_segs(child):
                    if seg[:60] == li_first_text[:60]:
                        continue
                    structure["li_paragraphs"].append({
                        "li_head": li_first_text,
                        "text": seg[:120],
                        "li_depth": _li_depth(li),
                    })

    for table in container.find_all("table"):
        li_parent = table.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        first_row = table.find("tr")
        preview = first_row.get_text(strip=True)[:80] if first_row else ""
        headers = []
        if first_row:
            for th in first_row.find_all(["th", "td"]):
                headers.append(re.sub(r"\s+", " ", _html_to_md_text(th).strip()))
        rows = table.find_all("tr")
        structure["tables"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "text_preview": preview,
            "element": table,
            "headers": headers,
            "row_count": len(rows),
            "col_count": len(headers),
        })

    for h in container.find_all(["h1", "h2", "h3", "h4", "h5", "h6"]):
        level = int(h.name[1])
        text = re.sub(r"\s+", " ", _html_to_md_text(h).strip())
        text = re.sub(r"\s*link$", "", text)
        structure["headings"].append({"level": level, "text": text})

    admon_own = {"note", "warning", "important", "information", "tip", "danger", "prereq"}
    admon_ancs = {"note", "warning", "important", "information", "tip", "danger"}

    def _is_para_skip(el):
        if el.find_parent("table"):
            return True
        if el.find_parent(["ul", "ol", "dl", "dd"]):
            return True
        if admon_own & set(el.get("class", [])):
            return True
        for anc in el.parents:
            if getattr(anc, "name", None) != "div":
                continue
            if admon_ancs & set(anc.get("class", [])):
                return True
        return False

    for p in container.find_all(["p", "div"]):
        if _is_para_skip(p):
            continue
        text = re.sub(r"\s+", " ", _html_to_md_text(p).strip())
        if text and len(text) > 10:
            structure["paragraphs"].append({"text": text})

    return structure


def parse_md_structure_enhanced(lines: List[str]) -> dict:
    """Enhanced markdown structure extraction used by the large-guide fixer."""
    structure = {
        "notes": [],
        "images": [],
        "list_items": [],
        "li_paragraphs": [],
        "code_blocks": [],
        "orphaned_paragraphs": [],
        "tables": [],
        "md_pipe_tables": [],
        "headings": [],
        "paragraphs": [],
        "frontmatter_title": "",
    }

    fence_open = re.compile(r'^(\s*):::(note|tip|warning|danger|info|important|caution)\s*$')
    fence_close = re.compile(r'^(\s*):::\s*$')
    image_re = re.compile(r'^(\s*)!\[')
    list_head = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    code_fence = re.compile(r'^\s*(```|~~~)')

    inside_code = False
    code_start = None
    i = 0
    n = len(lines)
    list_stack = []

    while i < n:
        line = lines[i]
        if code_fence.match(line):
            if inside_code:
                structure["code_blocks"].append({
                    "start_line": code_start,
                    "end_line": i,
                    "text": "\n".join(lines[code_start + 1:i]),
                })
                code_start = None
                inside_code = False
            else:
                code_start = i
                inside_code = True
            i += 1
            continue
        if inside_code:
            i += 1
            continue

        m_list = list_head.match(line)
        if m_list:
            indent = len(m_list.group(1))
            marker = m_list.group(2)
            while list_stack and list_stack[-1][0] >= indent:
                list_stack.pop()
            list_stack.append((indent, len(marker) + 1))
            structure["list_items"].append({
                "line": i,
                "indent": indent,
                "depth": len(list_stack) - 1,
                "text": line.strip(),
            })

        m_open = fence_open.match(line)
        if m_open:
            note_indent = len(m_open.group(1))
            start = i
            para_count = 0
            has_content = False
            j = i + 1
            while j < n and not fence_close.match(lines[j]):
                if lines[j].strip():
                    if not has_content or (j > 0 and not lines[j - 1].strip()):
                        para_count += 1
                    has_content = True
                j += 1
            end = j
            structure["notes"].append({
                "line": start,
                "end_line": end,
                "indent": note_indent,
                "in_list": bool(list_stack) and note_indent > 0,
                "paragraph_count": para_count,
                "admonition_type": m_open.group(2),
            })
            i = end + 1
            continue

        m_img = image_re.match(line)
        if m_img:
            img_indent = len(m_img.group(1))
            structure["images"].append({
                "line": i,
                "indent": img_indent,
                "in_list": bool(list_stack),
            })

        stripped = line.strip()
        indent = len(line) - len(line.lstrip(" "))
        if (list_stack and stripped and indent > 0 and not m_list and not m_img
                and len(stripped) > 10
                and not stripped.startswith((":::", "#", "|", "<"))
                and not re.match(r'^\d+\.?\s', stripped)):
            structure["li_paragraphs"].append({
                "line": i,
                "indent": indent,
                "depth": len(list_stack) - 1,
                "text": stripped,
            })

        i += 1

    table_open_re = re.compile(r'^(\s*)<table', re.I)
    table_close_re = re.compile(r'^\s*</table>', re.I)
    in_code_tbl = False
    for i, line in enumerate(lines):
        if code_fence.match(line):
            in_code_tbl = not in_code_tbl
            continue
        if in_code_tbl:
            continue
        m_tbl = table_open_re.match(line)
        if m_tbl:
            tbl_indent = len(m_tbl.group(1))
            end_idx = i
            for j in range(i + 1, n):
                if table_close_re.match(lines[j]):
                    end_idx = j
                    break
            preview = ""
            tbl_html = "\n".join(lines[i:end_idx + 1])
            tbl_soup = BeautifulSoup(tbl_html, "html.parser")
            tbl_tag = tbl_soup.find("table")
            md_headers = []
            md_row_count = 0
            if tbl_tag:
                first_tr = tbl_tag.find("tr")
                if first_tr:
                    for th in first_tr.find_all(["th", "td"]):
                        md_headers.append(re.sub(r"\s+", " ", th.get_text(strip=True)))
                md_row_count = len(tbl_tag.find_all("tr"))
                preview = first_tr.get_text(strip=True)[:80] if first_tr else ""
            structure["tables"].append({
                "start_line": i,
                "end_line": end_idx,
                "indent": tbl_indent,
                "text_preview": preview,
                "headers": md_headers,
                "row_count": md_row_count,
                "col_count": len(md_headers),
            })

    md_pipe_re = re.compile(r'^\s*\|')
    md_sep_re = re.compile(r'^\s*\|([\s:]*-+[\s:]*\|)+\s*$')
    long_code_re = re.compile(r'`{1,3}\s*(.+?)\s*`{1,3}')
    in_code_pipe = False
    i_pipe = 0
    while i_pipe < n:
        ln = lines[i_pipe]
        if code_fence.match(ln):
            in_code_pipe = not in_code_pipe
            i_pipe += 1
            continue
        if in_code_pipe:
            i_pipe += 1
            continue
        if md_pipe_re.match(ln) and i_pipe + 1 < n and md_sep_re.match(lines[i_pipe + 1]):
            tbl_start = i_pipe
            tbl_indent = len(ln) - len(ln.lstrip())
            j = i_pipe + 2
            while j < n and md_pipe_re.match(lines[j]):
                j += 1
            tbl_end = j - 1
            md_headers = [cell.strip() for cell in lines[tbl_start].split('|')[1:-1]]
            headers_empty = all(not h for h in md_headers)
            actual_row_count = max(0, tbl_end - tbl_start - (1 if not headers_empty else 2) + 1)
            preview = lines[tbl_start].replace('|', ' ').strip()[:80]
            structure["tables"].append({
                "start_line": tbl_start,
                "end_line": tbl_end,
                "indent": tbl_indent,
                "text_preview": preview,
                "headers": md_headers,
                "headers_empty": headers_empty,
                "row_count": actual_row_count,
                "col_count": len(md_headers),
            })
            has_long_code = False
            for k in range(tbl_start, tbl_end + 1):
                if k == tbl_start + 1:
                    continue
                for m in long_code_re.finditer(lines[k]):
                    if len(m.group(1).strip()) >= CODE_IN_TABLE_THRESHOLD:
                        has_long_code = True
                        break
                if has_long_code:
                    break
            if has_long_code:
                structure["md_pipe_tables"].append({
                    "start_line": tbl_start,
                    "end_line": tbl_end,
                    "indent": tbl_indent,
                })
            i_pipe = tbl_end + 1
        else:
            i_pipe += 1

    structure["tables"].sort(key=lambda t: t["start_line"])

    heading_detect_re = re.compile(r'^(#{1,6})\s+(.*)')
    in_code_hp = False
    in_table_hp = False
    in_note_hp = False
    in_frontmatter = False
    frontmatter_done = False
    note_open_hp = re.compile(r'^\s*:::\w+')
    note_close_hp = re.compile(r'^\s*:::\s*$')
    for i_hp, line_hp in enumerate(lines):
        if not frontmatter_done:
            if i_hp == 0 and line_hp.strip() == "---":
                in_frontmatter = True
                continue
            if in_frontmatter:
                if line_hp.strip() == "---":
                    in_frontmatter = False
                    frontmatter_done = True
                else:
                    m_title = re.match(r'^title:\s*(.+)', line_hp)
                    if m_title:
                        structure["frontmatter_title"] = m_title.group(1).strip().strip('"').strip("'")
                continue
            frontmatter_done = True
        if code_fence.match(line_hp):
            in_code_hp = not in_code_hp
            continue
        if in_code_hp:
            continue
        if table_open_re.match(line_hp):
            in_table_hp = True
            continue
        if table_close_re.match(line_hp):
            in_table_hp = False
            continue
        if in_table_hp:
            continue
        if note_open_hp.match(line_hp) and not note_close_hp.match(line_hp):
            in_note_hp = True
            continue
        if note_close_hp.match(line_hp):
            in_note_hp = False
            continue
        if in_note_hp:
            continue
        m_head = heading_detect_re.match(line_hp)
        if m_head:
            structure["headings"].append({
                "line": i_hp,
                "level": len(m_head.group(1)),
                "text": m_head.group(2).strip(),
            })
            continue
        stripped_hp = line_hp.strip()
        first_ch = line_hp[0:1] if line_hp else ""
        if first_ch in (" ", "\t", "#", "|", "!"):
            continue
        if first_ch == "<" and not re.match(r'^\s*</?(sup|sub|span)\b', stripped_hp, re.I):
            continue
        if first_ch in ("*", "-", "+") and len(line_hp) > 1 and line_hp[1:2] in (" ", "\t"):
            continue
        if (stripped_hp
                and not stripped_hp.startswith("---")
                and not list_head.match(line_hp)
                and len(stripped_hp) > 10
                and not re.match(r'^\d+\)\s', stripped_hp)
                and not re.match(r'^\d+\s*-\s+', stripped_hp)):
            structure["paragraphs"].append({
                "line": i_hp,
                "text": stripped_hp,
            })

    heading_re = re.compile(r'^#{1,6}\s')
    fence_open_admon = re.compile(r'^\s*:::\w+')
    list_head_re = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    code_fence_re = re.compile(r'^\s*(```|~~~)')
    in_code = False
    for i, line in enumerate(lines):
        if code_fence_re.match(line):
            in_code = not in_code
            continue
        if in_code or not line.strip():
            continue
        if line[0] in (" ", "\t"):
            continue
        if list_head_re.match(line) or heading_re.match(line) or fence_open_admon.match(line):
            continue
        if line.startswith("|") or line.startswith("---"):
            continue

        prev_list_line = None
        for k in range(i - 1, -1, -1):
            if not lines[k].strip():
                continue
            if lines[k][0:1] in (" ", "\t"):
                continue
            if list_head_re.match(lines[k]):
                prev_list_line = k
                break
            break
        if prev_list_line is None:
            continue

        next_is_heading = False
        for k in range(i + 1, len(lines)):
            if not lines[k].strip():
                continue
            stripped_k = lines[k].strip()
            if stripped_k.startswith('![') or lines[k][0:1] in (' ', '\t'):
                continue
            if heading_re.match(lines[k]):
                next_is_heading = True
            break
        if next_is_heading:
            continue

        kind = "image" if line.strip().startswith('![') else "paragraph"
        structure["orphaned_paragraphs"].append({
            "line": i,
            "text": re.sub(r'\s+', ' ', line.strip())[:120],
            "prev_list_line": prev_list_line,
            "kind": kind,
        })

    return structure


def fix_collapsed_list_items(lines: List[str]) -> List[str]:
    """Split list items collapsed onto a single line."""
    fixes = 0
    result = []
    for idx, line in enumerate(lines):
        stripped = line.lstrip(' ')
        indent = len(line) - len(stripped)
        if not re.match(r'^[*\-+] ', stripped):
            result.append(line)
            continue
        marker_char = stripped[0]
        current_marker = marker_char + ' '
        split_pattern = (re.compile(r'(?<=[^\s*])\* ') if marker_char == '*'
                         else re.compile(r'(?<=\S)' + re.escape(marker_char) + r' '))
        rest = stripped[2:]
        matches = list(split_pattern.finditer(rest))
        if not matches:
            result.append(line)
            continue
        prefix = ' ' * indent
        prev_end = 0
        items = []
        for m in matches:
            items.append(rest[prev_end:m.start()])
            prev_end = m.end()
        items.append(rest[prev_end:])
        result.append(prefix + current_marker + items[0])
        for item in items[1:]:
            result.append(prefix + current_marker + item)
        fixes += 1
        _log(f"  [fix] Line {idx + 1}: split {len(items)} collapsed list items")
    return result if fixes else lines

def fix_note_indent(lines: List[str], md_note: dict, prod_note: dict) -> List[str]:
    """Fix a note's indentation based on prod structure."""
    start = md_note["line"]
    end = md_note["end_line"]
    current_indent = md_note["indent"]

    if prod_note["in_list"]:
        # Note should be inside a list item — find the correct indent
        # Look backwards for the parent list item
        target_indent = _find_list_cont_indent(lines, start)
        if target_indent is not None and target_indent != current_indent:
            _log(f"  [fix] Note at line {start + 1}: indent {current_indent} -> {target_indent}")
            for k in range(start, min(end + 1, len(lines))):
                stripped = lines[k].lstrip(' ')
                lines[k] = ' ' * target_indent + stripped
    else:
        # Note should be top-level (indent 0)
        if current_indent > 0:
            _log(f"  [fix] Note at line {start + 1}: indent {current_indent} -> 0 (top-level)")
            for k in range(start, min(end + 1, len(lines))):
                lines[k] = lines[k].lstrip(' ')

    return lines


def fix_note_paragraphs(lines: List[str], md_note: dict, prod_note: dict) -> List[str]:
    """Fix condensed paragraphs in a note block.

    When md_maker strips <p> tags inside admonitions, multiple paragraphs
    get condensed onto one line.  We use the first words of each prod <p>
    to locate paragraph boundaries in the md content and insert blank lines.
    """
    if prod_note["paragraphs"] <= 1:
        return lines
    if md_note["paragraph_count"] >= prod_note["paragraphs"]:
        return lines

    para_starts = prod_note.get("para_starts", [])
    if len(para_starts) < 2:
        _log(f"  [warn] Note at line {md_note['line'] + 1}: has {md_note['paragraph_count']} paragraph(s) "
              f"but prod has {prod_note['paragraphs']}. Could not extract paragraph starts.")
        return lines

    start = md_note["line"]      # :::note line
    end = md_note["end_line"]    # ::: closing line
    indent = md_note["indent"]

    # Gather the note's content lines (between ::: fences)
    content_lines = []
    for k in range(start + 1, end):
        content_lines.append((k, lines[k]))

    if not content_lines:
        return lines

    # Join all content into one string to search for paragraph starts
    # We need to map positions in the joined string back to line indices
    joined = ""
    line_boundaries = []  # (start_pos_in_joined, line_idx)
    for line_idx, text in content_lines:
        stripped = text.strip()
        if not stripped:
            # Already a blank line — keep track but skip in search
            line_boundaries.append((len(joined), line_idx, True))
            continue
        if joined:
            joined += " "
        line_boundaries.append((len(joined), line_idx, False))
        joined += stripped

    # Normalize for matching
    joined_norm = re.sub(r'\s+', ' ', joined).strip()

    # Find where each paragraph starts in the joined text
    # Skip para_starts[0] — that's the beginning of the note
    split_positions = []  # char positions in joined_norm where a new paragraph starts
    for ps in para_starts[1:]:
        ps_norm = re.sub(r'\s+', ' ', ps).strip()
        # Try progressively shorter prefixes for fuzzy matching
        found = False
        for trim in range(0, max(0, len(ps_norm) - 10), 5):
            needle = ps_norm[:len(ps_norm) - trim] if trim else ps_norm
            if len(needle) < 10:
                break
            pos = joined_norm.find(needle)
            if pos > 0:
                split_positions.append(pos)
                found = True
                break
        if not found:
            # Try even shorter match (first 15 chars)
            short = ps_norm[:15]
            pos = joined_norm.find(short)
            if pos > 0:
                split_positions.append(pos)
            else:
                _log(f"  [warn] Note at line {start + 1}: could not locate paragraph break "
                      f"for \"{ps_norm[:40]}...\". Manual review recommended.")
                return lines

    if not split_positions:
        return lines

    # Map char positions back to line indices
    # For each split position, find which content line it falls in
    split_line_indices = set()
    for sp in split_positions:
        for i, (char_start, line_idx, is_blank) in enumerate(line_boundaries):
            if is_blank:
                continue
            # Check if this split position falls within this line's text
            # Find the end of this line's contribution in joined_norm
            next_start = len(joined_norm)
            for j in range(i + 1, len(line_boundaries)):
                if not line_boundaries[j][2]:  # not blank
                    next_start = line_boundaries[j][0]
                    break
            if char_start <= sp < next_start:
                # The paragraph break should go before this line
                split_line_indices.add(line_idx)
                break

    if not split_line_indices:
        return lines

    # Now check: if the split point is in the MIDDLE of a line, we need
    # to split that line in two. If it's at the start of a line, just
    # insert a blank line before it.
    # For simplicity and safety, we work from bottom to top.
    fixes = 0
    for line_idx in sorted(split_line_indices, reverse=True):
        line_text = lines[line_idx]
        stripped = line_text.strip()
        prefix = ' ' * indent

        # Find which paragraph start matches within this line
        for sp_idx, sp in enumerate(split_positions):
            ps_norm = re.sub(r'\s+', ' ', para_starts[sp_idx + 1]).strip()
            # Try to find the paragraph start text in this line
            line_norm = re.sub(r'\s+', ' ', stripped)
            short_needle = ps_norm[:20]
            pos_in_line = line_norm.find(short_needle)
            if pos_in_line > 0:
                # Split this line at the paragraph boundary
                # Find the actual position in the original line text
                # Walk through the original stripped text matching normalized chars
                orig = stripped
                orig_pos = _find_orig_pos(orig, pos_in_line)
                if orig_pos is not None and orig_pos > 0:
                    before = prefix + orig[:orig_pos].rstrip()
                    after = prefix + orig[orig_pos:].lstrip()
                    lines[line_idx] = before + "\n" + prefix + "\n" + after
                    fixes += 1
                    break
            elif pos_in_line == 0:
                # Para starts at the beginning of this line — insert blank line before
                if line_idx > start + 1 and lines[line_idx - 1].strip():
                    lines.insert(line_idx, prefix)
                    fixes += 1
                    break

    if fixes:
        # Re-split lines since we may have inserted \n within lines
        result = "\n".join(lines)
        lines[:] = result.splitlines()
        _log(f"  [fix] Note at line {start + 1}: split {fixes} condensed paragraph(s) "
              f"(had {md_note['paragraph_count']}, prod has {prod_note['paragraphs']})")

    return lines


def _find_orig_pos(text: str, normalized_pos: int) -> Optional[int]:
    """Map a position in normalized (single-space) text back to the original text."""
    norm_i = 0
    in_space = False
    for orig_i, ch in enumerate(text):
        if ch in (' ', '\t'):
            if not in_space:
                norm_i += 1
                in_space = True
        else:
            if norm_i >= normalized_pos:
                return orig_i
            norm_i += 1
            in_space = False
    return None


def fix_image_indent(lines: List[str], md_img: dict, prod_img: dict) -> List[str]:
    """Fix an image's indentation based on prod structure."""
    line_idx = md_img["line"]
    current_indent = md_img["indent"]

    if prod_img["in_list"]:
        target_indent = _find_list_cont_indent(lines, line_idx)
        if target_indent is not None and target_indent != current_indent:
            _log(f"  [fix] Image at line {line_idx + 1}: indent {current_indent} -> {target_indent}")
            stripped = lines[line_idx].lstrip(' ')
            lines[line_idx] = ' ' * target_indent + stripped
    else:
        if current_indent > 0:
            _log(f"  [fix] Image at line {line_idx + 1}: indent {current_indent} -> 0 (top-level)")
            lines[line_idx] = lines[line_idx].lstrip(' ')

    return lines


def fix_orphaned_paragraph(lines: List[str], orphan: dict, prod_li_paragraphs: List[dict],
                           prod_top_paragraphs: List[dict] = None) -> List[str]:
    """Fix a paragraph or image that escaped a list item by indenting it."""
    line_idx = orphan["line"]
    kind = orphan.get("kind", "paragraph")
    target_indent = _find_list_cont_indent(lines, line_idx)
    if target_indent is None or target_indent == 0:
        return lines

    current_indent = len(lines[line_idx]) - len(lines[line_idx].lstrip(' '))
    if kind == "image":
        if current_indent != target_indent:
            _log(f"  [fix] Image at line {line_idx + 1}: indent {current_indent} -> {target_indent} (orphaned)")
            lines[line_idx] = ' ' * target_indent + lines[line_idx].lstrip(' ')
        return lines

    orphan_text = _normalize(orphan["text"])
    matched = False
    for lip in prod_li_paragraphs:
        lip_norm = _normalize(lip["text"])
        if lip_norm.startswith(orphan_text[:30]) or orphan_text.startswith(lip_norm[:30]):
            matched = True
            break

    if matched:
        if current_indent != target_indent:
            _log(f"  [fix] Paragraph at line {line_idx + 1}: indent {current_indent} -> {target_indent} "
                  f"(\"{orphan['text'][:50]}...\")")
            lines[line_idx] = ' ' * target_indent + lines[line_idx].lstrip(' ')
        return lines

    if prod_top_paragraphs and current_indent > 0:
        for pp in prod_top_paragraphs:
            pp_norm = _normalize(pp["text"])
            if pp_norm.startswith(orphan_text[:30]) or orphan_text.startswith(pp_norm[:30]):
                _log(f"  [fix] Paragraph at line {line_idx + 1}: indent {current_indent} -> 0 "
                      f"(top-level in prod) (\"{orphan['text'][:50]}...\")")
                lines[line_idx] = lines[line_idx].lstrip(' ')
                return lines

    return lines


def fix_table_indent(lines: List[str], md_table: dict, prod_table: dict) -> List[str]:
    """Fix an HTML table's indentation within a list item.

    Strategy:
    1. Collapse multi-line <pre> blocks into single lines using &#10; entities
       (preserves code display while allowing proper indent)
    2. Re-indent all table lines to the list continuation indent
    3. Pad blank lines to prevent HTML block termination
    """
    if not prod_table["in_list"]:
        return lines

    start = md_table["start_line"]
    end = md_table["end_line"]

    target_indent = _find_list_cont_indent(lines, start)
    if target_indent is None or target_indent == 0:
        return lines

    # Check if any lines actually need fixing
    needs_fix = False
    for k in range(start, min(end + 1, len(lines))):
        line = lines[k]
        if not line.strip():
            # Blank lines inside tables can break HTML blocks
            needs_fix = True
            break
        current = len(line) - len(line.lstrip(' '))
        if current < target_indent:
            needs_fix = True
            break

    if not needs_fix:
        return lines

    # Step 1: Collapse multi-line <pre> blocks into single lines.
    # This replaces newlines within <pre> content with &#10; entities,
    # so code formatting is preserved when rendered while allowing
    # the entire table to be properly indented.
    tbl_lines = lines[start:end + 1]
    collapsed = _collapse_pre_blocks(tbl_lines)

    # Step 2: Re-indent all lines to target_indent
    indent_fixes = 0
    for k, line in enumerate(collapsed):
        current = len(line) - len(line.lstrip(' '))
        if not line.strip():
            # Blank line — pad to prevent HTML block break
            collapsed[k] = ' ' * target_indent
            indent_fixes += 1
        elif current < target_indent:
            collapsed[k] = ' ' * target_indent + line.lstrip(' ')
            indent_fixes += 1

    # Check if anything changed (collapse and/or indent)
    if collapsed != tbl_lines:
        lines[start:end + 1] = collapsed
        pre_collapsed = len(tbl_lines) - len(collapsed)
        parts = []
        if pre_collapsed > 0:
            parts.append(f"collapsed {pre_collapsed} <pre> line(s)")
        if indent_fixes > 0:
            parts.append(f"re-indented {indent_fixes} line(s) to indent {target_indent}")
        _log(f"  [fix] Table at lines {start + 1}-{end + 1}: {', '.join(parts)}")

    return lines


def _collapse_pre_blocks(tbl_lines: List[str]) -> List[str]:
    """Collapse multi-line <pre>...</pre> spans into single lines.

    Replaces real newlines with &#10; entities so code content renders
    correctly while staying on one indentable line.
    """
    pre_open_re = re.compile(r'<pre[\s>]', re.I)
    pre_close_re = re.compile(r'</pre>', re.I)

    result = []
    i = 0
    while i < len(tbl_lines):
        line = tbl_lines[i]

        # Check if this line opens a <pre> without closing it
        if pre_open_re.search(line) and not pre_close_re.search(line):
            # Multi-line <pre> — collapse into one line
            parts = [line.rstrip()]
            j = i + 1
            while j < len(tbl_lines):
                content = tbl_lines[j].rstrip()
                parts.append(content)
                if pre_close_re.search(tbl_lines[j]):
                    break
                j += 1
            # Join with &#10; to represent newlines in HTML
            # First part keeps its indent; subsequent parts are code content
            collapsed_line = parts[0]
            for part in parts[1:]:
                collapsed_line += "&#10;" + part
            result.append(collapsed_line)
            i = j + 1
        else:
            result.append(line)
            i += 1

    return result


def match_tables(md_tables: List[dict], prod_tables: List[dict]) -> List[Tuple[dict, dict]]:
    """Match md tables to prod tables by order."""
    pairs = []
    for i, md_t in enumerate(md_tables):
        if i < len(prod_tables):
            pairs.append((md_t, prod_tables[i]))
    return pairs


def _split_md_table_cells(row_text: str) -> List[str]:
    """Split a markdown table row into cell contents, respecting backtick spans."""
    s = row_text.strip()
    if s.startswith('|'):
        s = s[1:]
    if s.endswith('|'):
        s = s[:-1]

    cells = []
    current: List[str] = []
    in_code = False
    tick_count = 0
    i = 0
    while i < len(s):
        ch = s[i]
        if not in_code and ch == '`':
            j = i
            while j < len(s) and s[j] == '`':
                j += 1
            tick_count = j - i
            in_code = True
            current.append(s[i:j])
            i = j
            continue
        if in_code and ch == '`':
            j = i
            while j < len(s) and s[j] == '`':
                j += 1
            if j - i == tick_count:
                in_code = False
                current.append(s[i:j])
                i = j
                continue
            else:
                current.append(s[i:j])
                i = j
                continue
        if ch == '|' and not in_code:
            cells.append(''.join(current).strip())
            current = []
        else:
            current.append(ch)
        i += 1
    if current:
        cells.append(''.join(current).strip())
    return cells


def _md_cell_to_html(cell: str) -> str:
    """Convert markdown inline formatting in a table cell to HTML."""
    if not cell:
        return ""
    result = cell
    # Bold **text** → <strong>text</strong>
    result = re.sub(r'\*\*(.*?)\*\*', r'<strong>\1</strong>', result)

    # Inline code: handle ``` first, then `
    def _code_tag(code_text: str) -> str:
        if len(code_text) >= CODE_IN_TABLE_THRESHOLD:
            return f"<pre><code>{code_text}</code></pre>"
        return f"<code>{code_text}</code>"

    result = re.sub(r'```\s*(.*?)\s*```', lambda m: _code_tag(m.group(1)), result)
    result = re.sub(r'`([^`]+)`', lambda m: _code_tag(m.group(1).strip()), result)
    return result


def _clean_cell_html(td: Tag) -> str:
    """Convert a prod table cell to clean HTML, preserving structure tags.

    Searches the cell tree for meaningful content elements (<strong>, <pre>,
    <code>, <em>, <a>) while skipping UI chrome (buttons, styled wrappers).
    """
    _SKIP_TAGS = {"button", "svg", "path", "style", "script"}
    _WRAPPER_TAGS = {"div", "span", "p", "li", "td", "th"}

    parts: List[str] = []

    def _walk(node: Tag):
        for child in node.children:
            if isinstance(child, NavigableString):
                text = str(child).strip()
                if text:
                    parts.append(text)
            elif child.name in _SKIP_TAGS:
                continue
            elif child.name in ("strong", "b"):
                parts.append(f"<strong>{child.get_text()}</strong>")
            elif child.name in ("em", "i"):
                parts.append(f"<em>{child.get_text()}</em>")
            elif child.name == "pre":
                code = child.find("code")
                if code:
                    parts.append(f"<pre><code>{code.get_text()}</code></pre>")
                else:
                    parts.append(f"<pre>{child.get_text()}</pre>")
            elif child.name == "code":
                parts.append(f"<code>{child.get_text()}</code>")
            elif child.name == "a":
                href = child.get("href", "")
                parts.append(f'<a href="{href}">{child.get_text()}</a>')
            elif child.name in _WRAPPER_TAGS:
                _walk(child)

    _walk(td)
    return "".join(parts)


def _clean_table_html(table_tag: Tag, indent: int) -> List[str]:
    """Build clean, indented HTML lines from a prod BeautifulSoup table element."""
    prefix = ' ' * indent
    html = [f"{prefix}<table>", f"{prefix} <tbody>"]
    for tr in table_tag.find_all("tr"):
        html.append(f"{prefix}  <tr>")
        for td in tr.find_all(["td", "th"]):
            cell = _clean_cell_html(td)
            html.append(f"{prefix}   <td>{cell}</td>")
        html.append(f"{prefix}  </tr>")
    html.append(f"{prefix} </tbody>")
    html.append(f"{prefix}</table>")
    return html


def _match_pipe_table_to_prod(lines: List[str], md_pipe_table: dict,
                               prod_tables: List[dict]) -> Optional[dict]:
    """Find the prod table that matches a markdown pipe table by text content."""
    start = md_pipe_table["start_line"]
    end = md_pipe_table["end_line"]
    md_texts = []
    for k in range(start, end + 1):
        if k == start + 1:
            continue
        cells = _split_md_table_cells(lines[k])
        if k == start and all(not c for c in cells):
            continue
        for c in cells:
            clean = re.sub(r'`{1,3}', '', c)
            clean = re.sub(r'\*\*', '', clean)
            clean = re.sub(r'\s+', ' ', clean).strip()
            if clean:
                md_texts.append(clean[:40])

    if not md_texts:
        return None

    best = None
    best_score = 0
    for pt in prod_tables:
        el = pt.get("element")
        if not el:
            continue
        prod_text = re.sub(r'\s+', ' ', el.get_text(strip=True))
        score = sum(1 for t in md_texts if t in prod_text)
        if score > best_score:
            best_score = score
            best = pt

    if best and best_score >= max(1, len(md_texts) // 2):
        return best
    return None


def fix_md_pipe_table_to_html(lines: List[str], md_pipe_table: dict,
                               prod_table: Optional[dict] = None) -> List[str]:
    """Convert a markdown pipe table with long inline code to an HTML table.

    If a matching prod_table is provided, its HTML is used as the source of
    truth (preserving line breaks in code blocks).  Otherwise falls back to
    converting the markdown cells directly.
    """
    start = md_pipe_table["start_line"]
    end = md_pipe_table["end_line"]
    indent = md_pipe_table["indent"]

    if prod_table and prod_table.get("element"):
        html_lines = _clean_table_html(prod_table["element"], indent)
        lines[start:end + 1] = html_lines
        _log(f"  [fix] Markdown table at lines {start + 1}-{end + 1}: "
             f"converted to HTML table (from prod)")
        return lines

    prefix = ' ' * indent
    rows = []
    for k in range(start, end + 1):
        if k == start + 1:
            continue
        cells = _split_md_table_cells(lines[k])
        if k == start and all(not c for c in cells):
            continue
        rows.append(cells)

    if not rows:
        return lines

    html_lines = [f"{prefix}<table>"]
    html_lines.append(f"{prefix} <tbody>")
    for row in rows:
        html_lines.append(f"{prefix}  <tr>")
        for cell in row:
            html_content = _md_cell_to_html(cell)
            html_lines.append(f"{prefix}   <td>{html_content}</td>")
        html_lines.append(f"{prefix}  </tr>")
    html_lines.append(f"{prefix} </tbody>")
    html_lines.append(f"{prefix}</table>")

    lines[start:end + 1] = html_lines
    _log(f"  [fix] Markdown table at lines {start + 1}-{end + 1}: "
         f"converted to HTML table (from markdown, no prod match)")
    return lines


def _find_list_cont_indent(lines: List[str], target_line: int) -> Optional[int]:
    """Find the continuation indent for the list item preceding target_line."""
    list_head = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    for k in range(target_line - 1, -1, -1):
        if not lines[k].strip():
            continue
        m = list_head.match(lines[k])
        if m:
            base = len(m.group(1))
            marker_len = len(m.group(2)) + 1
            return base + marker_len
    return None


