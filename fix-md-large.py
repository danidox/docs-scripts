# Guide crawling
# ---------------------------------------------------------------------------

def discover_guide_urls(start_url: str, max_pages: int = 0) -> List[str]:
    """Crawl a guide starting from start_url and return all page URLs.

    Args:
        max_pages: Stop discovering after this many pages (0 = unlimited).
    """
    parsed = urllib.parse.urlparse(start_url)
    # Base = everything up to (but not including) the last segment
    path_parts = parsed.path.rstrip("/").rsplit("/", 1)
    base_url = parsed.scheme + "://" + parsed.netloc + path_parts[0] + "/"

    visited = set()
    queue = [start_url]
    ordered = []

    _log(f"Discovering guide pages under: {base_url}")
    if max_pages > 0:
        _log(f"  (limited to {max_pages} page(s))")

    while queue:
        if max_pages > 0 and len(ordered) >= max_pages:
            break

        url = urllib.parse.urldefrag(queue.pop(0))[0]
        if url in visited:
            continue
        visited.add(url)
        ordered.append(url)

        _, soup = fetch_page(url)
        if not soup:
            continue

        time.sleep(THROTTLE_SECONDS)

        for a in soup.find_all("a", href=True):
            href = a["href"]
            if href.startswith("#") or href.startswith("mailto:"):
                continue
            abs_url = urllib.parse.urldefrag(urllib.parse.urljoin(url, href))[0]
            if abs_url.startswith(base_url) and abs_url not in visited:
                queue.append(abs_url)

    _log(f"  Found {len(ordered)} page(s)\n")
    return ordered


def _url_file_name(start_url: str) -> str:
    """Derive a URL list filename from a guide URL."""
    parsed = urllib.parse.urlparse(start_url)
    parts = [s for s in parsed.path.strip("/").split("/") if s]
    slug = "-".join(parts[:2]) if len(parts) >= 2 else "guide"
    return f"urls-{slug}.txt"


def save_url_list(urls: List[str], path: str) -> None:
    """Write discovered URLs to a text file, one per line."""
    with open(path, "w", encoding="utf-8") as f:
        for url in urls:
            f.write(url + "\n")
    _log(f"  Saved {len(urls)} URL(s) to: {path}")


def load_url_list(path: str) -> List[str]:
    """Read URLs from a text file (one per line, ignores blanks/comments)."""
    urls = []
    with open(path, "r", encoding="utf-8") as f:
        for line in f:
            line = line.strip()
            if line and not line.startswith("#"):
                urls.append(line)
    _log(f"  Loaded {len(urls)} URL(s) from: {path}")
    return urls


