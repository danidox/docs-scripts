#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Large-guide fixer for existing .md files generated by md_maker.py.

Same fix logic as fix-md.py but optimised for guides with 100s-1000s of pages:
  - Discover once, save the URL list to a file
  - Fix in fast batches using the cached URL file (no re-crawling)

Fixes four known conversion bugs:
  1. Condensed paragraphs in :::note blocks (single line → multi-paragraph)
  2. :::note blocks at wrong indent (inside list vs. top-level)
  3. Images at wrong indent within list items
  4. HTML tables with broken indent (collapses multi-line <pre> blocks, re-indents)

Workflow for large guides:
  # Step 1 — discover once (slow, crawls every page)
  python fix-md-large.py <any_guide_url> --discover-only

  # Step 2 — fix in fast batches (no crawling)
  python fix-md-large.py --url-file urls-apps-automation-cloud.txt --limit 1-100 --dry-run
  python fix-md-large.py --url-file urls-apps-automation-cloud.txt --limit 101-200 --dry-run
  python fix-md-large.py --url-file urls-apps-automation-cloud.txt --limit 1-100

Single-page mode still works:
  python fix-md-large.py <prod_url>
  python fix-md-large.py <prod_url> --dry-run

Requires: requests, beautifulsoup4
Optional: playwright (for client-rendered pages)
"""

import re
import sys
import os
import time
import argparse
import importlib.util
import urllib.parse
from typing import List, Dict, Tuple, Optional

import requests
from bs4 import BeautifulSoup, Tag, NavigableString

THROTTLE_SECONDS = 1.0
FETCH_RETRIES = 3
FETCH_TIMEOUT = 30
CODE_IN_TABLE_THRESHOLD = 60  # inline code length that triggers md→HTML table conversion

# ---------------------------------------------------------------------------
# Logging — collects all output for the summary report
# ---------------------------------------------------------------------------

_log_lines: List[str] = []
_STRUCT_MODULE = None


def _log(msg: str):
    """Print and record a message."""
    print(msg)
    _log_lines.append(msg)


def _get_struct_module():
    """Load fix-md-struct.py once and reuse it."""
    global _STRUCT_MODULE
    if _STRUCT_MODULE is None:
        script_dir = os.path.dirname(os.path.abspath(__file__))
        struct_path = os.path.join(script_dir, "fix-md-struct.py")
        spec = importlib.util.spec_from_file_location("fix_md_struct_delegate", struct_path)
        if spec is None or spec.loader is None:
            raise RuntimeError(f"Could not load delegate script: {struct_path}")
        module = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(module)
        _STRUCT_MODULE = module
    return _STRUCT_MODULE


# ---------------------------------------------------------------------------
# Prod HTML fetching
# ---------------------------------------------------------------------------

_SESSION: Optional[requests.Session] = None

def _get_session() -> requests.Session:
    global _SESSION
    if _SESSION is None:
        _SESSION = requests.Session()
        _SESSION.headers["User-Agent"] = "UiPathDocsFixer/1.0"
    return _SESSION


def fetch_page(url: str) -> Tuple[Optional[Tag], Optional[BeautifulSoup]]:
    """Fetch a prod page with retries. Returns (container, full_soup)."""
    session = _get_session()
    for attempt in range(1, FETCH_RETRIES + 1):
        try:
            resp = session.get(url, timeout=FETCH_TIMEOUT)
            resp.raise_for_status()
            break
        except requests.RequestException as e:
            if attempt < FETCH_RETRIES:
                wait = attempt * 2
                _log(f"  [retry] Attempt {attempt}/{FETCH_RETRIES} failed for {url}: {e}")
                _log(f"          Retrying in {wait}s...")
                time.sleep(wait)
            else:
                _log(f"  [error] Failed to fetch {url} after {FETCH_RETRIES} attempts: {e}")
                return None, None

    soup = BeautifulSoup(resp.content, "html.parser")
    container = soup.find(id="DocContainer")
    if not container:
        container = soup.find("main") or soup.find("article") or soup.body

    if container and container.get_text(strip=True):
        return container, soup

    # Fallback: Playwright for client-rendered content
    try:
        from playwright.sync_api import sync_playwright
        with sync_playwright() as p:
            browser = p.chromium.launch(headless=True)
            page = browser.new_page()
            page.goto(url, wait_until="networkidle", timeout=30000)
            page.wait_for_selector("#DocContainer", timeout=15000)
            html = page.content()
            browser.close()
        soup_pw = BeautifulSoup(html, "html.parser")
        container_pw = soup_pw.find(id="DocContainer") or soup_pw.body
        return container_pw, soup_pw
    except Exception as e:
        _log(f"  [warn] Playwright fallback failed: {e}")
        return container, soup


def fetch_prod_html(url: str) -> Optional[Tag]:
    """Fetch prod page and return just the content container."""
    container, _ = fetch_page(url)
    return container


