def extract_prod_structure(container: Tag) -> dict:
    """Extract structural info from prod HTML: notes, images, list items, inline paragraphs."""
    structure = {
        "notes": [],       # [{"in_list": bool, "li_depth": int, "text_preview": str}]
        "images": [],      # [{"in_list": bool, "li_depth": int, "src": str}]
        "list_items": [],  # [{"depth": int, "text": str, "has_note": bool}]
        "li_paragraphs": [],  # paragraphs/divs inside <li> that md_maker may orphan
        "tables": [],      # [{"in_list": bool, "li_depth": int, "text_preview": str}]
    }

    ADMON_CLASSES = {"note", "warning", "important", "information", "tip", "danger"}

    # Extract note/admonition positions
    for div in container.find_all("div", class_=True):
        classes = set(div.get("class", []))
        if "note" not in classes and not ADMON_CLASSES & classes:
            continue
        li_parent = div.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        text = div.get_text(strip=True)[:80]
        # Collect direct-child <p> elements (not inside <ul>/<ol>/<li>)
        # to locate paragraph breaks.  <p> inside list items are bullet
        # content, not standalone paragraphs.
        para_starts = []
        direct_p_count = 0
        for p in div.find_all("p"):
            # Check if this <p> is inside a list within the note
            list_ancestor = p.find_parent(["ul", "ol"])
            if list_ancestor and div in list_ancestor.parents:
                continue  # inside a list — skip
            p_text = re.sub(r'\s+', ' ', p.get_text(strip=True))
            if p_text:
                para_starts.append(p_text[:60])
                direct_p_count += 1
        structure["notes"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "text_preview": text,
            "paragraphs": direct_p_count,
            "para_starts": para_starts,
        })

    # Extract image positions — only block-level images (not inline icons).
    # An image is inline if its immediate block-level ancestor (p, li, td)
    # contains significant text besides the image.
    for img in container.find_all("img"):
        src = img.get("src", "")
        # Walk up to the closest block-level element that directly contains this img
        block = img.parent
        while block and block.name not in ("p", "li", "td", "div", "body"):
            block = block.parent
        if block and block.name in ("p", "li"):
            sibling_text = ""
            for sib in block.children:
                if sib is img:
                    continue
                if isinstance(sib, NavigableString):
                    sibling_text += str(sib).strip()
                elif hasattr(sib, "get_text") and sib.name not in ("style", "script"):
                    if not sib.find("img"):
                        sibling_text += sib.get_text(strip=True)
            if len(sibling_text) > 5:
                continue  # inline icon — skip

        li_parent = img.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        structure["images"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "src": src,
        })

    # Extract list items
    for li in container.find_all("li"):
        depth = _li_depth(li)
        # Check if this li directly contains a note
        has_note = any(
            div for div in li.find_all("div", class_=True, recursive=False)
            if {"note"} & set(div.get("class", []))
            or ADMON_CLASSES & set(div.get("class", []))
        )
        # Also check in itemgroup info children
        for info_div in li.find_all("div", class_="itemgroup"):
            if any(
                div for div in info_div.find_all("div", class_=True, recursive=False)
                if ADMON_CLASSES & set(div.get("class", []))
            ):
                has_note = True

        text = _li_text(li)
        structure["list_items"].append({
            "depth": depth,
            "text": text,
            "has_note": has_note,
        })

    # Extract paragraphs/divs inside <li> that may get orphaned by md_maker.
    # These are block children (p, div) of a <li> that contain text and are
    # followed by or preceded by a sub-list.
    for li in container.find_all("li"):
        li_first_text = ""
        # Get the first text content of the li (the list item head)
        for child in li.children:
            if isinstance(child, NavigableString) and child.strip():
                li_first_text = child.strip()[:80]
                break
            if isinstance(child, Tag) and child.name in ("span", "p", "a", "strong", "em", "b"):
                li_first_text = child.get_text(strip=True)[:80]
                break

        for child in li.children:
            if not isinstance(child, Tag):
                continue
            if child.name in ("ul", "ol"):
                continue
            # Skip admonition divs
            cls = child.get("class") if child.attrs else None
            if cls and ADMON_CLASSES & set(cls or []):
                continue
            # Look for block elements with text that are siblings of sub-lists
            if child.name in ("div", "p") and child.get_text(strip=True):
                child_text = re.sub(r'\s+', ' ', child.get_text(strip=True))
                # Skip if this is the same as the first text (it's the li head itself)
                if child_text[:60] == li_first_text[:60]:
                    continue
                structure["li_paragraphs"].append({
                    "li_head": li_first_text,
                    "text": child_text[:120],
                    "li_depth": _li_depth(li),
                })

    # Extract HTML tables
    for table in container.find_all("table"):
        li_parent = table.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        first_row = table.find("tr")
        preview = first_row.get_text(strip=True)[:80] if first_row else ""
        structure["tables"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "text_preview": preview,
            "element": table,
        })

    return structure


def _li_depth(li: Tag) -> int:
    """Count nesting depth of a <li> element."""
    depth = 0
    parent = li.parent
    while parent:
        if parent.name == "li":
            depth += 1
        parent = parent.parent
    return depth


def _li_text(li: Tag) -> str:
    """Get the direct text of a <li>, excluding sub-lists and notes."""
    clone = BeautifulSoup(str(li), "html.parser").find("li")
    if not clone:
        return ""
    for sub in clone.find_all(["ul", "ol"]):
        sub.decompose()
    ADMON = {"note", "warning", "important", "information", "tip", "danger"}
    for div in list(clone.find_all("div")):
        cls = div.get("class") if div.attrs else None
        if cls and ADMON & set(cls):
            div.decompose()
    return re.sub(r'\s+', ' ', clone.get_text(strip=True))[:120]


# ---------------------------------------------------------------------------
# Markdown structure extraction
# ---------------------------------------------------------------------------

def parse_md_structure(lines: List[str]) -> dict:
    """Extract structural info from markdown lines."""
    structure = {
        "notes": [],    # [{"line": int, "indent": int, "in_list": bool, "end_line": int, "paragraph_count": int}]
        "images": [],   # [{"line": int, "indent": int}]
        "list_items": [],
        "orphaned_paragraphs": [],  # paragraphs at indent 0 between list items
        "tables": [],   # [{"start_line": int, "end_line": int, "indent": int, "text_preview": str}]
        "md_pipe_tables": [],  # markdown pipe tables with long code cells
    }

    fence_open = re.compile(r'^(\s*):::(note|tip|warning|danger|info|important|caution)\s*$')
    fence_close = re.compile(r'^(\s*):::\s*$')
    image_re = re.compile(r'^(\s*)!\[')
    list_head = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    code_fence = re.compile(r'^\s*(```|~~~)')

    inside_code = False
    i = 0
    n = len(lines)

    # Track list context
    list_stack = []  # [(indent, marker_len)]

    while i < n:
        line = lines[i]

        if code_fence.match(line):
            inside_code = not inside_code
            i += 1
            continue

        if inside_code:
            i += 1
            continue

        # Track list items
        m_list = list_head.match(line)
        if m_list:
            indent = len(m_list.group(1))
            marker = m_list.group(2)
            # Pop items at same or deeper indent
            while list_stack and list_stack[-1][0] >= indent:
                list_stack.pop()
            list_stack.append((indent, len(marker) + 1))
            structure["list_items"].append({
                "line": i,
                "indent": indent,
                "depth": len(list_stack) - 1,
                "text": line.strip()[:120],
            })

        # Detect ::: note blocks
        m_open = fence_open.match(line)
        if m_open:
            note_indent = len(m_open.group(1))
            start = i
            para_count = 0
            has_content = False
            j = i + 1
            while j < n and not fence_close.match(lines[j]):
                if lines[j].strip():
                    if not has_content or (j > 0 and not lines[j - 1].strip()):
                        para_count += 1
                    has_content = True
                j += 1
            end = j

            in_list = bool(list_stack) and note_indent > 0
            structure["notes"].append({
                "line": start,
                "end_line": end,
                "indent": note_indent,
                "in_list": in_list,
                "paragraph_count": para_count,
                "admonition_type": m_open.group(2),
            })
            i = end + 1
            continue

        # Detect images
        m_img = image_re.match(line)
        if m_img:
            img_indent = len(m_img.group(1))
            in_list = bool(list_stack)
            structure["images"].append({
                "line": i,
                "indent": img_indent,
                "in_list": in_list,
            })

        i += 1

    # Detect HTML <table> blocks
    table_open_re = re.compile(r'^(\s*)<table', re.I)
    table_close_re = re.compile(r'^\s*</table>', re.I)
    in_code_tbl = False
    for i, line in enumerate(lines):
        if code_fence.match(line):
            in_code_tbl = not in_code_tbl
            continue
        if in_code_tbl:
            continue
        m_tbl = table_open_re.match(line)
        if m_tbl:
            tbl_indent = len(m_tbl.group(1))
            # Find the matching </table>
            end_idx = i
            for j in range(i + 1, n):
                if table_close_re.match(lines[j]):
                    end_idx = j
                    break
            # Get text preview from first row
            preview = ""
            for j in range(i, min(end_idx + 1, n)):
                if "<td" in lines[j].lower() or "<th" in lines[j].lower():
                    preview = re.sub(r'<[^>]+>', '', lines[j]).strip()[:80]
                    break
            structure["tables"].append({
                "start_line": i,
                "end_line": end_idx,
                "indent": tbl_indent,
                "text_preview": preview,
            })

    # Detect markdown pipe tables with long inline code cells
    md_pipe_re = re.compile(r'^\s*\|')
    md_sep_re = re.compile(r'^\s*\|([\s:]*-+[\s:]*\|)+\s*$')
    long_code_re = re.compile(r'`{1,3}\s*(.+?)\s*`{1,3}')
    in_code_pipe = False
    i_pipe = 0
    while i_pipe < n:
        ln = lines[i_pipe]
        if code_fence.match(ln):
            in_code_pipe = not in_code_pipe
            i_pipe += 1
            continue
        if in_code_pipe:
            i_pipe += 1
            continue
        if md_pipe_re.match(ln) and i_pipe + 1 < n and md_sep_re.match(lines[i_pipe + 1]):
            tbl_start = i_pipe
            tbl_indent = len(ln) - len(ln.lstrip())
            j = i_pipe + 2
            while j < n and md_pipe_re.match(lines[j]):
                j += 1
            tbl_end = j - 1
            # Check if any cell has long inline code
            has_long_code = False
            for k in range(tbl_start, tbl_end + 1):
                if k == tbl_start + 1:
                    continue  # separator row
                for m in long_code_re.finditer(lines[k]):
                    if len(m.group(1).strip()) >= CODE_IN_TABLE_THRESHOLD:
                        has_long_code = True
                        break
                if has_long_code:
                    break
            if has_long_code:
                structure["md_pipe_tables"].append({
                    "start_line": tbl_start,
                    "end_line": tbl_end,
                    "indent": tbl_indent,
                })
            i_pipe = tbl_end + 1
        else:
            i_pipe += 1

    # Detect orphaned paragraphs: non-blank lines at indent 0 that sit
    # between a list item and its sub-items or the next list item.
    heading_re = re.compile(r'^#{1,6}\s')
    fence_open_admon = re.compile(r'^\s*:::\w+')
    list_head_re = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    code_fence_re = re.compile(r'^\s*(```|~~~)')
    in_code = False

    for i, line in enumerate(lines):
        if code_fence_re.match(line):
            in_code = not in_code
            continue
        if in_code:
            continue
        if not line.strip():
            continue
        # Must be at indent 0, not a list item, heading, fence, image, or table
        if line[0] == ' ' or line[0] == '\t':
            continue
        if list_head_re.match(line):
            continue
        if heading_re.match(line):
            continue
        if fence_open_admon.match(line):
            continue
        if line.startswith('!['):
            continue
        if line.startswith('|'):
            continue
        if line.startswith('---'):
            continue

        # Look backwards: is there a list item before this (skipping blanks)?
        prev_list_line = None
        for k in range(i - 1, -1, -1):
            if not lines[k].strip():
                continue
            if list_head_re.match(lines[k]):
                prev_list_line = k
            break

        if prev_list_line is None:
            continue

        # Look forwards: is there a list item or sub-item after (skipping blanks)?
        next_list_line = None
        for k in range(i + 1, len(lines)):
            if not lines[k].strip():
                continue
            if list_head_re.match(lines[k]):
                next_list_line = k
            break

        if next_list_line is None:
            continue

        # This paragraph is sandwiched between list items — it's orphaned
        text = re.sub(r'\s+', ' ', line.strip())
        structure["orphaned_paragraphs"].append({
            "line": i,
            "text": text[:120],
            "prev_list_line": prev_list_line,
        })

    return structure


