def fix_md_file(md_path: str, prod_url: str, dry_run: bool = False) -> dict:
    """Delegate per-page fixing to fix-md-struct.py."""
    struct_mod = _get_struct_module()
    delegate_log_start = len(struct_mod._log_lines)
