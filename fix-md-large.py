def resolve_md_path(prod_url: str) -> Optional[str]:
    """Derive the local .md file path from a prod URL.

    Example:
      https://docs.uipath.com/apps/automation-cloud/latest/user-guide/apps-feature-availability
      -> apps-feature-availability.md  (search current dir and subdirs)
    """
    import urllib.parse as up
    path = up.urlparse(prod_url).path.rstrip("/")
    slug = path.split("/")[-1] if path else None
    if not slug:
        return None
    filename = f"{slug}.md"

    # 1. Check current directory
    if os.path.isfile(filename):
        return filename

    # 2. Check mirror path (product/delivery/version/guide/slug.md)
    segments = [s for s in path.split("/") if s]
    if len(segments) >= 2:
        mirror = os.path.join(*segments[:-1], filename)
        if os.path.isfile(mirror):
            return mirror

    # 3. Recursive search from cwd
    for root, _dirs, files in os.walk("."):
        if filename in files:
            return os.path.join(root, filename)

    return None


def _guide_report_filename_from_url(prod_url: str) -> str:
    """Build a guide report filename aligned with fix-md-struct.py."""
    parsed = urllib.parse.urlparse(prod_url)
    parts = [segment for segment in parsed.path.strip("/").split("/") if segment]
    if len(parts) >= 4:
        product, delivery, version, publication = parts[:4]
        version = version.replace(".", "-")
        return f"report-{product}-{delivery}-{version}-{publication}.txt"
    slug = (parts[-1] if parts else "guide").replace(".", "-")
    return f"report-{slug}.txt"


def _page_report_filename_from_url(prod_url: str) -> str:
    """Build a single-page report filename from the page slug."""
    parsed = urllib.parse.urlparse(prod_url)
    parts = [segment for segment in parsed.path.strip("/").split("/") if segment]
    slug = "-".join(parts).replace(".", "-") if parts else "page"
    return f"report-{slug}.txt"


def _write_report(report_path: str, fixed_pages: List[dict], summary: str):
    """Write a report file listing only the fixed pages and their details."""
    from datetime import datetime
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(f"fix-md-large.py report — {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write("=" * 60 + "\n\n")
        if not fixed_pages:
            f.write("No files needed fixing.\n")
        else:
            f.write(f"{len(fixed_pages)} file(s) with fixes or warnings:\n\n")
            for page in fixed_pages:
                f.write(f"--- {page['md_path']} ---\n")
                f.write(f"    Prod: {page['url']}\n")
                for line in page["log"]:
                    stripped = line.strip()
                    if (stripped.startswith("[fix]") or stripped.startswith("[warn]")
                            or stripped.startswith("[struct]")
                            or stripped.startswith("MD:")
                            or stripped.startswith("Prod:")):
                        if stripped.startswith("Prod: http"):
                            continue
                        f.write(f"    {stripped}\n")
                f.write("\n")
        f.write(summary + "\n")
    _log(f"  Report written to: {report_path}")


def fix_guide(urls: List[str], guide_slug: str, dry_run: bool = False,
              start_page: int = 0, end_page: int = 0) -> dict:
    """Fix a list of guide URLs.

    Args:
        urls: Full list of guide URLs (from discovery or URL file).
        guide_slug: Short name for report filenames.
        start_page: 1-based start index (0 = from beginning).
        end_page: 1-based end index (0 = no limit).
    """
    # Slice to the requested range
    total_urls = len(urls)
    if end_page > 0:
        urls = urls[:end_page]
    if start_page > 0:
        urls = urls[start_page - 1:]

    if start_page > 0 or end_page > 0:
        actual_start = max(start_page, 1)
        actual_end = actual_start + len(urls) - 1
        _log(f"  Processing pages {actual_start}-{actual_end} of {total_urls}\n")

    totals = {"pages": 0, "fixed": 0, "skipped": 0, "errors": 0}
    fixed_pages = []

    for url in urls:
        md_path = resolve_md_path(url)
        if not md_path:
            slug = url.rstrip("/").split("/")[-1]
            _log(f"\n  [skip] No .md file found for {slug}")
            totals["skipped"] += 1
            continue

        totals["pages"] += 1
        stats = fix_md_file(md_path, url, dry_run=dry_run)
        if stats.get("errors"):
            totals["errors"] += 1
        elif sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings")) > 0:
            totals["fixed"] += 1
            fixed_pages.append({"md_path": md_path, "url": url, "log": stats.get("log", [])})
        elif stats.get("structural_warnings", 0) > 0:
            fixed_pages.append({"md_path": md_path, "url": url, "log": stats.get("log", [])})

        time.sleep(THROTTLE_SECONDS)

    summary = (f"Guide summary: {totals['pages']} pages processed, "
               f"{totals['fixed']} fixed, {totals['skipped']} skipped, "
               f"{totals['errors']} errors")
    _log(f"\n{'=' * 50}")
    _log(summary)

    report_path = _guide_report_filename_from_url(urls[0]) if urls else f"report-{guide_slug}.txt"
    _write_report(report_path, fixed_pages, summary)

    return totals


def _parse_limit(limit_str: str) -> Tuple[int, int]:
    """Parse --limit value into (start_page, end_page). Both 0 = no limit."""
    if "-" in limit_str:
        parts = limit_str.split("-", 1)
        return int(parts[0]), int(parts[1])
    return 0, int(limit_str)


def _guide_slug(url_or_path: str) -> str:
    """Derive a short slug from a URL or URL-file path."""
    if url_or_path.startswith("http"):
        parsed = urllib.parse.urlparse(url_or_path)
        parts = [s for s in parsed.path.strip("/").split("/") if s]
        return "-".join(parts[:2]) if len(parts) >= 2 else "guide"
    # From filename like urls-apps-automation-cloud.txt
    base = os.path.splitext(os.path.basename(url_or_path))[0]
    return base.removeprefix("urls-") if base.startswith("urls-") else base


def main():
    parser = argparse.ArgumentParser(
        description="Fix .md conversion bugs — optimised for large guides",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  # Discover all URLs in a guide (saves to urls-*.txt)
  python fix-md-large.py <any_guide_url> --discover-only

  # Fix pages 1-100 from a saved URL file
  python fix-md-large.py --url-file urls-apps-automation-cloud.txt --limit 1-100

  # Fix single page (same as fix-md.py)
  python fix-md-large.py <prod_url>
""",
    )
    parser.add_argument("prod_url", nargs="?", default=None,
                        help="Production URL (docs.uipath.com)")
    parser.add_argument("md_file", nargs="?", default=None,
                        help="Path to the .md file (auto-detected from URL if omitted)")
    parser.add_argument("--discover-only", action="store_true",
                        help="Crawl the guide, save URL list to file, then exit (no fixing)")
    parser.add_argument("--url-file", type=str, default=None,
                        help="Read URLs from a file instead of crawling (use with --limit)")
    parser.add_argument("--crawl", action="store_true",
                        help="Crawl all pages in the guide and fix each one")
    parser.add_argument("--limit", type=str, default="0",
                        help="Pages to process: N (first N) or M-N (range)")
    parser.add_argument("--dry-run", action="store_true",
                        help="Show what would be fixed without modifying files")
    args = parser.parse_args()

    # --- Mode 1: Discover only ---
    if args.discover_only:
        if not args.prod_url:
            parser.error("--discover-only requires a prod_url argument")
        urls = discover_guide_urls(args.prod_url)
        out_path = _url_file_name(args.prod_url)
        save_url_list(urls, out_path)
        return

    # --- Mode 2: Fix from URL file ---
    if args.url_file:
        if not os.path.isfile(args.url_file):
            print(f"Error: URL file not found: {args.url_file}")
            sys.exit(1)
        urls = load_url_list(args.url_file)
        slug = _guide_slug(args.url_file)
        start_page, end_page = _parse_limit(args.limit)
        totals = fix_guide(urls, slug, dry_run=args.dry_run,
                           start_page=start_page, end_page=end_page)
        if totals["errors"]:
            sys.exit(1)
        return

    # --- Mode 3: Crawl + fix (legacy --crawl) ---
    if args.crawl:
        if not args.prod_url:
            parser.error("--crawl requires a prod_url argument")
        start_page, end_page = _parse_limit(args.limit)
        urls = discover_guide_urls(args.prod_url, max_pages=end_page)
        slug = _guide_slug(args.prod_url)
        totals = fix_guide(urls, slug, dry_run=args.dry_run,
                           start_page=start_page, end_page=end_page)
        if totals["errors"]:
            sys.exit(1)
        return

    # --- Mode 4: Single page ---
    if not args.prod_url:
        parser.error("provide a prod_url, --url-file, or --discover-only")

    md_file = args.md_file
    if not md_file:
        md_file = resolve_md_path(args.prod_url)
        if not md_file:
            slug = args.prod_url.rstrip("/").split("/")[-1]
            print(f"Error: could not find {slug}.md in current directory or subdirectories.")
            sys.exit(1)
        print(f"  Resolved .md file: {md_file}")

    if not os.path.isfile(md_file):
        print(f"Error: file not found: {md_file}")
        sys.exit(1)

    stats = fix_md_file(md_file, args.prod_url, dry_run=args.dry_run)

    total_fixes = sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings"))
    struct_warns = stats.get("structural_warnings", 0)
    if total_fixes > 0 or struct_warns > 0:
        report_path = _page_report_filename_from_url(args.prod_url)
        fixed_pages = [{"md_path": md_file, "url": args.prod_url, "log": stats.get("log", [])}]
        parts = []
        if total_fixes > 0:
            parts.append(f"{total_fixes} fix(es) applied")
        if struct_warns > 0:
            parts.append(f"{struct_warns} structural warning(s)")
        summary = "Single page: " + ", ".join(parts)
        _write_report(report_path, fixed_pages, summary)

    if stats.get("errors"):
        sys.exit(1)


if __name__ == "__main__":
    main()
