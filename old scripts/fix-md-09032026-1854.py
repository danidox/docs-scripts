def fix_note_indent(lines: List[str], md_note: dict, prod_note: dict) -> List[str]:
    """Fix a note's indentation based on prod structure."""
    start = md_note["line"]
    end = md_note["end_line"]
    current_indent = md_note["indent"]

    if prod_note["in_list"]:
        # Note should be inside a list item — find the correct indent
        # Look backwards for the parent list item
        target_indent = _find_list_cont_indent(lines, start)
        if target_indent is not None and target_indent != current_indent:
            _log(f"  [fix] Note at line {start + 1}: indent {current_indent} -> {target_indent}")
            for k in range(start, min(end + 1, len(lines))):
                stripped = lines[k].lstrip(' ')
                lines[k] = ' ' * target_indent + stripped
    else:
        # Note should be top-level (indent 0)
        if current_indent > 0:
            _log(f"  [fix] Note at line {start + 1}: indent {current_indent} -> 0 (top-level)")
            for k in range(start, min(end + 1, len(lines))):
                lines[k] = lines[k].lstrip(' ')

    return lines


def fix_note_paragraphs(lines: List[str], md_note: dict, prod_note: dict) -> List[str]:
    """Fix condensed paragraphs in a note block.

    When md_maker strips <p> tags inside admonitions, multiple paragraphs
    get condensed onto one line.  We use the first words of each prod <p>
    to locate paragraph boundaries in the md content and insert blank lines.
    """
    if prod_note["paragraphs"] <= 1:
        return lines
    if md_note["paragraph_count"] >= prod_note["paragraphs"]:
        return lines

    para_starts = prod_note.get("para_starts", [])
    if len(para_starts) < 2:
        _log(f"  [warn] Note at line {md_note['line'] + 1}: has {md_note['paragraph_count']} paragraph(s) "
              f"but prod has {prod_note['paragraphs']}. Could not extract paragraph starts.")
        return lines

    start = md_note["line"]      # :::note line
    end = md_note["end_line"]    # ::: closing line
    indent = md_note["indent"]

    # Gather the note's content lines (between ::: fences)
    content_lines = []
    for k in range(start + 1, end):
        content_lines.append((k, lines[k]))

    if not content_lines:
        return lines

    # Join all content into one string to search for paragraph starts
    # We need to map positions in the joined string back to line indices
    joined = ""
    line_boundaries = []  # (start_pos_in_joined, line_idx)
    for line_idx, text in content_lines:
        stripped = text.strip()
        if not stripped:
            # Already a blank line — keep track but skip in search
            line_boundaries.append((len(joined), line_idx, True))
            continue
        if joined:
            joined += " "
        line_boundaries.append((len(joined), line_idx, False))
        joined += stripped

    # Normalize for matching
    joined_norm = re.sub(r'\s+', ' ', joined).strip()

    # Find where each paragraph starts in the joined text
    # Skip para_starts[0] — that's the beginning of the note
    split_positions = []  # char positions in joined_norm where a new paragraph starts
    for ps in para_starts[1:]:
        ps_norm = re.sub(r'\s+', ' ', ps).strip()
        # Try progressively shorter prefixes for fuzzy matching
        found = False
        for trim in range(0, max(0, len(ps_norm) - 10), 5):
            needle = ps_norm[:len(ps_norm) - trim] if trim else ps_norm
            if len(needle) < 10:
                break
            pos = joined_norm.find(needle)
            if pos > 0:
                split_positions.append(pos)
                found = True
                break
        if not found:
            # Try even shorter match (first 15 chars)
            short = ps_norm[:15]
            pos = joined_norm.find(short)
            if pos > 0:
                split_positions.append(pos)
            else:
                _log(f"  [warn] Note at line {start + 1}: could not locate paragraph break "
                      f"for \"{ps_norm[:40]}...\". Manual review recommended.")
                return lines

    if not split_positions:
        return lines

    # Map char positions back to line indices
    # For each split position, find which content line it falls in
    split_line_indices = set()
    for sp in split_positions:
        for i, (char_start, line_idx, is_blank) in enumerate(line_boundaries):
            if is_blank:
                continue
            # Check if this split position falls within this line's text
            # Find the end of this line's contribution in joined_norm
            next_start = len(joined_norm)
            for j in range(i + 1, len(line_boundaries)):
                if not line_boundaries[j][2]:  # not blank
                    next_start = line_boundaries[j][0]
                    break
            if char_start <= sp < next_start:
                # The paragraph break should go before this line
                split_line_indices.add(line_idx)
                break

    if not split_line_indices:
        return lines

    # Now check: if the split point is in the MIDDLE of a line, we need
    # to split that line in two. If it's at the start of a line, just
    # insert a blank line before it.
    # For simplicity and safety, we work from bottom to top.
    fixes = 0
    for line_idx in sorted(split_line_indices, reverse=True):
        line_text = lines[line_idx]
        stripped = line_text.strip()
        prefix = ' ' * indent

        # Find which paragraph start matches within this line
        for sp_idx, sp in enumerate(split_positions):
            ps_norm = re.sub(r'\s+', ' ', para_starts[sp_idx + 1]).strip()
            # Try to find the paragraph start text in this line
            line_norm = re.sub(r'\s+', ' ', stripped)
            short_needle = ps_norm[:20]
            pos_in_line = line_norm.find(short_needle)
            if pos_in_line > 0:
                # Split this line at the paragraph boundary
                # Find the actual position in the original line text
                # Walk through the original stripped text matching normalized chars
                orig = stripped
                orig_pos = _find_orig_pos(orig, pos_in_line)
                if orig_pos is not None and orig_pos > 0:
                    before = prefix + orig[:orig_pos].rstrip()
                    after = prefix + orig[orig_pos:].lstrip()
                    lines[line_idx] = before + "\n" + prefix + "\n" + after
                    fixes += 1
                    break
            elif pos_in_line == 0:
                # Para starts at the beginning of this line — insert blank line before
                if line_idx > start + 1 and lines[line_idx - 1].strip():
                    lines.insert(line_idx, prefix)
                    fixes += 1
                    break

    if fixes:
        # Re-split lines since we may have inserted \n within lines
        result = "\n".join(lines)
        lines[:] = result.splitlines()
        _log(f"  [fix] Note at line {start + 1}: split {fixes} condensed paragraph(s) "
              f"(had {md_note['paragraph_count']}, prod has {prod_note['paragraphs']})")

    return lines


def _find_orig_pos(text: str, normalized_pos: int) -> Optional[int]:
    """Map a position in normalized (single-space) text back to the original text."""
    norm_i = 0
    in_space = False
    for orig_i, ch in enumerate(text):
        if ch in (' ', '\t'):
            if not in_space:
                norm_i += 1
                in_space = True
        else:
            if norm_i >= normalized_pos:
                return orig_i
            norm_i += 1
            in_space = False
    return None


