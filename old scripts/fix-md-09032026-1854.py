def compare_structure(md: dict, prod: dict) -> List[str]:
    """Compare MD and prod structural elements, returning warning messages.

    Detects:
      - Heading count and text mismatches
      - Table count mismatches
      - Table column header and row count differences
      - Top-level paragraph text differences
    """
    warnings: List[str] = []

    # --- Headings ---
    # Prod headings are leveled-down in MD:
    #   prod h2 = MD frontmatter title
    #   prod h3 = MD h2, prod h4 = MD h3, etc.
    md_headings = md.get("headings", [])
    prod_headings = prod.get("headings", [])
    fm_title = md.get("frontmatter_title", "")

    # Separate prod h2 (should match frontmatter title) from subheadings
    prod_h2s = [h for h in prod_headings if h["level"] == 2]
    prod_sub = [h for h in prod_headings if h["level"] > 2]

    # Check prod h2 vs frontmatter title
    for ph2 in prod_h2s:
        ph2_text = re.sub(r'\s+', ' ', ph2["text"].strip().lower())
        fm_text = re.sub(r'\s+', ' ', fm_title.strip().lower())
        if fm_text and ph2_text != fm_text:
            warnings.append(
                f"  [struct] Page title differs: "
                f"MD frontmatter \"{fm_title[:40]}\" vs prod h2 \"{ph2['text'][:40]}\"")
        elif not fm_text:
            warnings.append(
                f"  [struct] Prod has h2 \"{ph2['text'][:40]}\" but MD has no frontmatter title")

    # Compare prod subheadings (h3+) with MD headings (level-shifted by -1)
    if len(md_headings) != len(prod_sub):
        warnings.append(
            f"  [struct] Heading count: MD has {len(md_headings)}, "
            f"prod has {len(prod_sub)} (excluding page title h2)")
        if md_headings:
            md_h_list = ", ".join(f'h{h["level"]}: "{h["text"][:40]}"' for h in md_headings)
            warnings.append(f"           MD headings: {md_h_list}")
        if prod_sub:
            prod_h_list = ", ".join(f'h{h["level"]}: "{h["text"][:40]}"' for h in prod_sub)
            warnings.append(f"           Prod headings: {prod_h_list}")
    else:
        for i, (md_h, prod_h) in enumerate(zip(md_headings, prod_sub)):
            # Expected: prod level - 1 = MD level (prod h3 → MD h2)
            expected_md_level = prod_h["level"] - 1
            if md_h["level"] != expected_md_level:
                warnings.append(
                    f"  [struct] Heading {i+1} level: MD h{md_h['level']} vs "
                    f"expected h{expected_md_level} (prod h{prod_h['level']}) "
                    f"(\"{md_h['text'][:40]}\")")
            md_text = re.sub(r'\s+', ' ', md_h["text"].strip().lower())
            prod_text = re.sub(r'\s+', ' ', prod_h["text"].strip().lower())
            if md_text != prod_text:
                warnings.append(
                    f"  [struct] Heading {i+1} text differs: "
                    f"MD \"{md_h['text'][:40]}\" vs prod \"{prod_h['text'][:40]}\"")

    # --- Tables ---
    md_tables = md.get("tables", [])
    prod_tables = prod.get("tables", [])
    if len(md_tables) != len(prod_tables):
        warnings.append(
            f"  [struct] Table count: MD has {len(md_tables)}, "
            f"prod has {len(prod_tables)}")
    # Compare matched tables
    for i, (md_t, prod_t) in enumerate(zip(md_tables, prod_tables)):
        md_cols = md_t.get("col_count", 0)
        prod_cols = prod_t.get("col_count", 0)
        if md_cols != prod_cols:
            warnings.append(
                f"  [struct] Table {i+1} columns: MD has {md_cols}, "
                f"prod has {prod_cols}")
        md_rows = md_t.get("row_count", 0)
        prod_rows = prod_t.get("row_count", 0)
        if md_rows != prod_rows:
            warnings.append(
                f"  [struct] Table {i+1} rows: MD has {md_rows}, "
                f"prod has {prod_rows}")
        # Compare headers
        md_hdrs = md_t.get("headers", [])
        prod_hdrs = prod_t.get("headers", [])
        for j, (mh, ph) in enumerate(zip(md_hdrs, prod_hdrs)):
            mh_norm = re.sub(r'\s+', ' ', mh.strip().lower())
            ph_norm = re.sub(r'\s+', ' ', ph.strip().lower())
            if mh_norm != ph_norm:
                warnings.append(
                    f"  [struct] Table {i+1} header col {j+1}: "
                    f"MD \"{mh[:40]}\" vs prod \"{ph[:40]}\"")

    # --- Top-level paragraphs ---
    md_paras = md.get("paragraphs", [])
    prod_paras = prod.get("paragraphs", [])

    def _norm_para(text: str) -> str:
        """Normalize paragraph text for comparison — strip markdown/HTML formatting."""
        t = re.sub(r'\[([^\]]*)\]\([^)]*\)', r'\1', text)   # [text](url) → text
        t = re.sub(r'\*\*([^*]*)\*\*', r'\1', t)            # **bold** → bold
        t = re.sub(r'`([^`]*)`', r'\1', t)                  # `code` → code
        t = re.sub(r'<[^>]+>', '', t)                        # strip HTML tags
        t = re.sub(r'\s+', '', t.strip().lower())            # collapse all whitespace
        return t

    if len(md_paras) != len(prod_paras):
        warnings.append(
            f"  [struct] Paragraph count: MD has {len(md_paras)}, "
            f"prod has {len(prod_paras)}")

    # Content-based matching: for each MD paragraph, find best match in prod.
    # This avoids cascading mismatches when counts differ by 1-2.
    prod_norms = [_norm_para(p["text"]) for p in prod_paras]
    prod_matched = [False] * len(prod_paras)
    md_unmatched = []
    for md_p in md_paras:
        md_norm = _norm_para(md_p["text"])
        found = False
        for j, pn in enumerate(prod_norms):
            if not prod_matched[j] and md_norm[:30] == pn[:30]:
                prod_matched[j] = True
                # Check for longer-range text differences
                if md_norm[:80] != pn[:80]:
                    warnings.append(
                        f"  [struct] Paragraph text differs:")
                    warnings.append(
                        f"           MD:   \"{md_p['text'][:60]}...\"")
                    warnings.append(
                        f"           Prod: \"{prod_paras[j]['text'][:60]}...\"")
                found = True
                break
        if not found:
            md_unmatched.append(md_p)

    prod_unmatched = [prod_paras[j] for j in range(len(prod_paras))
                      if not prod_matched[j]]

    for md_p in md_unmatched:
        warnings.append(
            f"  [struct] Paragraph in MD not found in prod: "
            f"\"{md_p['text'][:60]}...\"")
    for prod_p in prod_unmatched:
        warnings.append(
            f"  [struct] Paragraph in prod not found in MD: "
            f"\"{prod_p['text'][:60]}...\"")

    return warnings


def fix_md_file(md_path: str, prod_url: str, dry_run: bool = False) -> dict:
    """
    Fix known conversion issues in an existing .md file.

    Returns a dict with counts of fixes applied and a 'log' key
    containing the messages emitted for this page.
    """
    # Snapshot the log so we can capture this page's messages
    log_start = len(_log_lines)

    _log(f"\nFixing: {md_path}")
    _log(f"  Prod: {prod_url}")

    # Read the .md file
    with open(md_path, "r", encoding="utf-8") as f:
        original = f.read()
    lines = original.splitlines()

    # Fetch and parse prod HTML
    _log("  Fetching prod HTML...")
    container = fetch_prod_html(prod_url)
    if not container:
        _log("  [error] Could not fetch prod HTML. Aborting.")
        return {"errors": 1}

    prod = extract_prod_structure(container)
    md = parse_md_structure(lines)

    stats = {"note_indent": 0, "note_paragraphs": 0, "image_indent": 0,
             "orphaned_para": 0, "table_indent": 0, "md_table_to_html": 0,
             "errors": 0}

    # Fix orphaned paragraphs first (before other fixes shift line numbers)
    for orphan in reversed(md["orphaned_paragraphs"]):
        old_lines = lines[:]
        lines = fix_orphaned_paragraph(lines, orphan, prod["li_paragraphs"])
        if lines != old_lines:
            stats["orphaned_para"] += 1

    # Re-parse after orphan fixes (line numbers may have shifted due to indent changes)
    md = parse_md_structure(lines)

    # Fix notes
    note_pairs = match_notes(md["notes"], prod["notes"])
    for md_note, prod_note in reversed(note_pairs):  # reversed to preserve line numbers
        old_lines = lines[:]
        lines = fix_note_indent(lines, md_note, prod_note)
        if lines != old_lines:
            stats["note_indent"] += 1
        old_lines = lines[:]
        lines = fix_note_paragraphs(lines, md_note, prod_note)
        if lines != old_lines:
            stats["note_paragraphs"] += 1

    # Fix images
    img_pairs = match_images(md["images"], prod["images"])
    for md_img, prod_img in reversed(img_pairs):
        old_lines = lines[:]
        lines = fix_image_indent(lines, md_img, prod_img)
        if lines != old_lines:
            stats["image_indent"] += 1

    # Fix tables
    # Re-parse to get fresh line numbers after previous fixes
    md = parse_md_structure(lines)
    tbl_pairs = match_tables(md["tables"], prod["tables"])
    for md_tbl, prod_tbl in reversed(tbl_pairs):
        old_lines = lines[:]
        lines = fix_table_indent(lines, md_tbl, prod_tbl)
        if lines != old_lines:
            stats["table_indent"] += 1

    # Convert markdown pipe tables with long code to HTML
    # Re-parse to pick up md_pipe_tables with fresh line numbers
    md = parse_md_structure(lines)
    for md_pipe_tbl in reversed(md["md_pipe_tables"]):
        old_lines = lines[:]
        prod_match = _match_pipe_table_to_prod(lines, md_pipe_tbl, prod["tables"])
        lines = fix_md_pipe_table_to_html(lines, md_pipe_tbl, prod_match)
        if lines != old_lines:
            stats["md_table_to_html"] += 1

    # Structural comparison — detect mismatches between MD and prod
    md_final = parse_md_structure(lines)
    struct_warnings = compare_structure(md_final, prod)
    stats["structural_warnings"] = len(struct_warnings)
    if struct_warnings:
        _log("")
        for w in struct_warnings:
            _log(w)

    # Write fixes
    result = "\n".join(lines)
    total_fixes = sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings"))
    if result != original:
        if dry_run:
            _log(f"\n  [dry-run] Would fix: {total_fixes} issues")
        else:
            with open(md_path, "w", encoding="utf-8") as f:
                f.write(result)
            _log(f"\n  Applied {total_fixes} fix(es). File updated.")
    else:
        if struct_warnings:
            _log(f"\n  No auto-fixes needed, but {len(struct_warnings)} structural warning(s) found.")
        else:
            _log("\n  No fixes needed.")

    stats["log"] = _log_lines[log_start:]
    return stats


