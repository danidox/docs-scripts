def resolve_md_path(prod_url: str) -> Optional[str]:
    """Derive the local .md file path from a prod URL.

    Example:
      https://docs.uipath.com/apps/automation-cloud/latest/user-guide/apps-feature-availability
      -> apps-feature-availability.md  (search current dir and subdirs)
    """
    import urllib.parse as up
    path = up.urlparse(prod_url).path.rstrip("/")
    slug = path.split("/")[-1] if path else None
    if not slug:
        return None
    filename = f"{slug}.md"

    # 1. Check current directory
    if os.path.isfile(filename):
        return filename

    # 2. Check mirror path (product/delivery/version/guide/slug.md)
    segments = [s for s in path.split("/") if s]
    if len(segments) >= 2:
        mirror = os.path.join(*segments[:-1], filename)
        if os.path.isfile(mirror):
            return mirror

    # 3. Recursive search from cwd
    for root, _dirs, files in os.walk("."):
        if filename in files:
            return os.path.join(root, filename)

    return None


def _write_report(report_path: str, fixed_pages: List[dict], summary: str):
    """Write a report file listing only the fixed pages and their details."""
    from datetime import datetime
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(f"fix-md.py report — {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write("=" * 60 + "\n\n")
        if not fixed_pages:
            f.write("No files needed fixing.\n")
        else:
            f.write(f"{len(fixed_pages)} file(s) with fixes or warnings:\n\n")
            for page in fixed_pages:
                f.write(f"--- {page['md_path']} ---\n")
                f.write(f"    Prod: {page['url']}\n")
                for line in page["log"]:
                    # Include [fix], [warn], and [struct] lines + continuation lines
                    stripped = line.strip()
                    if (stripped.startswith("[fix]") or stripped.startswith("[warn]")
                            or stripped.startswith("[struct]")
                            or stripped.startswith("MD headings:") or stripped.startswith("Prod headings:")
                            or stripped.startswith("MD:") or stripped.startswith("Prod:")):
                        # Skip the "Prod: https://..." info line (already shown above)
                        if stripped.startswith("Prod: http"):
                            continue
                        f.write(f"    {stripped}\n")
                f.write("\n")
        f.write(summary + "\n")
    _log(f"  Report written to: {report_path}")


def fix_guide(start_url: str, dry_run: bool = False,
              start_page: int = 0, end_page: int = 0) -> dict:
    """Crawl all pages in a guide and fix each corresponding .md file.

    Args:
        start_page: 1-based start index (0 = from beginning).
        end_page: 1-based end index (0 = no limit).
    """
    # Discover up to end_page pages (or all if 0)
    urls = discover_guide_urls(start_url, max_pages=end_page)
    # Slice to the requested range
    if start_page > 0:
        urls = urls[start_page - 1:]
        _log(f"  Processing pages {start_page}-{start_page + len(urls) - 1}\n")

    totals = {"pages": 0, "fixed": 0, "skipped": 0, "errors": 0}
    fixed_pages = []

    for url in urls:
        md_path = resolve_md_path(url)
        if not md_path:
            slug = url.rstrip("/").split("/")[-1]
            _log(f"\n  [skip] No .md file found for {slug}")
            totals["skipped"] += 1
            continue

        totals["pages"] += 1
        stats = fix_md_file(md_path, url, dry_run=dry_run)
        if stats.get("errors"):
            totals["errors"] += 1
        elif sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings")) > 0:
            totals["fixed"] += 1
            fixed_pages.append({"md_path": md_path, "url": url, "log": stats.get("log", [])})
        elif stats.get("structural_warnings", 0) > 0:
            # Include pages with only structural warnings in the report
            fixed_pages.append({"md_path": md_path, "url": url, "log": stats.get("log", [])})

        time.sleep(THROTTLE_SECONDS)

    summary = (f"Guide summary: {totals['pages']} pages processed, "
               f"{totals['fixed']} fixed, {totals['skipped']} skipped, "
               f"{totals['errors']} errors")
    _log(f"\n{'=' * 50}")
    _log(summary)

    # Derive report filename from guide slug
    parsed = urllib.parse.urlparse(start_url)
    path_parts = [s for s in parsed.path.strip("/").split("/") if s]
    guide_slug = "-".join(path_parts[:2]) if len(path_parts) >= 2 else "guide"
    report_path = f"~fix-md-report-{guide_slug}.txt"
    _write_report(report_path, fixed_pages, summary)

    return totals


def main():
    parser = argparse.ArgumentParser(
        description="Fix known conversion bugs in .md files generated by md_maker.py"
    )
    parser.add_argument("prod_url", help="Production URL (docs.uipath.com)")
    parser.add_argument("md_file", nargs="?", default=None,
                        help="Path to the .md file (auto-detected from URL if omitted)")
    parser.add_argument("--crawl", action="store_true",
                        help="Crawl all pages in the guide and fix each one")
    parser.add_argument("--limit", type=str, default="0",
                        help="Pages to process: N (first N) or M-N (range). E.g. --limit 10, --limit 10-20")
    parser.add_argument("--dry-run", action="store_true",
                        help="Show what would be fixed without modifying the file")
    args = parser.parse_args()

    if args.crawl:
        # Parse --limit: "0" = all, "N" = first N, "M-N" = range
        start_page, end_page = 0, 0
        if "-" in args.limit:
            parts = args.limit.split("-", 1)
            start_page, end_page = int(parts[0]), int(parts[1])
        else:
            end_page = int(args.limit)
        totals = fix_guide(args.prod_url, dry_run=args.dry_run,
                           start_page=start_page, end_page=end_page)
        if totals["errors"]:
            sys.exit(1)
        return

    md_file = args.md_file
    if not md_file:
        md_file = resolve_md_path(args.prod_url)
        if not md_file:
            slug = args.prod_url.rstrip("/").split("/")[-1]
            print(f"Error: could not find {slug}.md in current directory or subdirectories.")
            sys.exit(1)
        print(f"  Resolved .md file: {md_file}")

    if not os.path.isfile(md_file):
        print(f"Error: file not found: {md_file}")
        sys.exit(1)

    stats = fix_md_file(md_file, args.prod_url, dry_run=args.dry_run)

    # Write report for single-page mode too
    total_fixes = sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings"))
    struct_warns = stats.get("structural_warnings", 0)
    if total_fixes > 0 or struct_warns > 0:
        slug = args.prod_url.rstrip("/").split("/")[-1]
        report_path = f"~fix-md-report-{slug}.txt"
        fixed_pages = [{"md_path": md_file, "url": args.prod_url, "log": stats.get("log", [])}]
        parts = []
        if total_fixes > 0:
            parts.append(f"{total_fixes} fix(es) applied")
        if struct_warns > 0:
            parts.append(f"{struct_warns} structural warning(s)")
        summary = f"Single page: {', '.join(parts)}"
        _write_report(report_path, fixed_pages, summary)

    if stats.get("errors"):
        sys.exit(1)


if __name__ == "__main__":
    main()
