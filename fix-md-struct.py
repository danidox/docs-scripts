def resolve_md_path(prod_url: str, search_root: Optional[str] = None) -> Optional[str]:
    """Derive the local .md file path from a prod URL.

    Example:
      https://docs.uipath.com/apps/automation-cloud/latest/user-guide/apps-feature-availability
      -> apps-feature-availability.md  (search current dir and subdirs)
    """
    import urllib.parse as up
    path = up.urlparse(prod_url).path.rstrip("/")
    slug = path.split("/")[-1] if path else None
    if not slug:
        return None
    filename = f"{slug}.md"

    candidate_roots = []
    if search_root:
        candidate_roots.append(search_root)
    candidate_roots.append(".")

    for root in candidate_roots:
        direct = os.path.join(root, filename) if root not in (".", "") else filename
        if os.path.isfile(direct):
            return direct

    # Check mirror path (product/delivery/version/guide/slug.md)
    segments = [s for s in path.split("/") if s]
    if len(segments) >= 2:
        mirror = os.path.join(*segments[:-1], filename)
        if os.path.isfile(mirror):
            return mirror

    # Recursive search, preferring the known publication subtree when provided.
    walked_roots = []
    if search_root:
        walked_roots.append(search_root)
    walked_roots.append(".")
    seen_roots = set()
    for walk_root in walked_roots:
        norm_root = os.path.abspath(walk_root)
        if norm_root in seen_roots or not os.path.isdir(walk_root):
            continue
        seen_roots.add(norm_root)
        for root, _dirs, files in os.walk(walk_root):
            if filename in files:
                return os.path.join(root, filename)

    return None


def _resolve_report_path(filename: str) -> str:
    """Save report files under a dedicated Desktop folder, with a cwd fallback."""
    desktop_candidates = []

    onedrive = os.getenv("OneDrive")
    if onedrive:
        desktop_candidates.append(os.path.join(onedrive, "Desktop"))

    userprofile = os.getenv("USERPROFILE")
    if userprofile:
        desktop_candidates.append(os.path.join(userprofile, "Desktop"))

    desktop_candidates.append(os.path.join(os.path.expanduser("~"), "Desktop"))

    report_folder_name = "fix-md-struct reports"
    for desktop in desktop_candidates:
        if not desktop or not os.path.isdir(desktop):
            continue
        report_dir = os.path.join(desktop, report_folder_name)
        os.makedirs(report_dir, exist_ok=True)
        return os.path.join(report_dir, filename)

    report_dir = os.path.abspath(report_folder_name)
    os.makedirs(report_dir, exist_ok=True)
    return os.path.join(report_dir, filename)


def _write_report(report_path: str, fixed_pages: List[dict], summary: str):
    """Write a report file listing only the fixed pages and their details."""
    from datetime import datetime
    with open(report_path, "w", encoding="utf-8") as f:
        f.write(f"fix-md-struct.py report â€” {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write("=" * 60 + "\n\n")
        if not fixed_pages:
            f.write("No files needed fixing.\n")
        else:
            f.write(f"{len(fixed_pages)} file(s) with fixes or warnings:\n\n")
            for page in fixed_pages:
                f.write(f"--- {page['md_path']} ---\n")
                f.write(f"    Prod: {page['url']}\n")
                page_stats = page.get("stats", {})
                page_fix_count = sum(
                    v for k, v in page_stats.items()
                    if k not in ("errors", "log", "structural_warnings")
                )
                page_struct_warnings = page_stats.get("structural_warnings", 0)
                if page_fix_count or page_struct_warnings:
                    summary_bits = []
                    if page_fix_count:
                        summary_bits.append(f"{page_fix_count} fix(es)")
                    if page_struct_warnings:
                        summary_bits.append(f"{page_struct_warnings} structural warning(s)")
                    f.write(f"    Summary: {', '.join(summary_bits)}\n")
                for line in page["log"]:
                    # Include [fix], [warn], and [struct] lines + continuation lines
                    stripped = line.strip()
                    if (stripped.startswith("[fix]") or stripped.startswith("[warn]")
                            or stripped.startswith("[struct]")
                            or stripped.startswith("MD headings:") or stripped.startswith("Prod headings:")
                            or stripped.startswith("MD:") or stripped.startswith("Prod:")):
                        # Skip the "Prod: https://..." info line (already shown above)
                        if stripped.startswith("Prod: http"):
                            continue
                        f.write(f"    {stripped}\n")
                f.write("\n")
        f.write(summary + "\n")
    _log(f"  Report written to: {report_path}")


def fix_guide(start_url: str, dry_run: bool = False,
              start_page: int = 0, end_page: int = 0) -> dict:
    """Crawl all pages in a guide and fix each corresponding .md file.

    Args:
        start_page: 1-based start index (0 = from beginning).
        end_page: 1-based end index (0 = no limit).
    """
    # Discover up to end_page pages (or all if 0)
    urls = discover_guide_urls(start_url, max_pages=end_page)
    # Slice to the requested range
    if start_page > 0:
        urls = urls[start_page - 1:]
        _log(f"  Processing pages {start_page}-{start_page + len(urls) - 1}\n")

    start_md_path = resolve_md_path(start_url)
    guide_search_root = os.path.dirname(start_md_path) if start_md_path else None

    totals = {"pages": 0, "fixed": 0, "skipped": 0, "errors": 0}
    fixed_pages = []

    for url in urls:
        md_path = resolve_md_path(url, search_root=guide_search_root)
        if not md_path:
            slug = url.rstrip("/").split("/")[-1]
            _log(f"\n  [skip] No .md file found for {slug}")
            totals["skipped"] += 1
            continue

        totals["pages"] += 1
        stats = fix_md_file(md_path, url, dry_run=dry_run)
        if stats.get("errors"):
            totals["errors"] += 1
        elif sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings")) > 0:
            totals["fixed"] += 1
            fixed_pages.append({"md_path": md_path, "url": url, "log": stats.get("log", []), "stats": stats})
        elif stats.get("structural_warnings", 0) > 0:
            # Include pages with only structural warnings in the report
            fixed_pages.append({"md_path": md_path, "url": url, "log": stats.get("log", []), "stats": stats})

        time.sleep(THROTTLE_SECONDS)

    summary = (f"Guide summary: {totals['pages']} pages processed, "
               f"{totals['fixed']} fixed, {totals['skipped']} skipped, "
               f"{totals['errors']} errors")
    _log(f"\n{'=' * 50}")
    _log(summary)

    # Derive report filename from guide slug
    parsed = urllib.parse.urlparse(start_url)
    path_parts = [s for s in parsed.path.strip("/").split("/") if s]
    guide_slug = "-".join(path_parts[:2]) if len(path_parts) >= 2 else "guide"
    report_filename = f"~fix-md-report-{guide_slug}.txt"
    report_path = _resolve_report_path(report_filename)
    _write_report(report_path, fixed_pages, summary)

    return totals


