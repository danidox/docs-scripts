def _replace_special_image_sources(text: str) -> str:
    """Replace known availability image markdown/HTML/source strings with emoji."""
    for image_name, replacement in SPECIAL_IMAGE_SOURCE_TO_TEXT.items():
        text = re.sub(rf'!\[[^\]]*\]\([^)]*{re.escape(image_name)}[^)]*\)', replacement, text)
        text = re.sub(rf'<img\b[^>]*src="[^"]*{re.escape(image_name)}[^"]*"[^>]*>', replacement, text)
        text = text.replace(image_name, replacement)
    return text


def _special_image_replacement_from_tag(tag: Tag) -> Optional[str]:
    """Return emoji replacement when a tag contains one of the known status images."""
    for img in tag.find_all("img"):
        src = img.get("src", "")
        for image_name, replacement in SPECIAL_IMAGE_SOURCE_TO_TEXT.items():
            if image_name in src:
                return replacement
    return None


def _repair_mojibake_line(text: str) -> str:
    """Repair common UTF-8 text that was mis-decoded as Windows-1252/Latin-1."""
    markers = ("\u00c2", "\u00c3", "\u00e2", "\u00f0", "\u00ef", "\ufffd")

    def score(value: str) -> int:
        return sum(value.count(marker) for marker in markers)

    if not text or not any(marker in text for marker in markers):
        return text

    best = text
    best_score = score(text)
    candidate = text

    for _ in range(2):
        improved = False
        for encoding in ("cp1252", "latin-1"):
            try:
                repaired = candidate.encode(encoding).decode("utf-8")
            except (UnicodeEncodeError, UnicodeDecodeError):
                continue
            repaired_score = score(repaired)
            if repaired_score < best_score:
                best = repaired
                best_score = repaired_score
                candidate = repaired
                improved = True
                break
        if not improved:
            break

    return best

def remove_toplevel_note_markers(lines: List[str]) -> Tuple[List[str], int]:
    """Remove leaked TOPLEVELNOTEMARKER sentinels left by md_maker."""
    marker_re = re.compile(r'(^|(?<=\S) )TOPLEVELNOTEMARKER\s*$')
    new_lines: List[str] = []
    fixes = 0

    for idx, line in enumerate(lines):
        updated = marker_re.sub(r'\1', line).rstrip()
        if updated != line:
            fixes += 1
            _log(f"  [fix] Removed leaked TOPLEVELNOTEMARKER at line {idx + 1}")
        new_lines.append(updated)

    return new_lines, fixes


def _match_docs_host(url: str) -> str | None:
    try:
        netloc = urllib.parse.urlparse(url).netloc.lower()
    except Exception:
        return None
    for host in DOCS_ALL_HOSTS:
        if netloc.endswith(host):
            return host
    return None


def _match_protected_docs_host(url: str) -> str | None:
    try:
        netloc = urllib.parse.urlparse(url).netloc.lower()
    except Exception:
        return None
    for host in DOCS_PROTECTED_HOSTS:
        if netloc.endswith(host):
            return host
    return None


def _needs_cloudflare_access(url: str) -> bool:
    return _match_protected_docs_host(url) is not None


def _parse_cookie_string(cookie_string: str) -> Dict[str, str]:
    jar: Dict[str, str] = {}
    if not cookie_string:
        return jar
    for part in [p.strip() for p in cookie_string.split(";") if p.strip()]:
        if "=" in part:
            key, value = part.split("=", 1)
            jar[key.strip()] = value.strip()
    return jar


def _parse_retry_after(value: str) -> float:
    if not value:
        return 0.0
    value = value.strip()
    if value.isdigit():
        return float(value)
    try:
        dt = email.utils.parsedate_to_datetime(value)
        if dt.tzinfo is None:
            dt = dt.replace(tzinfo=timezone.utc)
        return max(0.0, (dt - datetime.now(timezone.utc)).total_seconds())
    except Exception:
        return 0.0


def looks_like_login_wall(html: str) -> bool:
    if not html:
        return False
    text = html.lower()
    cues = [
        "sign in", "log in", "login", "single sign-on", "sso",
        "continue with", "enter your email", "verify your identity",
    ]
    if 'id="doccontainer"' in text or "id='doccontainer'" in text:
        return False
    return any(cue in text for cue in cues)


def load_saved_session_state() -> Optional[dict]:
    if not os.path.exists(SESSION_FILE):
        return None
    try:
        with open(SESSION_FILE, "r", encoding="utf-8") as f:
            data = json.load(f)
    except Exception:
        return None

    cookies = data.get("cookies") or []
    valid = any(
        (cookie.get("name") in {"CF_AppSession", "CF_Authorization"})
        and any(host in (cookie.get("domain") or "") for host in DOCS_PROTECTED_HOSTS)
        for cookie in cookies
    )
    return data if valid else None


def invalidate_saved_session() -> None:
    if not os.path.exists(SESSION_FILE):
        return
    try:
        shutil.copyfile(SESSION_FILE, EXPIRED_SESSION_FILE)
    except Exception:
        pass
    try:
        os.remove(SESSION_FILE)
    except Exception:
        pass
    _log("  [warn] Session invalidated (backed up to auth-session-expired.json).")


def save_session_state(storage_state: dict) -> None:
    try:
        with open(SESSION_FILE, "w", encoding="utf-8") as f:
            json.dump(storage_state, f, indent=2)
        _log("  [ok] Saved authenticated session to auth-session.json.")
    except Exception as exc:
        _log(f"  [warn] Could not save auth-session.json: {exc}")


def _storage_cookies_to_playwright_cookies(storage_state: Optional[dict]) -> List[dict]:
    cookies_to_add: List[dict] = []
    if not storage_state:
        return cookies_to_add
    for cookie in storage_state.get("cookies") or []:
        domain = cookie.get("domain") or ""
        if not any(host in domain for host in DOCS_PROTECTED_HOSTS):
            continue
        cookies_to_add.append(
            {
                "name": cookie.get("name", ""),
                "value": cookie.get("value", ""),
                "domain": domain,
                "path": cookie.get("path") or "/",
                "httpOnly": bool(cookie.get("httpOnly", False)),
                "secure": bool(cookie.get("secure", True)),
                "sameSite": cookie.get("sameSite") or "Lax",
            }
        )
    return cookies_to_add


def _requests_session_to_playwright_cookies(session: Optional[requests.Session]) -> List[dict]:
    cookies_to_add: List[dict] = []
    if session is None:
        return cookies_to_add
    for cookie in session.cookies:
        domain = cookie.domain or ""
        if any(domain.endswith(host) for host in DOCS_PROTECTED_HOSTS) or domain.endswith(".uipath.com"):
            cookies_to_add.append(
                {
                    "name": cookie.name,
                    "value": cookie.value,
                    "domain": domain,
                    "path": cookie.path or "/",
                    "httpOnly": False,
                    "secure": False,
                    "sameSite": "Lax",
                }
            )
    return cookies_to_add


def apply_storage_state_to_session(session: requests.Session, storage_state: Optional[dict]) -> bool:
    host_matched = False
    if not storage_state:
        return host_matched
    for cookie in storage_state.get("cookies") or []:
        domain = cookie.get("domain") or ""
        if not any(host in domain for host in DOCS_PROTECTED_HOSTS):
            continue
        session.cookies.set(
            cookie.get("name", ""),
            cookie.get("value", ""),
            domain=domain,
            path=cookie.get("path") or "/",
        )
        host_matched = True
    return host_matched


def configure_docs_dev_auth(session: requests.Session, host: Optional[str] = None) -> None:
    host = host or "docs-dev.uipath.com"
    if host not in DOCS_PROTECTED_HOSTS:
        return

    apply_storage_state_to_session(session, load_saved_session_state())

    token = os.getenv("DOCS_DEV_BEARER")
    if token and "authorization" not in {k.lower() for k in session.headers}:
        session.headers["Authorization"] = f"Bearer {token}"

    cookies = {}
    cookies.update(_parse_cookie_string(os.getenv("DOCS_DEV_COOKIES") or ""))
    cookies.update(_parse_cookie_string(os.getenv("CLOUDFRONT_COOKIES") or ""))
    for key, value in cookies.items():
        session.cookies.set(key, value, domain=host)

    session.headers.setdefault("User-Agent", DEFAULT_USER_AGENT)
    session.headers.setdefault(
        "Accept",
        "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
    )
    session.headers.setdefault("Accept-Language", "en-US,en;q=0.9")


def _detect_kid_from_cloudflare(target_url: str, session: requests.Session) -> Optional[str]:
    host = _match_protected_docs_host(target_url)
    if not host:
        return None

    try:
        app_login = f"https://{host}/cdn-cgi/access/login?" + urllib.parse.urlencode(
            {"redirect_url": target_url}
        )
        resp = session.get(app_login, allow_redirects=False, timeout=10)
        location = resp.headers.get("Location", "") or resp.headers.get("location", "")
        if location:
            parsed = urllib.parse.urlparse(location)
            kid_vals = urllib.parse.parse_qs(parsed.query).get("kid") or []
            if kid_vals:
                return kid_vals[0]

        if resp.is_redirect or resp.status_code in (301, 302, 303, 307, 308):
            resp2 = session.get(urllib.parse.urljoin(app_login, location), allow_redirects=False, timeout=10)
            location2 = resp2.headers.get("Location", "") or resp2.headers.get("location", "")
            if location2:
                parsed2 = urllib.parse.urlparse(location2)
                kid_vals2 = urllib.parse.parse_qs(parsed2.query).get("kid") or []
                if kid_vals2:
                    return kid_vals2[0]
    except Exception:
        pass

    try:
        team_login = CLOUDFLARE_LOGIN_BASE + "?" + urllib.parse.urlencode({"redirect_url": target_url})
        resp3 = session.get(team_login, allow_redirects=False, timeout=10)
        location3 = resp3.headers.get("Location", "") or resp3.headers.get("location", "")
        if location3:
            parsed3 = urllib.parse.urlparse(location3)
            kid_vals3 = urllib.parse.parse_qs(parsed3.query).get("kid") or []
            if kid_vals3:
                return kid_vals3[0]
    except Exception:
        pass

    return None


def _install_fast_routes(page) -> None:
    try:
        page.route(
            "**/*",
            lambda route: (
                route.abort()
                if route.request.resource_type in {"image", "media", "font"}
                else route.continue_()
            ),
        )
    except Exception:
        pass


