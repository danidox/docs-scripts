def _split_md_table_cells(row_text: str) -> List[str]:
    """Split a markdown table row into cell contents, respecting backtick spans."""
    s = row_text.strip()
    if s.startswith('|'):
        s = s[1:]
    if s.endswith('|'):
        s = s[:-1]

    cells = []
    current: List[str] = []
    in_code = False
    tick_count = 0
    i = 0
    while i < len(s):
        ch = s[i]
        if not in_code and ch == '`':
            j = i
            while j < len(s) and s[j] == '`':
                j += 1
            tick_count = j - i
            in_code = True
            current.append(s[i:j])
            i = j
            continue
        if in_code and ch == '`':
            j = i
            while j < len(s) and s[j] == '`':
                j += 1
            if j - i == tick_count:
                in_code = False
                current.append(s[i:j])
                i = j
                continue
            else:
                current.append(s[i:j])
                i = j
                continue
        if ch == '|' and not in_code:
            cells.append(''.join(current).strip())
            current = []
        else:
            current.append(ch)
        i += 1
    if current:
        cells.append(''.join(current).strip())
    return cells




def _find_list_cont_indent(lines: List[str], target_line: int) -> Optional[int]:
    """Find the continuation indent for the list item preceding target_line.

    Stops at headings (a heading closes the current list context).
    """
    list_head = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    heading_re = re.compile(r'^#{1,6}\s+')
    for k in range(target_line - 1, -1, -1):
        prev = lines[k]
        if not prev.strip():
            continue
        if heading_re.match(prev):
            return None  # heading closes list context
        m = list_head.match(prev)
        if m:
            base = len(m.group(1))
            marker_len = len(m.group(2)) + 1
            return base + marker_len
        # A non-blank, non-list, non-indented line (not a heading) also closes context
        if prev[0] not in (' ', '\t'):
            return None
    return None


def fix_misindented_paragraphs(lines: List[str], md: dict, prod: dict) -> Tuple[List[str], int]:
    """Re-indent top-level (col-0) paragraphs that belong inside a list item.

    Uses prod li_paragraphs as the signal: if an MD top-level paragraph's
    text appears in prod inside a list item, re-indent it to the preceding
    list item's continuation indent.
    """
    prod_li_norms = [_norm_para(p.get("text", "")) for p in prod.get("li_paragraphs", [])]
    prod_li_norms = [n for n in prod_li_norms if n and len(n) >= 10]
    if not prod_li_norms:
        return lines, 0

    result = lines[:]
    count = 0

    for para in md.get("paragraphs", []):
        line_idx = para["line"]
        if line_idx >= len(result):
            continue
        current_line = result[line_idx]
        # Only act on paragraphs truly at col 0
        if not current_line or current_line[0] in (' ', '\t'):
            continue

        para_norm = _norm_para(para["text"])
        if not para_norm or len(para_norm) < 10:
            continue

        # Check if this paragraph appears in prod's li_paragraphs
        search_prefix = para_norm[:40]
        if not any(search_prefix in pn or pn[:40] in para_norm for pn in prod_li_norms):
            continue

        # Find preceding list item's continuation indent (stops at headings)
        continuation_indent = _find_list_cont_indent(result, line_idx)
        if not continuation_indent:
            continue

        result[line_idx] = ' ' * continuation_indent + current_line.strip()
        count += 1
        _log(f"  [fix] Paragraph at line {line_idx + 1}: indented to "
             f"{continuation_indent} spaces (list continuation)")

    return result, count


