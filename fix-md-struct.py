def _normalize_url_for_match(url: str) -> str:
    """Normalize a docs URL for case-insensitive guide matching."""
    parsed = urllib.parse.urlparse(urllib.parse.urldefrag(url)[0])
    path = parsed.path or "/"
    if path != "/":
        path = path.rstrip("/")
    return urllib.parse.urlunparse((
        parsed.scheme.lower(),
        parsed.netloc.lower(),
        path.lower(),
        "",
        parsed.query,
        "",
    ))


def discover_guide_urls(start_url: str, max_pages: int = 0) -> List[str]:
    """Crawl a guide starting from start_url and return all page URLs.

    Args:
        max_pages: Stop discovering after this many pages (0 = unlimited).
    """
    parsed = urllib.parse.urlparse(start_url)
    # Base = everything up to (but not including) the last segment
    path_parts = parsed.path.rstrip("/").rsplit("/", 1)
    base_url = parsed.scheme + "://" + parsed.netloc + path_parts[0] + "/"

    normalized_base_url = _normalize_url_for_match(base_url)
    visited = set()
    queued = {_normalize_url_for_match(start_url)}
    queue = [start_url]
    ordered = []

    _log(f"Discovering guide pages under: {base_url}")
    if max_pages > 0:
        _log(f"  (limited to {max_pages} page(s))")

    while queue:
        if max_pages > 0 and len(ordered) >= max_pages:
            break

        url = urllib.parse.urldefrag(queue.pop(0))[0]
        normalized_url = _normalize_url_for_match(url)
        queued.discard(normalized_url)
        if normalized_url in visited:
            continue
        visited.add(normalized_url)
        ordered.append(url)

        _, soup = fetch_page(url)
        if not soup:
            continue

        time.sleep(THROTTLE_SECONDS)

        for a in soup.find_all("a", href=True):
            href = a["href"]
            if href.startswith("#") or href.startswith("mailto:"):
                continue
            abs_url = urllib.parse.urldefrag(urllib.parse.urljoin(url, href))[0]
            normalized_abs_url = _normalize_url_for_match(abs_url)
            if (normalized_abs_url.startswith(normalized_base_url)
                    and normalized_abs_url not in visited
                    and normalized_abs_url not in queued):
                queue.append(abs_url)
                queued.add(normalized_abs_url)

    _log(f"  Found {len(ordered)} page(s)\n")
    return ordered


