def parse_md_structure(lines: List[str]) -> dict:
    """Extract structural info from markdown lines."""
    structure = {
        "notes": [],    # [{"line": int, "indent": int, "in_list": bool, "end_line": int, "paragraph_count": int}]
        "images": [],   # [{"line": int, "indent": int}]
        "videos": [],   # [{"start_line": int, "end_line": int, "tag": str, "src": str}]
        "list_items": [],
        "li_paragraphs": [],  # [{"line": int, "indent": int, "depth": int, "text": str}]
        "code_blocks": [],  # [{"start_line": int, "end_line": int, "text": str}]
        "orphaned_paragraphs": [],  # paragraphs at indent 0 between list items
        "tables": [],   # [{"start_line": int, "end_line": int, "indent": int, "text_preview": str}]
        "headings": [],  # [{"line": int, "level": int, "text": str}]
        "paragraphs": [],  # [{"line": int, "text": str}]  â€” top-level paragraphs
        "frontmatter_title": "",  # title from YAML frontmatter
    }

    fence_open = re.compile(r'^(\s*):::(note|tip|warning|danger|info|important|caution)\s*$')
    fence_close = re.compile(r'^(\s*):::\s*$')
    image_re = re.compile(r'^(\s*)!\[')
    table_caption_re = re.compile(r'^\s*Table\s+\d+\.\s+\S+', re.I)
    image_caption_re = re.compile(r'^\s*Figure\s+\d+\.\s+\S+', re.I)
    list_head = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    code_fence = re.compile(r'^\s*(```|~~~)')

    inside_code = False
    code_start = None
    i = 0
    n = len(lines)

    # Track list context
    list_stack = []  # [(indent, marker_len)]

    while i < n:
        line = lines[i]

        if code_fence.match(line):
            if inside_code:
                structure["code_blocks"].append({
                    "start_line": code_start,
                    "end_line": i,
                    "text": "\n".join(lines[code_start + 1:i]),
                })
                code_start = None
                inside_code = False
            else:
                code_start = i
                inside_code = True
            i += 1
            continue

        if inside_code:
            i += 1
            continue

        # Track list items
        m_list = list_head.match(line)
        if m_list:
            indent = len(m_list.group(1))
            marker = m_list.group(2)
            # Pop items at same or deeper indent
            while list_stack and list_stack[-1][0] >= indent:
                list_stack.pop()
            list_stack.append((indent, len(marker) + 1))
            structure["list_items"].append({
                "line": i,
                "indent": indent,
                "depth": len(list_stack) - 1,
                "text": line.strip(),
            })

        # Detect ::: note blocks
        m_open = fence_open.match(line)
        if m_open:
            note_indent = len(m_open.group(1))
            start = i
            para_count = 0
            has_content = False
            j = i + 1
            while j < n and not fence_close.match(lines[j]):
                if lines[j].strip():
                    if not has_content or (j > 0 and not lines[j - 1].strip()):
                        para_count += 1
                    has_content = True
                j += 1
            end = j

            in_list = bool(list_stack) and note_indent > 0
            structure["notes"].append({
                "line": start,
                "end_line": end,
                "indent": note_indent,
                "in_list": in_list,
                "paragraph_count": para_count,
                "admonition_type": m_open.group(2),
            })
            i = end + 1
            continue

        # Detect images
        m_img = image_re.match(line)
        if m_img:
            img_indent = len(m_img.group(1))
            in_list = bool(list_stack)
            prev_nonblank = _nearest_nonblank_line(lines, i - 1, -1)
            next_nonblank = _nearest_nonblank_line(lines, i + 1, 1)
            structure["images"].append({
                "line": i,
                "indent": img_indent,
                "in_list": in_list,
                "has_caption": bool(
                    image_caption_re.match(prev_nonblank)
                    or image_caption_re.match(next_nonblank)
                ),
            })

        stripped = line.strip()
        if stripped.startswith('<iframe') or stripped.startswith('<video'):
            tag_name = 'iframe' if stripped.startswith('<iframe') else 'video'
            closing_tag = f'</{tag_name}>'
            start = i
            end = i
            block_lines = [line]
            if closing_tag not in line:
                while end + 1 < n:
                    end += 1
                    block_lines.append(lines[end])
                    if closing_tag in lines[end]:
                        break
            html_block = "\n".join(block_lines)
            structure["videos"].append({
                "start_line": start,
                "end_line": end,
                "tag": tag_name,
                "src": _extract_media_signature_from_html(html_block),
            })
            i = end + 1
            continue

        # Detect indented paragraph continuations inside list items so structural
        # comparison can recognize list content that is not a list head.
        stripped = line.strip()
        indent = len(line) - len(line.lstrip(' '))
        if (list_stack and stripped and indent > 0 and not m_list and not m_img
                and len(stripped) > 10
                and not stripped.startswith((':::', '#', '|', '<'))
                and not re.match(r'^\d+\.?\s', stripped)):
            structure["li_paragraphs"].append({
                "line": i,
                "indent": indent,
                "depth": len(list_stack) - 1,
                "text": stripped,
            })

        i += 1

    # Detect HTML <table> blocks
    table_open_re = re.compile(r'^(\s*)<table', re.I)
    table_close_re = re.compile(r'^\s*</table>', re.I)
    in_code_tbl = False
    for i, line in enumerate(lines):
        if code_fence.match(line):
            in_code_tbl = not in_code_tbl
            continue
        if in_code_tbl:
            continue
        m_tbl = table_open_re.match(line)
        if m_tbl:
            tbl_indent = len(m_tbl.group(1))
            # Find the matching </table>
            end_idx = i
            for j in range(i + 1, n):
                if table_close_re.match(lines[j]):
                    end_idx = j
                    break
            # Get text preview from first row and extract headers/row count
            preview = ""
            tbl_html = "\n".join(lines[i:end_idx + 1])
            tbl_soup = BeautifulSoup(tbl_html, "html.parser")
            tbl_tag = tbl_soup.find("table")
            md_headers = []
            md_row_count = 0
            html_caption_text = ""
            preceding_caption_text = _nearest_nonblank_line(lines, i - 1, -1)
            if tbl_tag:
                first_tr = tbl_tag.find("tr")
                if first_tr:
                    for th in first_tr.find_all(["th", "td"]):
                        md_headers.append(re.sub(r'\s+', ' ', th.get_text(strip=True)))
                md_row_count = len(tbl_tag.find_all("tr"))
                preview = first_tr.get_text(strip=True)[:80] if first_tr else ""
                caption_tag = tbl_tag.find("caption")
                if caption_tag:
                    html_caption_text = re.sub(r'\s+', ' ', caption_tag.get_text(" ", strip=True))
            structure["tables"].append({
                "start_line": i,
                "end_line": end_idx,
                "indent": tbl_indent,
                "text_preview": preview,
                "headers": md_headers,
                "row_count": md_row_count,
                "col_count": len(md_headers),
                "caption_text": html_caption_text or (
                    preceding_caption_text if table_caption_re.match(preceding_caption_text) else ""
                ),
                "has_caption": bool(
                    html_caption_text
                    or table_caption_re.match(preceding_caption_text)
                ),
            })

    # Detect markdown pipe tables
    md_pipe_re = re.compile(r'^\s*\|')
    md_sep_re = re.compile(r'^\s*\|([\s:]*-+[\s:]*\|)+\s*$')
    in_code_pipe = False
    i_pipe = 0
    while i_pipe < n:
        ln = lines[i_pipe]
        if code_fence.match(ln):
            in_code_pipe = not in_code_pipe
            i_pipe += 1
            continue
        if in_code_pipe:
            i_pipe += 1
            continue
        if md_pipe_re.match(ln) and i_pipe + 1 < n and md_sep_re.match(lines[i_pipe + 1]):
            tbl_start = i_pipe
            tbl_indent = len(ln) - len(ln.lstrip())
            j = i_pipe + 2
            while j < n and md_pipe_re.match(lines[j]):
                j += 1
            tbl_end = j - 1

            # Extract table headers and row count from markdown table
            md_headers = []
            header_line = lines[tbl_start]
            for cell in header_line.split('|')[1:-1]:
                md_headers.append(cell.strip())

            # Check if headers are empty (all cells are empty strings)
            headers_empty = all(not h for h in md_headers)

            # Calculate actual data row count:
            # - If headers empty: skip row 0 (headers) and row 1 (separator) â†’ count from row 2
            # - If headers have content: skip row 1 (separator) â†’ count all rows except separator
            if headers_empty:
                # Row 0 = headers, row 1 = separator, rows 2+ = data
                actual_row_count = max(0, tbl_end - tbl_start - 1)  # exclude header and separator rows
            else:
                # All rows except separator
                actual_row_count = max(0, tbl_end - tbl_start)  # exclude separator row

            # Add to main tables list for structural comparison
            preview = header_line.replace('|', ' ').strip()[:80]
            preceding_caption_text = _nearest_nonblank_line(lines, tbl_start - 1, -1)
            structure["tables"].append({
                "start_line": tbl_start,
                "end_line": tbl_end,
                "indent": tbl_indent,
                "text_preview": preview,
                "headers": md_headers,
                "headers_empty": headers_empty,
                "row_count": actual_row_count,
                "col_count": len(md_headers),
                "caption_text": preceding_caption_text if table_caption_re.match(preceding_caption_text) else "",
                "has_caption": bool(table_caption_re.match(preceding_caption_text)),
            })

            i_pipe = tbl_end + 1
        else:
            i_pipe += 1

    # Sort tables by document order (HTML tables are processed before pipe tables,
    # but pipe tables may appear earlier in the file)
    structure["tables"].sort(key=lambda t: t["start_line"])
    _assign_md_heading_context(structure, "tables", "start_line")
    _assign_md_heading_context(structure, "videos", "start_line")

    # Detect headings and top-level paragraphs for structural comparison
    heading_detect_re = re.compile(r'^(#{1,6})\s+(.*)')
    in_code_hp = False
    in_table_hp = False
    in_note_hp = False
    in_frontmatter = False
    frontmatter_done = False
    table_open_hp = re.compile(r'^\s*<table', re.I)
    table_close_hp = re.compile(r'^\s*</table>', re.I)
    note_open_hp = re.compile(r'^\s*:::\w+')
    note_close_hp = re.compile(r'^\s*:::\s*$')
    for i_hp, line_hp in enumerate(lines):
        # Skip YAML frontmatter (--- ... ---)
        if not frontmatter_done:
            if i_hp == 0 and line_hp.strip() == '---':
                in_frontmatter = True
                continue
            if in_frontmatter:
                if line_hp.strip() == '---':
                    in_frontmatter = False
                    frontmatter_done = True
                else:
                    # Extract title from frontmatter
                    m_title = re.match(r'^title:\s*(.+)', line_hp)
                    if m_title:
                        structure["frontmatter_title"] = m_title.group(1).strip().strip('"').strip("'")
                continue
            frontmatter_done = True
        if code_fence.match(line_hp):
            in_code_hp = not in_code_hp
            continue
        if in_code_hp:
            continue
        if table_open_hp.match(line_hp):
            in_table_hp = True
            continue
        if table_close_hp.match(line_hp):
            in_table_hp = False
            continue
        if in_table_hp:
            continue
        if note_open_hp.match(line_hp) and not note_close_hp.match(line_hp):
            in_note_hp = True
            continue
        if note_close_hp.match(line_hp):
            in_note_hp = False
            continue
        if in_note_hp:
            continue
        m_head = heading_detect_re.match(line_hp)
        if m_head:
            structure["headings"].append({
                "line": i_hp,
                "level": len(m_head.group(1)),
                "text": m_head.group(2).strip(),
            })
            continue
        # Top-level paragraphs: non-blank, not indented, not a list item, not a heading,
        # not inside code/table/note, not frontmatter, not HTML tags
        stripped_hp = line_hp.strip()
        first_ch = line_hp[0:1] if line_hp else ''
        # Exclude lines starting with whitespace, heading markers, pipe, image,
        # or block HTML. For *, -, + only exclude if it's a list item (char + space),
        # not bold (**text**) or other inline usage. Allow inline HTML starters
        # such as <sup> and <sub>, which can be valid paragraph content.
        if first_ch in (' ', '\t', '#', '|', '!'):
            continue
        if first_ch == '<' and not re.match(r'^\s*</?(sup|sub|span)\b', stripped_hp, re.I):
            continue
        if first_ch in ('*', '-', '+') and len(line_hp) > 1 and line_hp[1:2] in (' ', '\t'):
            continue  # list item
        if (stripped_hp
                and not stripped_hp.startswith('---')
                and not list_head.match(line_hp)
                and len(stripped_hp) > 10
                and not re.match(r'^\d+\)\s', stripped_hp)
                and not re.match(r'^\d+\s*-\s+', stripped_hp)):
            structure["paragraphs"].append({
                "line": i_hp,
                "text": stripped_hp,
            })

    # Detect orphaned paragraphs: non-blank lines at indent 0 that sit
    # between a list item and its sub-items or the next list item.
    heading_re = re.compile(r'^#{1,6}\s')
    fence_open_admon = re.compile(r'^\s*:::\w+')
    list_head_re = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    code_fence_re = re.compile(r'^\s*(```|~~~)')
    in_code = False

    for i, line in enumerate(lines):
        if code_fence_re.match(line):
            in_code = not in_code
            continue
        if in_code:
            continue
        if not line.strip():
            continue
        # Must be at indent 0, not a list item, heading, fence, pipe table, or separator
        if line[0] == ' ' or line[0] == '\t':
            continue
        if list_head_re.match(line):
            continue
        if heading_re.match(line):
            continue
        if fence_open_admon.match(line):
            continue
        if line.startswith('|'):
            continue
        if line.startswith('---'):
            continue

        # Look backwards: find the enclosing list item, skipping blanks and indented
        # content (table rows, continuation lines) that may sit between the orphan
        # and the list item that owns it.
        prev_list_line = None
        for k in range(i - 1, -1, -1):
            if not lines[k].strip():
                continue
            # Skip indented content (table rows, continuation lines, etc.)
            if lines[k][0:1] in (' ', '\t'):
                continue
            if list_head_re.match(lines[k]):
                prev_list_line = k
                break
            # Anything else at indent 0 (heading, fence, bare paragraph) closes context
            break

        if prev_list_line is None:
            continue

        # Look forwards: what is the next structural element (skipping blanks,
        # images, and indented continuation lines)?
        next_list_line = None
        next_is_heading = False
        for k in range(i + 1, len(lines)):
            if not lines[k].strip():
                continue
            stripped_k = lines[k].strip()
            # Skip images and indented content â€” they don't break list context
            if stripped_k.startswith('![') or lines[k][0:1] in (' ', '\t'):
                continue
            if list_head_re.match(lines[k]):
                next_list_line = k
            elif heading_re.match(lines[k]):
                next_is_heading = True
            break

        # If the next structural element is a heading, this paragraph is post-list
        # (before a new section), not a list continuation â€” don't treat as orphaned.
        if next_is_heading:
            continue

        # This line is a continuation of the last list item â€” it's orphaned.
        # next_list_line being None means it's the final item before the list ends.
        text = re.sub(r'\s+', ' ', line.strip())
        kind = "image" if line.strip().startswith('![') else "paragraph"
        structure["orphaned_paragraphs"].append({
            "line": i,
            "text": text[:120],
            "prev_list_line": prev_list_line,
            "kind": kind,
        })

    # NOTE: Do NOT exclude orphaned paragraphs from structural comparison!
    # Paragraphs between lists may or may not be orphaned depending on prod rendering.
    # The orphaned_paragraphs list is used for file fixing, not for structural comparison.
    # Keep all extracted paragraphs for comparison against prod, then let the comparison
    # logic (content matching) determine if they're truly present in prod.

    return structure


