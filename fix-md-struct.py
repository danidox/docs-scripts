def _html_to_md_text(element) -> str:
    """Convert an HTML element's content to Markdown-like text.

    Converts inline formatting tags to their Markdown equivalents so that
    prod text can be compared directly against MD source text:
      <code>, <codeph>  â†’ `...`
      <strong>, <b>     â†’ **...**
      <em>, <i>         â†’ *...*
      <img>             â†’ ![](src)

    Block-level children (tables, lists, divs with block classes) are excluded.
    """
    parts = []
    for child in element.children:
        if isinstance(child, NavigableString):
            text = str(child)
            if text.strip():
                parts.append(text)
        elif isinstance(child, Tag):
            # Skip UI chrome tags entirely
            if child.name in ("style", "script", "button", "svg"):
                continue
            # Skip headings â€” they're block-level, not part of paragraph text
            if child.name in ("h1", "h2", "h3", "h4", "h5", "h6"):
                continue
            # Stop at major block elements
            if child.name in ("table", "ul", "ol", "dl", "blockquote"):
                break
            # Skip code blocks â€” <pre> elements and divs containing <pre>
            # (prod wraps para + code block + para in one <div class="p">)
            if child.name == "pre":
                continue
            if child.name == "div":
                classes = child.get("class", [])
                if any(c in classes for c in ("tablenoborder", "tablecont", "table")):
                    break
                # Skip inline images â€” but continue so trailing text is captured
                if any(c in classes for c in ("fignone", "fig")):
                    replacement = _special_image_replacement_from_tag(child)
                    if replacement:
                        parts.append(replacement)
                    continue
                # Skip code block container divs (contain <pre> with copy button)
                if child.find("pre"):
                    continue
                # Skip admonition divs (note, warning, etc.) â€” don't mix their
                # content into the surrounding paragraph text
                _ADMON = {"note", "warning", "important", "information", "tip", "danger", "prereq"}
                if _ADMON & set(classes):
                    continue
            # Convert inline formatting to Markdown equivalents
            if child.name in ("code", "codeph"):
                inner = child.get_text()
                parts.append(f"`{inner}`")
            elif child.name in ("strong", "b"):
                inner = _html_to_md_text(child)
                parts.append(f"**{inner}**")
            elif child.name in ("em", "i"):
                inner = _html_to_md_text(child)
                parts.append(f"*{inner}*")
            elif child.name == "img":
                src = child.get("src", "")
                for image_name, replacement in SPECIAL_IMAGE_SOURCE_TO_TEXT.items():
                    if image_name in src:
                        parts.append(replacement)
                        break
                else:
                    parts.append(f"![]({src})")
            elif child.name == "a":
                href = child.get("href", "")
                inner = _html_to_md_text(child)
                parts.append(f"[{inner}]({href})")
            elif child.name in ("sup", "sub"):
                inner = _html_to_md_text(child)
                parts.append(f"<{child.name}>{inner}</{child.name}>")
            else:
                # Recurse into other inline elements (span, etc.)
                parts.append(_html_to_md_text(child))
    return ''.join(parts)


# Block-level tags that terminate an inline text segment when iterating
# direct children of a container (used by _extract_inline_segs).
_INLINE_BLOCK_TAGS = frozenset({
    "ul", "ol", "li", "dl", "dt", "dd", "div", "table", "pre", "blockquote",
    "h1", "h2", "h3", "h4", "h5", "h6",
})


def _extract_inline_segs(div) -> list:
    """Return all non-empty inline text segments from direct children of *div*.

    Iterates children and collects NavigableString / inline elements into
    segments; flushes a segment whenever a block-level tag is encountered.
    Each flushed segment is converted to Markdown via _html_to_md_text on a
    temporary <span> wrapper and returned as a list of strings.
    """
    results = []
    seg = []
    for child in list(div.children) + [None]:   # None sentinel flushes final seg
        is_block = (child is None or
                    (hasattr(child, "name") and child.name in _INLINE_BLOCK_TAGS))
        if is_block:
            if seg:
                html = "".join(str(p) for p in seg)
                frag = BeautifulSoup(f"<span>{html}</span>", "html.parser").find("span")
                text = re.sub(r"\s+", " ", _html_to_md_text(frag).strip())
                if text and len(text) > 10:
                    if not (text.startswith("`") and text.endswith("`")):
                        results.append(text)
            seg = []
        else:
            seg.append(child)
    return results


