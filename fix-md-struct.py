def _nearest_nonblank_line(lines: List[str], start_index: int, step: int) -> str:
    i = start_index
    while 0 <= i < len(lines):
        if lines[i].strip():
            return lines[i].strip()
        i += step
    return ""


def _heading_alignment_score(md_headings: List[dict], prod_headings: List[dict], fm_title: str, offset: int) -> float:
    score = 0.0
    prod_levels = sorted(set(h["level"] for h in prod_headings))

    if offset == -1:
        prod_h2s = [h for h in prod_headings if h["level"] == 2]
        fm_norm = _norm_heading(fm_title)
        if fm_norm and prod_h2s:
            for h in prod_h2s:
                score += 3.0 if _norm_heading(h["text"]) == fm_norm else -1.0
        elif prod_h2s:
            score -= 0.5 * len(prod_h2s)

    for prod_level in prod_levels:
        md_level = prod_level + offset
        if md_level < 1:
            continue
        prod_at = [h for h in prod_headings if h["level"] == prod_level]
        md_at = [h for h in md_headings if h["level"] == md_level]
        score -= abs(len(md_at) - len(prod_at)) * 1.5
        md_norms = {_norm_heading(h["text"]) for h in md_at}
        prod_norms = {_norm_heading(h["text"]) for h in prod_at}
        score += len(md_norms & prod_norms) * 4.0
        if md_norms and prod_norms:
            score += len(md_norms & prod_norms) / max(len(md_norms), len(prod_norms))
    return score


def _choose_heading_offset(md_headings: List[dict], prod_headings: List[dict], fm_title: str) -> int:
    candidates = (-1, 0)
    best_offset = -1
    best_score = None
    for offset in candidates:
        score = _heading_alignment_score(md_headings, prod_headings, fm_title, offset)
        if best_score is None or score > best_score:
            best_score = score
            best_offset = offset
    return best_offset


def _table_match_score(md_table: dict, prod_table: dict) -> float:
    score = 0.0
    md_heading = _norm_heading(md_table.get("heading_text", ""))
    prod_heading = _norm_heading(prod_table.get("heading_text", ""))
    if md_heading and prod_heading:
        if md_heading == prod_heading:
            score += 8.0
        else:
            score += _similarity_score(md_heading, prod_heading) * 3.0

    md_headers = _table_header_signature(md_table)
    prod_headers = _table_header_signature(prod_table)
    if md_headers and prod_headers:
        if md_headers == prod_headers:
            score += 8.0
        else:
            overlap = len(set(md_headers) & set(prod_headers))
            score += overlap * 1.5
            score += _similarity_score(" ".join(md_headers), " ".join(prod_headers)) * 2.0

    md_cols = md_table.get("col_count", 0)
    prod_cols = prod_table.get("col_count", 0)
    if md_cols and prod_cols:
        if md_cols == prod_cols:
            score += 4.0
        else:
            score -= abs(md_cols - prod_cols) * 1.25

    md_rows = md_table.get("row_count", 0)
    prod_rows = prod_table.get("row_count", 0)
    if md_rows and prod_rows:
        score -= min(abs(md_rows - prod_rows), 10) * 0.1

    md_preview = _norm_para(md_table.get("text_preview", ""))
    prod_preview = _norm_para(prod_table.get("text_preview", ""))
    if md_preview and prod_preview:
        score += _similarity_score(md_preview, prod_preview) * 1.5

    return score


def _pair_tables(md_tables: List[dict], prod_tables: List[dict]) -> List[Tuple[dict, dict]]:
    pairs: List[Tuple[dict, dict]] = []
    used_prod: set[int] = set()
    next_prod_idx = 0

    for md_table in md_tables:
        best_j = None
        best_score = None
        for j in range(next_prod_idx, len(prod_tables)):
            if j in used_prod:
                continue
            score = _table_match_score(md_table, prod_tables[j])
            if best_score is None or score > best_score:
                best_score = score
                best_j = j
        if best_j is None:
            break
        used_prod.add(best_j)
        next_prod_idx = best_j + 1
        pairs.append((md_table, prod_tables[best_j]))

    return pairs


def _extract_all_text_content(structure: dict) -> set:
    """Extract all text content from all formats (paragraphs, tables, lists).

    Returns normalized text snippets to check if content exists in any format.
    """
    content = set()

    # Extract from paragraphs
    for para in structure.get("paragraphs", []):
        text = _norm_para(para.get("text", ""))
        if text:
            content.add(text)

    # Extract from tables (header and cell content)
    for table in structure.get("tables", []):
        caption = table.get("caption_text", "")
        if caption:
            text = _norm_para(caption)
            if text:
                content.add(text)
        headers = table.get("headers", [])
        for h in headers:
            text = _norm_para(h)
            if text:
                content.add(text)
        # Also add preview/raw content
        preview = table.get("text_preview", "")
        if preview:
            text = _norm_para(preview)
            if text:
                content.add(text)

    # Extract from list items
    for item in structure.get("list_items", []):
        text = _norm_para(item.get("text", ""))
        if text:
            content.add(text)

    # Extract from li_paragraphs (block elements inside <li>)
    for li_p in structure.get("li_paragraphs", []):
        text = _norm_para(li_p.get("text", ""))
        if text:
            content.add(text)

    return content


def _content_exists_in_format(text: str, structure: dict, exclude_tables: bool = False) -> bool:
    """Check if given normalized text exists in structure (any format).

    Args:
        text: Normalized text to search for
        structure: MD or prod structure dict
        exclude_tables: If True, don't search in tables (for table content checks)

    Returns:
        True if text found in paragraphs, lists, or tables (unless excluded)
    """
    if not text:
        return False

    # Helper to check if two normalized texts match with reasonable tolerance
    def _text_matches(search_text: str, target_text: str) -> bool:
        """Check if search_text appears in target_text with tolerance for minor variations."""
        if not search_text or not target_text:
            return False
        # Check exact substring match: first 50 chars of what we're searching for
        # must appear somewhere in the target element text.
        search_start = search_text[:min(50, len(search_text))]
        if search_start in target_text:
            return True
        # For very short texts, use higher threshold (match first 30 chars)
        if len(search_text) < 30 or len(target_text) < 30:
            min_len = min(len(search_text), len(target_text))
            if min_len >= 20 and search_text[:min_len] == target_text[:min_len]:
                return True
        # Fallback: check similarity score for texts with formatting variations
        # If texts are very similar (>75%), consider it a match despite formatting differences
        similarity = _similarity_score(search_text, target_text)
        if similarity > 0.75:
            return True
        return False

    # Check paragraphs
    for para in structure.get("paragraphs", []):
        para_norm = _norm_para(para.get("text", ""))
        if _text_matches(text, para_norm):
            return True
        # Also handle combined prod paragraphs (e.g. image between two sentences):
        # prod may wrap "Sentence A. [img] Sentence B." in one <div class="p">,
        # while MD has them as separate paragraphs. If the structure paragraph's
        # text is a prefix of the search text, the content exists.
        if len(para_norm) >= 20 and text.startswith(para_norm):
            return True

    # Check lists
    for item in structure.get("list_items", []):
        item_norm = _norm_para(item.get("text", ""))
        if _text_matches(text, item_norm):
            return True

    # Check li_paragraphs (block elements inside <li> that are not the list head)
    for li_p in structure.get("li_paragraphs", []):
        li_p_norm = _norm_para(li_p.get("text", ""))
        if _text_matches(text, li_p_norm):
            return True

    # Check tables (unless excluded)
    if not exclude_tables:
        for table in structure.get("tables", []):
            caption_norm = _norm_para(table.get("caption_text", ""))
            if _text_matches(text, caption_norm):
                return True
            table_preview = _norm_para(table.get("text_preview", ""))
            if _text_matches(text, table_preview):
                return True
            for h in table.get("headers", []):
                h_norm = _norm_para(h)
                if _text_matches(text, h_norm):
                    return True

    return False


def _warn_mixed_captions(md: dict) -> None:
    tables = md.get("tables", [])
    if len(tables) >= 2:
        table_with = sum(1 for t in tables if t.get("has_caption"))
        if 0 < table_with < len(tables):
            _log(
                f"  [warn] Not all tables have Table captions "
                f"({table_with}/{len(tables)} captioned)."
            )

    images = md.get("images", [])
    if len(images) >= 2:
        image_with = sum(1 for img in images if img.get("has_caption"))
        if 0 < image_with < len(images):
            _log(
                f"  [warn] Not all images have Figure captions "
                f"({image_with}/{len(images)} captioned)."
            )


def compare_structure(md: dict, prod: dict) -> List[str]:
    """Compare MD and prod structural elements, returning warning messages.

    Detects:
      - Heading count and text mismatches
      - Missing tables (when table content doesn't exist in any format in MD)
      - Table structure changes (column/row count, header text differences)
      - Top-level content differences (only when truly missing, not just format changes)

    Note: Does NOT flag "table count mismatch" if table content exists in another format
    (e.g., content is in list instead of table), as this is a format variation, not missing content.
    """
    warnings: List[str] = []

    # --- Headings ---
    # Prod headings are leveled-down in MD:
    #   prod h2 = MD frontmatter title
    #   prod h3 = MD h2, prod h4 = MD h3, etc.
    md_headings = md.get("headings", [])
    prod_headings = prod.get("headings", [])
    fm_title = md.get("frontmatter_title", "")

    prod_has_content = any((
        prod_headings,
        prod.get("paragraphs", []),
        prod.get("tables", []),
        prod.get("list_items", []),
        prod.get("images", []),
        prod.get("videos", []),
        prod.get("notes", []),
    ))
    prod_only_title = (
        len(prod_headings) == 1
        and prod_headings[0].get("level") == 2
        and not prod.get("paragraphs", [])
        and not prod.get("tables", [])
        and not prod.get("list_items", [])
        and not prod.get("images", [])
        and not prod.get("videos", [])
        and not prod.get("notes", [])
    )
    md_has_content = any((
        md_headings,
        md.get("paragraphs", []),
        md.get("tables", []),
        md.get("list_items", []),
        md.get("images", []),
        md.get("videos", []),
        md.get("notes", []),
        fm_title,
    ))
    if md_has_content and (not prod_has_content or prod_only_title):
        warnings.append(
            "  [struct] Prod page appears blank or title-only, but MD contains content "
            "(possible intentional authoring divergence)")
        return warnings

    # --- Code blocks ---
    # Flag suspicious quote balance in MD code fences for manual review.
    for block in md.get("code_blocks", []):
        text = block.get("text", "")
        if not text.strip():
            continue
        double_quotes = text.count('"')
        single_quotes = text.count("'")
        if double_quotes % 2 == 1 or single_quotes % 2 == 1:
            excerpt = re.sub(r'\s+', ' ', text.strip())[:60]
            odd_parts = []
            if double_quotes % 2 == 1:
                odd_parts.append(f'{double_quotes} double quote(s)')
            if single_quotes % 2 == 1:
                odd_parts.append(f"{single_quotes} single quote(s)")
            warnings.append(
                f"  [struct] Code block at line {block['start_line'] + 1} has odd quote count "
                f"({', '.join(odd_parts)}). Manual review recommended: "
                f"\"{excerpt}...\"")

    # Separate prod h2 (should match frontmatter title) from subheadings
    heading_offset = _choose_heading_offset(md_headings, prod_headings, fm_title)
    prod_h2s = [h for h in prod_headings if h["level"] == 2]
    prod_sub = [h for h in prod_headings if h["level"] > 2] if heading_offset == -1 else list(prod_headings)

    # Check prod h2 vs frontmatter title only for the classic shifted layout.
    if heading_offset == -1:
        for ph2 in prod_h2s:
            ph2_text = _norm_heading(ph2["text"])
            fm_text = _norm_heading(fm_title)
            if fm_text and ph2_text != fm_text:
                warnings.append(
                    f"  [struct] Page title differs: "
                    f"MD frontmatter \"{fm_title[:40]}\" vs prod h2 \"{ph2['text'][:40]}\"")
            elif not fm_text:
                warnings.append(
                    f"  [struct] Prod has h2 \"{ph2['text'][:40]}\" but MD has no frontmatter title")

    # Compare prod subheadings (h3+) with MD headings level by level.
    # prod h3 â†’ MD h2, prod h4 â†’ MD h3, etc.
    prod_sub_levels = sorted(set(h["level"] for h in prod_sub))
    md_heading_levels = sorted(set(h["level"] for h in md_headings))

    for prod_level in prod_sub_levels:
        expected_md_level = prod_level + heading_offset
        if expected_md_level < 1:
            continue
        prod_at = [h for h in prod_sub if h["level"] == prod_level]
        md_at = [h for h in md_headings if h["level"] == expected_md_level]
        if len(md_at) != len(prod_at):
            warnings.append(
                f"  [struct] h{expected_md_level} heading count: "
                f"MD has {len(md_at)}, prod has {len(prod_at)}")
            if md_at:
                warnings.append("           MD:   " + ", ".join(
                    f'"{h["text"][:40]}"' for h in md_at))
            if prod_at:
                warnings.append("           Prod: " + ", ".join(
                    f'"{h["text"][:40]}"' for h in prod_at))
        else:
            for i, (md_h, prod_h) in enumerate(zip(md_at, prod_at)):
                md_text = _norm_heading(md_h["text"])
                prod_text = _norm_heading(prod_h["text"])
                if md_text != prod_text:
                    warnings.append(
                        f"  [struct] h{expected_md_level} heading {i+1} text differs: "
                        f"MD \"{md_h['text'][:40]}\" vs prod \"{prod_h['text'][:40]}\"")

    # Report MD headings at levels that have no prod equivalent
    for md_level in md_heading_levels:
        expected_prod_level = md_level - heading_offset
        if expected_prod_level not in prod_sub_levels:
            md_at = [h for h in md_headings if h["level"] == md_level]
            warnings.append(
                f"  [struct] MD has {len(md_at)} h{md_level} heading(s) "
                f"with no prod h{expected_prod_level} equivalent: "
                + ", ".join(f'"{h["text"][:40]}"' for h in md_at))

    # --- Videos ---
    prod_videos = prod.get("videos", [])
    md_videos = md.get("videos", [])
    if prod_videos:
        md_video_signatures = {
            _normalize_media_signature(v.get("src", ""))
            for v in md_videos
            if _normalize_media_signature(v.get("src", ""))
        }
        missing_videos = []
        for prod_video in prod_videos:
            signature = _normalize_media_signature(prod_video.get("src", ""))
            if signature and signature in md_video_signatures:
                continue
            missing_videos.append(prod_video)

        if missing_videos:
            warnings.append(
                f"  [struct] Missing embedded video(s): MD has {len(md_videos)}, prod has {len(prod_videos)}. "
                "Videos are not auto-converted; add them manually post-conversion.")
            for video in missing_videos[:5]:
                detail = video.get("src") or video.get("text_preview") or video.get("tag", "video")
                warnings.append(f"           Prod video: {detail[:140]}")

    # --- Tables ---
    md_tables = md.get("tables", [])
    prod_tables = prod.get("tables", [])

    # Only flag table count mismatch if table content is actually missing
    # (content might exist in list format instead)
    if len(md_tables) != len(prod_tables):
        # Check if prod tables' content exists in MD (in any format)
        all_prod_table_content = set()
        for t in prod_tables:
            content = _norm_para(t.get("text_preview", ""))
            if content:
                all_prod_table_content.add(content)
            for h in t.get("headers", []):
                h_norm = _norm_para(h)
                if h_norm:
                    all_prod_table_content.add(h_norm)

        # If all prod table content exists in MD (in any format), don't flag count mismatch
        missing_table_content = False
        for content_snippet in all_prod_table_content:
            if not _content_exists_in_format(content_snippet, md, exclude_tables=False):
                missing_table_content = True
                break

        if missing_table_content:
            warnings.append(
                f"  [struct] Table count: MD has {len(md_tables)}, "
                f"prod has {len(prod_tables)}")
    # Compare matched tables using heading/header-aware pairing instead of pure order.
    for i, (md_t, prod_t) in enumerate(_pair_tables(md_tables, prod_tables)):
        md_cols = md_t.get("col_count", 0)
        prod_cols = prod_t.get("col_count", 0)
        if md_cols != prod_cols:
            warnings.append(
                f"  [struct] Table {i+1} columns: MD has {md_cols}, "
                f"prod has {prod_cols}")
        md_rows = md_t.get("row_count", 0)
        prod_rows = prod_t.get("row_count", 0)
        if md_rows != prod_rows:
            warnings.append(
                f"  [struct] Table {i+1} rows: MD has {md_rows}, "
                f"prod has {prod_rows}")
        # Compare headers (only if MD headers are not empty)
        # If MD headers are empty, skip header comparison since prod won't render empty headers
        md_headers_empty = md_t.get("headers_empty", False)
        if not md_headers_empty:
            md_hdrs = md_t.get("headers", [])
            prod_hdrs = prod_t.get("headers", [])
            for j, (mh, ph) in enumerate(zip(md_hdrs, prod_hdrs)):
                # Use _norm_para to handle Markdown syntax (links, bold, etc.)
                mh_norm = _norm_para(mh)
                ph_norm = _norm_para(ph)
                # Only flag if the normalized content differs significantly
                # Use similarity check to avoid false positives from minor formatting differences
                if mh_norm and ph_norm:
                    similarity = _similarity_score(mh_norm, ph_norm)
                    if similarity < 0.8:  # Allow 20% difference for minor variations
                        warnings.append(
                            f"  [struct] Table {i+1} header col {j+1}: "
                            f"MD \"{mh[:40]}\" vs prod \"{ph[:40]}\"")
                elif mh_norm != ph_norm:
                    # If one is empty, flag only if they're significantly different
                    warnings.append(
                        f"  [struct] Table {i+1} header col {j+1}: "
                        f"MD \"{mh[:40]}\" vs prod \"{ph[:40]}\"")

    # --- Top-level paragraphs ---
    md_paras = md.get("paragraphs", [])
    prod_paras = prod.get("paragraphs", [])

    # Paragraph count differences are expected when prod combines paragraphs with
    # code blocks (intro + code + output in one <div class="p">). The content-based
    # matching below surfaces any real differences without noisy count warnings.

    # Content-based matching: for each MD paragraph, find best match in prod.
    # This avoids cascading mismatches when counts differ by 1-2.
    prod_norms = [_norm_para(p["text"]) for p in prod_paras]
    prod_matched = [False] * len(prod_paras)
    md_unmatched = []
    for md_p in md_paras:
        md_norm = _norm_para(md_p["text"])
        found = False
        for j, pn in enumerate(prod_norms):
            if not prod_matched[j] and md_norm and pn and md_norm == pn:
                prod_matched[j] = True
                found = True
                # Prod text has Markdown formatting via _html_to_md_text,
                # so we can detect both wording and formatting differences.
                if md_norm != pn:
                    # Suppress when MD text is a prefix of prod â€” prod often
                    # wraps intro paragraph + code block + output paragraph in
                    # a single <div class="p">, so the prod text is longer.
                    if not pn.startswith(md_norm):
                        warnings.append(
                            f"  [struct] Paragraph text differs:\n"
                            f"    MD:   \"{md_p['text'][:60]}...\"\n"
                            f"    Prod: \"{prod_paras[j]['text'][:60]}...\"")
                elif _norm_para_fmt(md_p["text"]) != _norm_para_fmt(prod_paras[j]["text"]):
                    warnings.append(
                        f"  [struct] Paragraph formatting differs:\n"
                        f"    MD:   \"{md_p['text'][:60]}...\"\n"
                        f"    Prod: \"{prod_paras[j]['text'][:60]}...\"")
                break
        if not found:
            md_unmatched.append(md_p)

    prod_unmatched = [prod_paras[j] for j in range(len(prod_paras))
                      if not prod_matched[j]]

    # Enhanced content matching: if unmatched MD paragraph's content appears anywhere
    # in prod (in any format: paragraphs, tables, lists), suppress the warning.
    # Similarly for prod content â€” it might be reformatted in MD.
    still_unmatched_md = []
    for md_p in md_unmatched:
        # Code-only MD paragraphs (start and end with backtick) correspond to
        # inline code spans that exist in prod but are excluded from paragraph
        # extraction â€” suppress them symmetrically to the prod-side check above.
        if md_p["text"].strip().startswith('`') and md_p["text"].strip().endswith('`'):
            continue
        md_norm = _norm_para(md_p["text"])
        # Check if this content exists in prod in any format
        if not _content_exists_in_format(md_norm, prod, exclude_tables=False):
            still_unmatched_md.append(md_p)

    still_unmatched_prod = []
    for prod_p in prod_unmatched:
        # Code-only prod paragraphs (start with backtick) correspond to code
        # fences in MD which are never extracted as paragraphs â€” suppress them
        if prod_p["text"].strip().startswith('`'):
            continue
        # Image-led prod paragraphs often represent icon + label patterns that
        # MD may express as list items or plain text instead of a paragraph.
        if prod_p["text"].strip().startswith('![]('):
            continue
        pn = _norm_para(prod_p["text"])
        # Check if this content exists in MD in any format
        if not _content_exists_in_format(pn, md, exclude_tables=False):
            still_unmatched_prod.append(prod_p)

    # Only flag truly missing content (not just reformatted content)
    # Try to pair unmatched MDâ†”prod paragraphs to show "wording differs" instead
    # of just "not found" when a close match exists.
    paired_md = set()
    paired_prod = set()
    for i, md_p in enumerate(still_unmatched_md):
        mn = _norm_para(md_p["text"])
        best_j, best_score = -1, 0.0
        for j, prod_p in enumerate(still_unmatched_prod):
            if j in paired_prod:
                continue
            pn = _norm_para(prod_p["text"])
            # Use longest common prefix as pairing signal â€” catches word
            # insertions/changes that break char-by-char similarity
            lcp = 0
            for c1, c2 in zip(mn, pn):
                if c1 != c2:
                    break
                lcp += 1
            if lcp >= 10:
                score = 0.9
            else:
                score = _similarity_score(mn, pn)
            if score > best_score:
                best_score = score
                best_j = j
        if best_score > 0.5 and best_j >= 0:
            paired_md.add(i)
            paired_prod.add(best_j)
            warnings.append(
                f"  [struct] Paragraph wording differs (line {md_p['line'] + 1}):\n"
                f"    MD:   \"{md_p['text'][:60]}...\"\n"
                f"    Prod: \"{still_unmatched_prod[best_j]['text'][:60]}...\"")

    for i, md_p in enumerate(still_unmatched_md):
        if i not in paired_md:
            md_norm = _norm_para(md_p["text"])
            in_prod_note = False
            for note in prod.get("notes", []):
                note_norm = _norm_para(note.get("text", ""))
                if note_norm and (md_norm[:50] in note_norm or note_norm[:50] in md_norm):
                    in_prod_note = True
                    break
            if in_prod_note:
                warnings.append(
                    f"  [struct] Paragraph location differs (line {md_p['line'] + 1}): "
                    f"plain text in MD, inside a note in prod "
                    f"(\"{md_p['text'][:60]}...\")")
            else:
                warnings.append(
                    f"  [struct] Paragraph in MD not found in prod (line {md_p['line'] + 1}): "
                    f"\"{md_p['text'][:60]}...\"")
    for j, prod_p in enumerate(still_unmatched_prod):
        if j not in paired_prod:
            warnings.append(
                f"  [struct] Paragraph in prod not found in MD: "
                f"\"{prod_p['text'][:60]}...\"")

    return warnings


def fix_md_file(md_path: str, prod_url: str, dry_run: bool = False) -> dict:
    """
    Fix known conversion issues in an existing .md file.

    Returns a dict with counts of fixes applied and a 'log' key
    containing the messages emitted for this page.
    """
    # Snapshot the log so we can capture this page's messages
    log_start = len(_log_lines)

    _log(f"\nFixing: {md_path}")
    _log(f"  Prod: {prod_url}")

    # Read the .md file
    with open(md_path, "r", encoding="utf-8") as f:
        original = f.read()
    lines = original.splitlines()

    # Pre-processing: collapse quoted multi-line YAML titles into one line.
    old_lines = lines[:]
    lines = fix_multiline_frontmatter_title(lines)
    frontmatter_title_fixes = 1 if lines != old_lines else 0
    if frontmatter_title_fixes:
        _log("  [fix] Collapsed multi-line frontmatter title into one line")

    # Pre-processing: replace HTML entities &lt; &gt; with <> (no prod needed)
    # Temporarily disabled.
    # code_entity_fixes = 0
    # for i, ln in enumerate(lines):
    #     if '&lt;' in ln or '&gt;' in ln:
    #         new_ln = ln.replace('&lt;', '<').replace('&gt;', '>')
    #         if new_ln != ln:
    #             lines[i] = new_ln
    #             code_entity_fixes += 1
    # if code_entity_fixes:
    #     _log(f"  [fix] Replaced HTML entities (&lt;/&gt;) with <> in {code_entity_fixes} line(s)")
    code_entity_fixes = 0

    # Pre-processing: remove "Expand Table" lines (artefact from DITA conversion).
    expand_table_fixes = 0
    filtered = []
    for ln in lines:
        if ln.strip() == "Expand Table":
            expand_table_fixes += 1
        else:
            filtered.append(ln)
    if expand_table_fixes:
        lines = filtered
        _log(f"  [fix] Removed {expand_table_fixes} 'Expand Table' line(s)")

    # Pre-processing: remove "assignment" DITA artefacts.
    # 1. Trailing " assignment" at the end of a markdown line that introduces a
    #    code block (next non-blank line is a code fence).
    # 2. <button> assignment </button> inside raw HTML table cells.
    assignment_fixes = 0
    for i, ln in enumerate(lines):
        # Case 1: trailing word in markdown prose
        stripped_ln = ln.rstrip()
        if stripped_ln.endswith(' assignment') or stripped_ln == 'assignment':
            j = i + 1
            while j < len(lines) and not lines[j].strip():
                j += 1
            if j < len(lines) and re.match(r'^\s*```', lines[j]):
                if stripped_ln == 'assignment':
                    lines[i] = ''
                else:
                    lines[i] = stripped_ln[:-len(' assignment')]
                assignment_fixes += 1
        # Case 2: <button> assignment </button> in raw HTML table cells
        if '<button>' in ln and 'assignment' in ln:
            new_ln = re.sub(r'<button>\s*assignment\s*</button>\s*', '', ln)
            if new_ln != ln:
                lines[i] = new_ln
                assignment_fixes += 1
    if assignment_fixes:
        _log(f"  [fix] Removed {assignment_fixes} 'assignment' artefact(s)")

    # Pre-processing: fix UTF-8 chars corrupted by Windows-1252 mis-decoding.
    # Each entry is (corrupted_str, correct_unicode_char).
    # The corrupted sequences arise from UTF-8 multi-byte sequences being read
    # as Windows-1252: e.g. em dash U+2014 = E2 80 94 â†’ Ã¢ (E2) + â‚¬ (80) + " (94).
    _WIN1252_FIXES = [
        ('\u00e2\u20ac\u201d', '\u2014'),   # ??? em dash        (E2 80 94)
        ('\u00e2\u20ac\u201c', '\u2013'),   # ??? en dash        (E2 80 93)
        ('\u00e2\u20ac\u0153', '\u201c'),   # " left dbl quote (E2 80 9C)
        ('\u00e2\u20ac\u009d', '\u201d'),   # " right dbl quote (E2 80 9D)
        ('\u00e2\u20ac\u2122', '\u2019'),   # ' right sgl quote (E2 80 99)
        ('\u00e2\u20ac\u02dc', '\u2018'),   # ' left sgl quote  (E2 80 98)
        ('\u00e2\u20ac\u00a6', '\u2026'),   # ??? ellipsis       (E2 80 A6)
        ('\u00e2\u20ac\u00a2', '\u2022'),   # ??? bullet         (E2 80 A2)
        ('\u00c2\u00ae', '\u00ae'),         # ?? registered mark
        ('\u00e2\u201e\u00a2', '\u2122'),   # ??? trademark
        ('\u00e2\u0153\u2026', '\u2705'),   # ??? check mark
        ('\u00e2\u009d\u0152', '\u274c'),   # ??? cross mark
        ('\u00e2\u2020\u2019', '\u2192'),   # ??? right arrow
        ('\u00e2\u2030\u00a4', '\u2264'),   # ??? less-than or equal
        ('\u00c3\u2014', '\u00d7'),         # ?? multiplication sign
        ('\u00c3\u00a9', '\u00e9'),         # ?? e-acute
    ]
    encoding_fixes = 0
    for i, ln in enumerate(lines):
        new_ln = _replace_special_image_sources(ln)
        new_ln = _repair_mojibake_line(new_ln)
        for corrupted, correct in _WIN1252_FIXES:
            if corrupted in new_ln:
                new_ln = new_ln.replace(corrupted, correct)
        if new_ln != ln:
            lines[i] = new_ln
            encoding_fixes += 1
    if encoding_fixes:
        _log(f"  [fix] Fixed encoding corruption (Windows-1252 mis-decode) in {encoding_fixes} line(s)")

    # Pre-processing: split collapsed list items (no prod needed)
    old_lines = lines[:]
    lines = fix_collapsed_list_items(lines)
    collapsed_list_fixes = 1 if lines != old_lines else 0

    # Fetch and parse prod HTML
    _log("  Fetching prod HTML...")
    container = fetch_prod_html(prod_url)
    if not container:
        _log("  [error] Could not fetch prod HTML. Aborting.")
        return {"errors": 1}

    prod = extract_prod_structure(container)
    md = parse_md_structure(lines)

    stats = {"note_indent": 0, "note_paragraphs": 0, "image_indent": 0,
             "orphaned_para": 0, "table_indent": 0,
             "para_indent": 0, "collapsed_list": collapsed_list_fixes,
             "frontmatter_title": frontmatter_title_fixes,
             "code_entities": code_entity_fixes, "encoding": encoding_fixes,
             "expand_table": expand_table_fixes, "assignment": assignment_fixes, "errors": 0}

    # Fix orphaned paragraphs first (before other fixes shift line numbers)
    for orphan in reversed(md["orphaned_paragraphs"]):
        old_lines = lines[:]
        lines = fix_orphaned_paragraph(lines, orphan, prod["li_paragraphs"],
                                       prod_top_paragraphs=prod["paragraphs"])
        if lines != old_lines:
            stats["orphaned_para"] += 1

    # Fix wrongly indented paragraphs: lines that are indented but match a
    # top-level prod paragraph (not inside a list).  These were likely indented
    # by a previous run or by the converter.
    md = parse_md_structure(lines)
    prod_top_norms = [_normalize(p["text"]) for p in prod["paragraphs"]]
    code_fence_re_fix = re.compile(r'^\s*(```|~~~)')
    list_head_re_fix = re.compile(r'^(\s*)([-*+]|\d+\.)\s+')
    heading_re_fix = re.compile(r'^#{1,6}\s')
    in_code_fix = False
    in_note_fix = False
    for i, line in enumerate(lines):
        if code_fence_re_fix.match(line):
            in_code_fix = not in_code_fix
            continue
        if in_code_fix:
            continue
        stripped = line.strip()
        if re.match(r'^:::\w+', stripped):
            in_note_fix = True
            continue
        if stripped == ':::':
            in_note_fix = False
            continue
        if in_note_fix:
            continue
        indent = len(line) - len(line.lstrip(' '))
        if indent == 0 or not stripped:
            continue
        # Skip list items, headings, images, tables, HTML, fences
        if list_head_re_fix.match(line) or heading_re_fix.match(stripped):
            continue
        if stripped.startswith('![') or stripped.startswith('|') or stripped.startswith('<'):
            continue
        if stripped.startswith('---'):
            continue
        # Check if this indented text matches a top-level prod paragraph
        line_norm = _normalize(stripped)
        if len(line_norm) < 10:
            continue
        for pn in prod_top_norms:
            if pn.startswith(line_norm[:30]) or line_norm.startswith(pn[:30]):
                _log(f"  [fix] Paragraph at line {i + 1}: indent {indent} -> 0 "
                      f"(top-level in prod) (\"{stripped[:50]}...\")")
                lines[i] = lines[i].lstrip(' ')
                stats["orphaned_para"] += 1
                break

    # Re-parse after fixes (line numbers may have shifted due to indent changes)
    md = parse_md_structure(lines)

    # Fix notes
    note_pairs = match_notes(md["notes"], prod["notes"])
    for md_note, prod_note in reversed(note_pairs):  # reversed to preserve line numbers
        old_lines = lines[:]
        lines = fix_note_indent(lines, md_note, prod_note)
        if lines != old_lines:
            stats["note_indent"] += 1
        old_lines = lines[:]
        lines = fix_note_paragraphs(lines, md_note, prod_note)
        if lines != old_lines:
            stats["note_paragraphs"] += 1

    # Re-parse after note fixes (they can add/remove lines, shifting all subsequent line numbers)
    md = parse_md_structure(lines)

    # Fix images
    img_pairs = match_images(md["images"], prod["images"])
    for md_img, prod_img in reversed(img_pairs):
        old_lines = lines[:]
        lines = fix_image_indent(lines, md_img, prod_img)
        if lines != old_lines:
            stats["image_indent"] += 1

    # Fix tables
    # Re-parse to get fresh line numbers after previous fixes
    md = parse_md_structure(lines)
    tbl_pairs = match_tables(md["tables"], prod["tables"])
    for md_tbl, prod_tbl in reversed(tbl_pairs):
        old_lines = lines[:]
        lines = fix_table_indent(lines, md_tbl, prod_tbl)
        if lines != old_lines:
            stats["table_indent"] += 1

    # Insert <br> between code blocks and text in markdown table cells
    md = parse_md_structure(lines)
    md_pipe_re = re.compile(r'^\s*\|')
    md_sep_re = re.compile(r'^\s*\|([\s:]*-+[\s:]*\|)+\s*$')
    code_br_fixes = 0
    for i, ln in enumerate(lines):
        if not md_pipe_re.match(ln) or md_sep_re.match(ln):
            continue
        if '```' not in ln or ln.count('```') < 2:
            continue
        # Split line on | respecting backticks, process each cell
        raw = ln.rstrip()
        prefix = raw[:len(raw) - len(raw.lstrip())]  # preserve indent
        stripped = raw.strip()
        if stripped.startswith('|'):
            stripped = stripped[1:]
        if stripped.endswith('|'):
            stripped = stripped[:-1]
        # Split into cells on | outside backticks
        cells = []
        cur, in_bt, bt_n = [], False, 0
        ci = 0
        while ci < len(stripped):
            ch = stripped[ci]
            if not in_bt and ch == '`':
                j = ci
                while j < len(stripped) and stripped[j] == '`':
                    j += 1
                bt_n = j - ci
                in_bt = True
                cur.append(stripped[ci:j])
                ci = j
                continue
            if in_bt and ch == '`':
                j = ci
                while j < len(stripped) and stripped[j] == '`':
                    j += 1
                if j - ci == bt_n:
                    in_bt = False
                cur.append(stripped[ci:j])
                ci = j
                continue
            if ch == '|' and not in_bt:
                cells.append(''.join(cur))
                cur = []
            else:
                cur.append(ch)
            ci += 1
        cells.append(''.join(cur))
        # Process each cell: insert <br> between code blocks and text
        changed = False
        for j, cell in enumerate(cells):
            if '```' not in cell or cell.count('```') < 2:
                continue
            parts = cell.split('```')
            if len(parts) < 3:
                continue
            new_parts = [parts[0]]
            for k in range(1, len(parts)):
                if k % 2 == 1:
                    # Inside code block
                    new_parts.append('```' + parts[k])
                else:
                    # Closing ``` + outside text
                    new_parts.append('```')
                    text = parts[k]
                    text_stripped = text.strip()
                    if text_stripped:
                        # Text between code blocks or after last code block
                        # Don't prepend <br> if already present (idempotency)
                        if text_stripped.startswith('<br>'):
                            new_parts.append(text_stripped)
                        else:
                            new_parts.append('<br> ' + text_stripped)
                        if k < len(parts) - 1:
                            # If text already ends with <br>, preserve trailing
                            # whitespace instead of adding a new separator
                            if text_stripped.endswith('<br>'):
                                new_parts.append(text[len(text.rstrip()):] or ' ')
                            else:
                                new_parts.append('<br> ')
                        else:
                            new_parts.append(text[len(text.rstrip()):])
                    else:
                        new_parts.append(text)
            new_cell = ''.join(new_parts)
            if new_cell != cell:
                cells[j] = new_cell
                changed = True
        if changed:
            lines[i] = prefix + '| ' + ' | '.join(cells) + ' |'
            code_br_fixes += 1
            _log(f"  [fix] Table row at line {i + 1}: inserted <br> between code blocks and text")
    stats["table_code_br"] = code_br_fixes

    # Fix paragraphs at col-0 that belong inside a list item
    md = parse_md_structure(lines)
    lines, n_para = fix_misindented_paragraphs(lines, md, prod)
    stats["para_indent"] += n_para

    lines, marker_fixes = remove_toplevel_note_markers(lines)
    stats["marker_cleanup"] = marker_fixes

    # Structural comparison â€” detect mismatches between MD and prod
    md_final = parse_md_structure(lines)
    _warn_mixed_captions(md_final)
    struct_warnings = compare_structure(md_final, prod)
    stats["structural_warnings"] = len(struct_warnings)
    if struct_warnings:
        _log("")
        for w in struct_warnings:
            _log(w)

    # Write fixes â€” preserve trailing newline if original had one
    result = "\n".join(lines)
    if original.endswith("\n"):
        result += "\n"
    total_fixes = sum(v for k, v in stats.items() if k not in ("errors", "log", "structural_warnings"))
    if result != original:
        if dry_run:
            _log(f"\n  [dry-run] Would fix: {total_fixes} issues")
        else:
            with open(md_path, "w", encoding="utf-8") as f:
                f.write(result)
            _log(f"\n  Applied {total_fixes} fix(es). File updated.")
    else:
        if struct_warnings:
            _log(f"\n  No auto-fixes needed, but {len(struct_warnings)} structural warning(s) found.")
        else:
            _log("\n  No fixes needed.")

    stats["log"] = _log_lines[log_start:]
    return stats


