def _similarity_score(s1: str, s2: str) -> float:
    """Calculate similarity score between two normalized strings (0-1)."""
    if not s1 or not s2:
        return 1.0 if s1 == s2 else 0.0
    # Calculate character overlap (simplified similarity)
    matches = sum(1 for a, b in zip(s1, s2) if a == b)
    return matches / max(len(s1), len(s2))


def _table_header_signature(table: dict) -> tuple[str, ...]:
    return tuple(_norm_para(h) for h in table.get("headers", []) if _norm_para(h))


def _assign_md_heading_context(structure: dict, key: str, line_key: str) -> None:
    headings = sorted(structure.get("headings", []), key=lambda h: h.get("line", -1))
    items = structure.get(key, [])
    heading_idx = 0
    current_heading = None
    for item in sorted(items, key=lambda t: t.get(line_key, -1)):
        start_line = item.get(line_key, -1)
        while heading_idx < len(headings) and headings[heading_idx].get("line", -1) < start_line:
            current_heading = headings[heading_idx]
            heading_idx += 1
        if current_heading:
            item["heading_text"] = current_heading.get("text", "")
            item["heading_level"] = current_heading.get("level", 0)
        else:
            item["heading_text"] = ""
            item["heading_level"] = 0


