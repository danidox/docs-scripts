def _normalize_media_signature(value: str) -> str:
    value = (value or "").strip()
    if not value:
        return ""
    parsed = urllib.parse.urlparse(value)
    path = (parsed.path or "").rstrip('/')
    query = urllib.parse.parse_qsl(parsed.query, keep_blank_values=True)
    filtered_query = [
        (k, v)
        for k, v in query
        if k.lower() not in {"si", "feature", "autoplay", "rel"}
    ]
    normalized_query = urllib.parse.urlencode(filtered_query)
    return urllib.parse.urlunparse((
        parsed.scheme.lower(),
        parsed.netloc.lower(),
        path,
        "",
        normalized_query,
        "",
    ))


def _is_video_url(value: str) -> bool:
    lowered = _normalize_media_signature(value).lower()
    if not lowered:
        return False
    video_markers = (
        "youtube.com/embed/", "youtube.com/watch", "youtu.be/",
        "player.vimeo.com/video/", "vimeo.com/", "wistia",
        "loom.com/embed", "loom.com/share", "vidyard",
        ".mp4", ".webm", ".m3u8",
    )
    return any(marker in lowered for marker in video_markers)


def _extract_media_signature_from_html(html: str) -> str:
    for attr in ("src", "data-src", "poster"):
        marker = f'{attr}="'
        start = html.lower().find(marker)
        if start == -1:
            continue
        start += len(marker)
        end = html.find('"', start)
        if end == -1:
            continue
        value = _normalize_media_signature(html[start:end])
        if attr == "poster" or _is_video_url(value):
            return value
    return ""


def _tag_represents_video(tag: Tag) -> bool:
    if tag.name == "video":
        return True
    if tag.name != "iframe":
        return False
    for attr in ("src", "data-src", "poster"):
        if _is_video_url(tag.get(attr, "")):
            return True
    attrs_text = " ".join(str(v) for v in tag.attrs.values())
    return "video" in attrs_text.lower()


def extract_prod_structure(container: Tag) -> dict:
    """Extract structural info from prod HTML: notes, images, videos, list items, inline paragraphs."""
    structure = {
        "notes": [],       # [{"in_list": bool, "li_depth": int, "text_preview": str}]
        "images": [],      # [{"in_list": bool, "li_depth": int, "src": str}]
        "videos": [],      # [{"in_list": bool, "li_depth": int, "src": str, "tag": str}]
        "list_items": [],  # [{"depth": int, "text": str, "has_note": bool}]
        "li_paragraphs": [],  # paragraphs/divs inside <li> that md_maker may orphan
        "tables": [],      # [{"in_list": bool, "li_depth": int, "text_preview": str}]
        "headings": [],    # [{"level": int, "text": str}]
        "paragraphs": [],  # [{"text": str}]  â€” top-level paragraphs (not in lists/tables/notes)
    }

    ADMON_CLASSES = {"note", "warning", "important", "information", "tip", "danger"}

    # Extract note/admonition positions
    for div in container.find_all("div", class_=True):
        classes = set(div.get("class", []))
        if "note" not in classes and not ADMON_CLASSES & classes:
            continue
        li_parent = div.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        text = div.get_text(strip=True)[:80]
        # Collect direct-child <p> elements (not inside <ul>/<ol>/<li>)
        # to locate paragraph breaks.  <p> inside list items are bullet
        # content, not standalone paragraphs.
        para_starts = []
        direct_p_count = 0
        for p in div.find_all("p"):
            # Check if this <p> is inside a list within the note
            list_ancestor = p.find_parent(["ul", "ol"])
            if list_ancestor and div in list_ancestor.parents:
                continue  # inside a list â€” skip
            p_text = re.sub(r'\s+', ' ', _html_to_md_text(p).strip())
            if p_text:
                para_starts.append(p_text)
                direct_p_count += 1
        structure["notes"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "text_preview": text,
            "text": re.sub(r'\s+', ' ', _html_to_md_text(div).strip()),
            "paragraphs": direct_p_count,
            "para_starts": para_starts,
        })

    # Extract image positions â€” only block-level images (not inline icons).
    # An image is inline if its immediate block-level ancestor (p, li, td)
    # contains significant text besides the image.
    # Images inside admonition divs (notes/warnings) are also excluded because
    # the MD image extractor skips images inside :::note blocks, so both must
    # agree on which images exist to keep ordering consistent for match_images().
    for img in container.find_all("img"):
        src = img.get("src", "")
        if any(image_name in src for image_name in SPECIAL_IMAGE_SOURCE_TO_TEXT):
            continue
        # Skip images inside admonition divs (notes, warnings, etc.)
        if img.find_parent(lambda tag: tag.name == "div"
                           and ADMON_CLASSES & set(tag.get("class") or [])):
            continue
        block = img.parent
        while block and block.name not in ("p", "li", "td", "div", "body"):
            block = block.parent
        if block and block.name in ("p", "li"):
            sibling_text = ""
            for sib in block.children:
                if sib is img:
                    continue
                if isinstance(sib, NavigableString):
                    sibling_text += str(sib).strip()
                elif hasattr(sib, "get_text") and sib.name not in ("style", "script"):
                    if not sib.find("img"):
                        sibling_text += sib.get_text(strip=True)
            if len(sibling_text) > 5:
                continue  # inline icon â€” skip

        li_parent = img.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        structure["images"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "src": src,
        })

    # Extract embedded videos so converted markdown can flag media that must
    # be added back manually after conversion.
    seen_videos = set()
    for media_tag in container.find_all(["video", "iframe"]):
        if not _tag_represents_video(media_tag):
            continue
        src = _normalize_media_signature(
            media_tag.get("src")
            or media_tag.get("data-src")
            or media_tag.get("poster")
            or ""
        )
        preview = re.sub(r'\s+', ' ', media_tag.get_text(" ", strip=True))[:80]
        signature = src or f"{media_tag.name}:{preview}"
        if signature in seen_videos:
            continue
        seen_videos.add(signature)
        li_parent = media_tag.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        structure["videos"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "src": src,
            "tag": media_tag.name,
            "text_preview": preview,
        })

    # Extract figure captions. In DITA output these often live in
    # <span class="figcap"> elements rather than regular paragraphs.
    for cap in container.find_all("span", class_="figcap"):
        text = re.sub(r'\s+', ' ', _html_to_md_text(cap).strip())
        if text and len(text) > 10:
            text_norm = re.sub(r'\s+', '', text.lower())
            if not any(re.sub(r'\s+', '', p["text"].lower()) == text_norm
                       for p in structure["paragraphs"]):
                structure["paragraphs"].append({"text": text})

    # Extract table captions/titles. These are often emitted as real <caption>
    # elements or as adjacent title blocks like "Table 1. Foo", which MD keeps
    # as plain text lines above the table. Treat them like figure captions for
    # Structural comparison so they do not get flagged as missing paragraphs.
    table_caption_re = re.compile(r'^table\s+\d+\.\s+\S+', re.I)

    def _append_caption_text(text: str) -> None:
        text = re.sub(r'\s+', ' ', text.strip())
        if not text or len(text) <= 10:
            return
        text_norm = re.sub(r'\s+', '', text.lower())
        if not any(re.sub(r'\s+', '', p["text"].lower()) == text_norm
                   for p in structure["paragraphs"]):
            structure["paragraphs"].append({"text": text})

    for table in container.find_all("table"):
        caption = table.find("caption")
        if caption:
            caption_text = _html_to_md_text(caption).strip() or caption.get_text(" ", strip=True)
            _append_caption_text(caption_text)

        for sibling in (table.find_previous_sibling(), table.find_next_sibling()):
            if not isinstance(sibling, Tag):
                continue
            if sibling.name not in ("p", "div", "span"):
                continue
            sibling_text = re.sub(r'\s+', ' ', _html_to_md_text(sibling).strip() or sibling.get_text(" ", strip=True))
            if table_caption_re.match(sibling_text):
                _append_caption_text(sibling_text)

    # Extract list items
    for li in container.find_all("li"):
        depth = _li_depth(li)
        # Check if this li directly contains a note
        has_note = any(
            div for div in li.find_all("div", class_=True, recursive=False)
            if {"note"} & set(div.get("class", []))
            or ADMON_CLASSES & set(div.get("class", []))
        )
        # Also check in itemgroup info children
        for info_div in li.find_all("div", class_="itemgroup"):
            if any(
                div for div in info_div.find_all("div", class_=True, recursive=False)
                if ADMON_CLASSES & set(div.get("class", []))
            ):
                has_note = True

        text = _li_text(li)
        structure["list_items"].append({
            "depth": depth,
            "text": text,
            "has_note": has_note,
        })

    # Extract paragraphs/divs inside <li> that may get orphaned by md_maker.
    # These are block children (p, div) of a <li> that contain text and are
    # followed by or preceded by a sub-list.
    for li in container.find_all("li"):
        li_first_text = ""
        # Get the first text content of the li (the list item head)
        for child in li.children:
            if isinstance(child, NavigableString) and child.strip():
                li_first_text = child.strip()[:80]
                break
            if isinstance(child, Tag) and child.name in ("span", "p", "a", "strong", "em", "b"):
                li_first_text = child.get_text(strip=True)[:80]
                break

        for child in li.children:
            if not isinstance(child, Tag):
                continue
            if child.name in ("ul", "ol"):
                continue
            # Skip admonition divs
            cls = child.get("class") if child.attrs else None
            if cls and ADMON_CLASSES & set(cls or []):
                continue
            # Look for block elements with text that are siblings of sub-lists
            if child.name in ("div", "p") and child.get_text(strip=True):
                # Extract inline text segments (bare text between block children, e.g.
                # footnotes that appear after a table inside the same <div class="p">).
                # Falls back to full child text if no inline segments are found.
                segs = _extract_inline_segs(child)
                if not segs:
                    segs = [re.sub(r'\s+', ' ', child.get_text(strip=True))]
                # Also capture nested block paragraphs inside itemgroup/info-like
                # wrappers, e.g. direct text followed by <div><p>...</p></div>.
                if child.name == "div":
                    for nested in child.find_all(["div", "p"]):
                        if nested is child:
                            continue
                        if nested.find_parent(["ul", "ol"]) and child in nested.find_parents(["ul", "ol"]):
                            continue
                        nested_segs = _extract_inline_segs(nested)
                        if not nested_segs:
                            nested_segs = [re.sub(r'\s+', ' ', nested.get_text(strip=True))]
                        segs.extend(nested_segs)
                for seg in segs:
                    seg_text = re.sub(r'\s+', ' ', seg).strip()
                    if not seg_text or len(seg_text) <= 10:
                        continue
                    # Skip if this segment is the same as the li head itself
                    if seg_text[:60] == li_first_text[:60]:
                        continue
                    structure["li_paragraphs"].append({
                        "li_head": li_first_text,
                        "text": seg_text,
                        "li_depth": _li_depth(li),
                    })

    def _cell_text(cell) -> str:
        """Get text from a table cell, stripping UI chrome (copy buttons, icons)."""
        from bs4 import BeautifulSoup as _BS
        clone = _BS(str(cell), "html.parser").find(cell.name)
        if clone:
            for btn in clone.find_all("button"):
                btn.decompose()
        return re.sub(r'\s+', ' ', clone.get_text(strip=True) if clone else "")

    # Extract HTML tables
    for table in container.find_all("table"):
        li_parent = table.find_parent("li")
        depth = _li_depth(li_parent) if li_parent else -1
        first_row = table.find("tr")
        preview = first_row.get_text(strip=True)[:80] if first_row else ""
        # Extract table headers and row count for structural comparison
        headers = []
        header_row = table.find("tr")
        if header_row:
            for th in header_row.find_all(["th", "td"]):
                headers.append(_cell_text(th))
        rows = table.find_all("tr")
        heading = table.find_previous(["h1", "h2", "h3", "h4", "h5", "h6"])
        structure["tables"].append({
            "in_list": li_parent is not None,
            "li_depth": depth,
            "text_preview": preview,
            "element": table,
            "headers": headers,
            "row_count": len(rows),
            "col_count": len(headers),
            "heading_text": re.sub(r'\s+', ' ', _html_to_md_text(heading).strip()) if heading else "",
            "heading_level": int(heading.name[1]) if heading and heading.name and len(heading.name) == 2 else 0,
        })

    # Extract headings (h1-h6) for structural comparison
    for h in container.find_all(["h1", "h2", "h3", "h4", "h5", "h6"]):
        level = int(h.name[1])
        text = re.sub(r'\s+', ' ', _html_to_md_text(h).strip())
        # Strip trailing "link" text from anchor links
        text = re.sub(r'\s*link$', '', text)
        structure["headings"].append({"level": level, "text": text})

    # Extract top-level paragraphs (not inside tables, lists, or admonitions)
    # "prereq" only applies to the element's own class (a <div class="prereq p">
    # should not be extracted as a paragraph), but paragraphs INSIDE a prereq
    # section are valid content and should not be skipped.
    ADMON_OWN   = {"note", "warning", "important", "information", "tip", "danger", "prereq"}
    ADMON_ANCS  = {"note", "warning", "important", "information", "tip", "danger"}
    def _is_para_skip(el):
        if el.find_parent("table"):
            return True
        if el.find_parent(["ul", "ol", "dl", "dd"]):
            return True
        # Check the element's own classes (e.g. <div class="section prereq p">)
        if ADMON_OWN & set(el.get("class", [])):
            return True
        # Check ALL ancestor divs for admonition classes (prereq excluded â€”
        # children of prereq sections are regular paragraphs)
        for admon_parent in el.find_parents("div"):
            if ADMON_ANCS & set(admon_parent.get("class", [])):
                return True
        return False

    for p in container.find_all("p"):
        if _is_para_skip(p):
            continue
        text = re.sub(r'\s+', ' ', _html_to_md_text(p).strip())
        if text and len(text) > 10:
            # Skip footnote paragraphs (start with superscript number)
            if re.match(r'^\d+[A-Z]', text) and p.find("sup"):
                continue
            # Skip paragraphs that are purely a code span â€” these correspond to
            # code fence blocks in MD which are not extracted as paragraphs
            if text.startswith('`') and text.endswith('`'):
                continue
            structure["paragraphs"].append({"text": text})

    # Also extract text from <div class="p"> elements (not just <p> tags).
    # Use segment extraction so text after block children (tables, images, dl)
    # is also captured â€” not just text before the first block element.
    # Note: _is_para_skip is NOT applied here because _extract_inline_segs only
    # collects NavigableStrings/inline elements between block children â€” it
    # naturally skips content inside <dl>/<li> etc. â€” so a <div class="p">
    # that is itself nested inside a <dd> can still yield valid inline text
    # (e.g. a footnote that follows a </dl> inside the same div).
    # We only skip tables and admonition-owned divs.
    for div in container.find_all("div", class_="p"):
        if div.find_parent("table"):
            continue
        if ADMON_OWN & set(div.get("class", [])):
            continue
        in_admon = any(ADMON_ANCS & set(ap.get("class", []))
                       for ap in div.find_parents("div"))
        if in_admon:
            continue
        for text in _extract_inline_segs(div):
            text_norm = re.sub(r'\s+', '', text.lower())
            if not any(re.sub(r'\s+', '', p["text"].lower()) == text_norm
                       for p in structure["paragraphs"]):
                structure["paragraphs"].append({"text": text})

    # Handle <div class="context"> elements that may contain bare text nodes
    # alongside block children such as <p>. Some DITA pages put follow-up intro
    # text directly in the context div even when the first intro paragraph is a
    # normal <p>, so we need segment extraction here instead of skipping the div.
    for div in container.find_all("div", class_="context"):
        if _is_para_skip(div):
            continue
        for text in _extract_inline_segs(div):
            if text.startswith('`') and text.endswith('`'):
                continue
            text_norm = re.sub(r'\s+', '', text.lower())
            already = any(re.sub(r'\s+', '', p["text"].lower()) == text_norm
                         for p in structure["paragraphs"])
            if not already:
                structure["paragraphs"].append({"text": text})

    # Handle <div class="section"> with bare text nodes (no child <p>).
    # Extract ALL inline text segments between block elements.
    # No heading guard needed: the segment extractor only collects NavigableString
    # and inline elements â€” content in nested <div> blocks is naturally skipped,
    # so nav/TOC sections produce no segments.
    for div in container.find_all("div", class_="section"):
        if any(c.name == "p" for c in div.children if hasattr(c, "name") and c.name):
            continue
        own_classes = set(div.get("class", []))
        # Some task intro/prereq blocks are emitted as <div class="section prereq p">
        # with bare text nodes. Treat those as regular content, not admonitions.
        if _is_para_skip(div) and not ({"section", "prereq", "p"} <= own_classes):
            continue
        for text in _extract_inline_segs(div):
            text_norm = re.sub(r'\s+', '', text.lower())
            if not any(re.sub(r'\s+', '', p["text"].lower()) == text_norm
                       for p in structure["paragraphs"]):
                structure["paragraphs"].append({"text": text})

    # Handle <dl> definition lists â€” combine <dt>/<dd> pairs as "term : definition"
    # to match the MD format (e.g. "DisplayName : Retrieves the display name...")
    for dl in container.find_all("dl"):
        if _is_para_skip(dl):
            continue
        dts = dl.find_all("dt")
        dds = dl.find_all("dd")
        for dt, dd in zip(dts, dds):
            dt_text = re.sub(r'\s+', ' ', _html_to_md_text(dt).strip())
            dd_text = re.sub(r'\s+', ' ', _html_to_md_text(dd).strip())
            if dt_text and dd_text:
                text = f"{dt_text} : {dd_text}"
                if len(text) > 10:
                    text_norm = re.sub(r'\s+', '', text.lower())
                    if not any(re.sub(r'\s+', '', p["text"].lower()) == text_norm
                               for p in structure["paragraphs"]):
                        structure["paragraphs"].append({"text": text})

    # A prod sentence should not be classified both as a top-level paragraph and
    # as a list-continuation paragraph. When that happens, later fix phases can
    # fight each other and toggle indentation on the same MD line.
    li_para_norms = {
        _norm_para(p.get("text", ""))
        for p in structure["li_paragraphs"]
        if p.get("text")
    }
    if li_para_norms:
        filtered = []
        for para in structure["paragraphs"]:
            para_norm = _norm_para(para.get("text", ""))
            if para_norm and para_norm in li_para_norms:
                continue
            filtered.append(para)
        structure["paragraphs"] = filtered

    return structure


def _li_depth(li: Tag) -> int:
    """Count nesting depth of a <li> element."""
    depth = 0
    parent = li.parent
    while parent:
        if parent.name == "li":
            depth += 1
        parent = parent.parent
    return depth


def _li_text(li: Tag) -> str:
    """Get the direct text of a <li>, excluding sub-lists and notes."""
    clone = BeautifulSoup(str(li), "html.parser").find("li")
    if not clone:
        return ""
    for sub in clone.find_all(["ul", "ol"]):
        sub.decompose()
    ADMON = {"note", "warning", "important", "information", "tip", "danger"}
    for div in list(clone.find_all("div")):
        cls = div.get("class") if div.attrs else None
        if cls and ADMON & set(cls):
            div.decompose()
    return re.sub(r'\s+', ' ', clone.get_text(strip=True))[:120]


