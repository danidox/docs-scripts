def _normalize(s: str) -> str:
    return re.sub(r'\s+', ' ', s.strip().lower())[:60]


def _strip_sup_sub(text: str) -> str:
    """Strip <sup>/<sub> tags: remove tag+content if special chars, keep content if alphanumeric."""
    def _repl(m):
        inner = m.group(1)
        if re.match(r'^[\w\d]+$', inner):
            return inner  # keep numbers/letters like <sup>1</sup> â†’ 1
        return ''         # drop special chars like <sup>Â®</sup> â†’ ''
    t = re.sub(r'<sup[^>]*>(.*?)</sup>', _repl, text, flags=re.IGNORECASE)
    t = re.sub(r'<sub[^>]*>(.*?)</sub>', _repl, t, flags=re.IGNORECASE)
    return t


def _norm_para(text: str) -> str:
    """Normalize paragraph text for comparison â€” strip markdown/HTML formatting."""
    t = re.sub(r'!\[([^\]]*)\]\([^)]*\)', '', text)      # ![alt](url) â†’ strip entirely
    t = re.sub(r'\[([^\]]*)\]\([^)]*\)', r'\1', t)       # [text](url) â†’ text
    t = re.sub(r'\*\*(.+?)\*\*', r'\1', t)              # **bold** â†’ bold (lazy: allows * inside)
    t = re.sub(r'`([^`]*)`', r'\1', t)                  # `code` â†’ code
    t = _strip_sup_sub(t)                                 # handle <sup>/<sub>
    t = re.sub(r'<[^>]+>', '', t)                        # strip remaining HTML tags
    t = t.replace('&gt;', '>').replace('&lt;', '<')      # MD entities â†’ chars
    t = t.replace('&amp;', '&')                          # MD &amp; â†’ &
    t = t.replace('\u2019', "'").replace('\u2018', "'")  # curly single â†’ straight
    t = t.replace('\u201c', '"').replace('\u201d', '"')  # curly double â†’ straight
    t = t.replace('\u2014', '-').replace('\u2013', '-')  # em/en dash â†’ hyphen
    t = re.sub(r'\s+', '', t.strip().lower())            # collapse all whitespace
    return t


def _norm_para_fmt(text: str) -> str:
    """Normalize paragraph text keeping formatting (bold, code, italic).

    Strips URLs (which differ between environments) but keeps formatting
    markers so formatting differences can be detected.
    """
    t = re.sub(r'!\[([^\]]*)\]\([^)]*\)', r'![]()', text)    # ![alt](url) â†’ ![]()
    t = re.sub(r'\[([^\]]*)\]\([^)]*\)', r'[\1]()', t)       # [text](url) â†’ [text]()
    t = _strip_sup_sub(t)                                      # handle <sup>/<sub>
    t = re.sub(r'<[^>]+>', '', t)                              # strip remaining HTML tags
    t = t.replace('&gt;', '>').replace('&lt;', '<')            # MD entities â†’ chars
    t = t.replace('&amp;', '&')                                # MD &amp; â†’ &
    t = t.replace('\u2019', "'").replace('\u2018', "'")        # curly single â†’ straight
    t = t.replace('\u201c', '"').replace('\u201d', '"')        # curly double â†’ straight
    t = t.replace('\u2014', '-').replace('\u2013', '-')        # em/en dash â†’ hyphen
    t = re.sub(r'\s+', '', t.strip().lower())                  # collapse whitespace
    return t


def _norm_heading(text: str) -> str:
    """Normalize heading text for comparison.

    Prod heading HTML often includes injected self-anchor links rendered as
    Markdown links like `[â€‹](/#section-id)`. These are UI chrome, not content.
    Strip them before applying the standard text normalization.
    """
    t = re.sub(r'\[\s*\]\(/#[^)]*\)', '', text)
    return _norm_para(t)


