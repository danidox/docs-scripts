def get_with_retries(
    url: str,
    session: Optional[requests.Session] = None,
    max_tries: int = 5,
    base_sleep: float = 1.0,
    timeout: int = 20,
    **request_kwargs,
) -> requests.Response:
    s = session or requests.Session()
    headers = request_kwargs.pop("headers", None) or {"User-Agent": DEFAULT_USER_AGENT}

    tries = 0
    while True:
        tries += 1
        try:
            resp = s.get(url, headers=headers, timeout=timeout, **request_kwargs)
            if resp.status_code == 429:
                wait = _parse_retry_after(resp.headers.get("Retry-After"))
                if wait <= 0:
                    wait = base_sleep * (2 ** (tries - 1))
                wait *= (1 + random.random() * 0.25)
                if tries >= max_tries:
                    resp.raise_for_status()
                time.sleep(wait)
                continue
            if 500 <= resp.status_code < 600:
                if tries >= max_tries:
                    resp.raise_for_status()
                wait = base_sleep * (2 ** (tries - 1))
                wait *= (1 + random.random() * 0.25)
                time.sleep(wait)
                continue
            resp.raise_for_status()
            return resp
        except requests.exceptions.HTTPError as exc:
            status = getattr(exc.response, "status_code", None)
            if status and 500 <= status < 600 and tries < max_tries:
                wait = base_sleep * (2 ** (tries - 1))
                wait *= (1 + random.random() * 0.25)
                time.sleep(wait)
                continue
            raise
        except requests.exceptions.RequestException:
            if tries < max_tries:
                wait = base_sleep * (2 ** (tries - 1))
                wait *= (1 + random.random() * 0.25)
                time.sleep(wait)
                continue
            raise


def safe_get_html(url: str, session: requests.Session, **kw) -> requests.Response:
    resp = get_with_retries(url, session=session, **kw)
    content_type = resp.headers.get("Content-Type", "")
    looks_html = ("html" in content_type) or (content_type == "")

    if looks_html and _needs_cloudflare_access(url) and looks_like_login_wall(resp.text):
        host = _match_protected_docs_host(url)
        had_saved_session = load_saved_session_state() is not None
        configure_docs_dev_auth(session, host=host)
        resp = get_with_retries(url, session=session, **kw)
        content_type_2 = resp.headers.get("Content-Type", "")
        if ("html" in content_type_2 or content_type_2 == "") and looks_like_login_wall(resp.text):
            if had_saved_session:
                invalidate_saved_session()
            global _DOCS_DEV_PROMPTED
            if not _DOCS_DEV_PROMPTED:
                prompt_for_docs_dev_login(url, session=session)
                resp = get_with_retries(url, session=session, **kw)

    return resp


def _try_playwright_login(target_url: str, session: requests.Session) -> bool:
    if not _needs_cloudflare_access(target_url):
        return False

    try:
        from playwright.sync_api import sync_playwright
    except Exception as exc:
        _log(f"  [warn] Playwright login not available: {exc}")
        return False

    try:
        with sync_playwright() as pw:
            browser = pw.chromium.launch(headless=False)
            context = browser.new_context()
            page = context.new_page()
            page.goto(target_url)

            print("\nA browser window has opened to complete Cloudflare Access login.")
            print("1) Use the email login and enter your UiPath email, then click 'Send code'.")
            print("2) Wait for the Cloudflare Access login code email.")
            print("3) Paste the code in the login window.")
            print("4) Make sure you land on the docs content.")
            input("Press ENTER here after you finish SSO in the browser... ")

            got_any = False
            for cookie in context.cookies():
                domain = cookie.get("domain") or ""
                for host in DOCS_PROTECTED_HOSTS:
                    if host in domain:
                        session.cookies.set(cookie["name"], cookie["value"], domain=host)
                        got_any = True
                        break

            try:
                save_session_state(context.storage_state())
            except Exception:
                pass

            browser.close()
            if got_any:
                print("Imported authenticated cookies from Playwright into this session.")
            else:
                print("Didn't detect protected docs cookies from Playwright session.")
            return got_any
    except Exception as exc:
        _log(f"  [warn] Playwright login failed: {exc}")
        return False


def prompt_for_docs_dev_login(target_url: str, session: requests.Session) -> None:
    global _DOCS_DEV_PROMPTED
    _DOCS_DEV_PROMPTED = True

    host = _match_protected_docs_host(target_url)
    if not host:
        return

    parsed = urllib.parse.urlparse(target_url)
    if not parsed.netloc:
        return

    full_url = urllib.parse.urlunparse(parsed._replace())
    print("\nOpening Cloudflare Access login in your browser...")
    print("1) Use the email login and enter your UiPath email, then click 'Send code'.")
    print(f"2) Wait to receive the login code from [EXTERNAL] Cloudflare Access login code for {host}.")
    print("3) Paste the code in the login window.")
    print("4) Make sure you land on the docs content.")

    detected_kid = None
    try:
        detected_kid = _detect_kid_from_cloudflare(full_url, session=session)
        if detected_kid:
            print(f"[info] Detected Cloudflare Access KID: {detected_kid[:8]}...")
    except Exception:
        pass

    params = {"redirect_url": full_url}
    if detected_kid:
        params["kid"] = detected_kid
    team_login = CLOUDFLARE_LOGIN_BASE + "?" + urllib.parse.urlencode(params)
    app_login = f"https://{host}/cdn-cgi/access/login?" + urllib.parse.urlencode({"redirect_url": full_url})

    if _try_playwright_login(full_url, session):
        return

    opened_any = False
    for candidate in (team_login, app_login, full_url):
        try:
            webbrowser.open(candidate, new=2, autoraise=True)
            opened_any = True
        except Exception:
            pass
    if not opened_any:
        print("If the browser didn't open, try any of these manually:")
        print(f"- Team login: {team_login}")
        print(f"- App login:  {app_login}")
        print(f"- Direct:     {full_url}")

    input("Press ENTER after you finish signing in... ")
    time.sleep(1.2)


def render_html_with_playwright(url: str, session: requests.Session | None = None) -> str | None:
    global PLAYWRIGHT_DRIVER

    try:
        from playwright.sync_api import sync_playwright
    except Exception as exc:
        _log(f"  [warn] Playwright render not available: {exc}")
        return None

    try:
        if PLAYWRIGHT_DRIVER is None:
            pw = sync_playwright().start()
            browser = pw.chromium.launch(headless=True)
            storage_state = load_saved_session_state()
            context = browser.new_context(storage_state=storage_state or None)
            cookies_to_add = _requests_session_to_playwright_cookies(session)
            if cookies_to_add:
                context.add_cookies(cookies_to_add)

            page = context.new_page()
            _install_fast_routes(page)
            page.set_default_timeout(7000)
            page.set_default_navigation_timeout(15000)
            PLAYWRIGHT_DRIVER = (pw, browser, context, page)
        else:
            pw, browser, context, page = PLAYWRIGHT_DRIVER

        page.goto(url, wait_until="domcontentloaded")
        try:
            page.wait_for_selector("#DocContainer", timeout=5000)
        except Exception:
            pass
        return page.content()
    except Exception as exc:
        _log(f"  [warn] Playwright render failed: {exc}")
        return None

def _get_session() -> requests.Session:
    global _SESSION
    if _SESSION is None:
        _SESSION = requests.Session()
        _SESSION.headers["User-Agent"] = DEFAULT_USER_AGENT
    return _SESSION


def _extract_topic_body_container_from_next_data(soup: BeautifulSoup) -> Optional[Tag]:
    script = soup.find("script", id="__NEXT_DATA__")
    if not script:
        return None

    next_data_text = script.string or script.get_text()
    if not next_data_text.strip():
        return None

    try:
        data = json.loads(next_data_text)
    except Exception:
        return None

    container_items = ((((data.get("props") or {}).get("pageProps") or {}).get("page") or {})
                       .get("containerItems") or [])
    html_blocks: List[str] = []
    for item in container_items:
        values = ((((item.get("rawContent") or {}).get("data") or {}).get("Component") or {})
                  .get("Fields", {}).get("topicBody", {}).get("Values") or [])
        for value in values:
            if isinstance(value, str) and "<" in value:
                html_blocks.append(value)

    if not html_blocks:
        return None

    topic_soup = BeautifulSoup('<div id="DocContainer">' + "\n".join(html_blocks) + '</div>', "html.parser")
    return topic_soup.find(id="DocContainer")


def fetch_page(url: str) -> Tuple[Optional[Tag], Optional[BeautifulSoup]]:
    """Fetch a docs page with retries and optional Cloudflare auth."""
    session = _get_session()
    for attempt in range(1, FETCH_RETRIES + 1):
        try:
            resp = safe_get_html(url, session=session, timeout=FETCH_TIMEOUT)
            break
        except requests.RequestException as e:
            if attempt < FETCH_RETRIES:
                wait = attempt * 2
                _log(f"  [retry] Attempt {attempt}/{FETCH_RETRIES} failed for {url}: {e}")
                _log(f"          Retrying in {wait}s...")
                time.sleep(wait)
            else:
                _log(f"  [error] Failed to fetch {url} after {FETCH_RETRIES} attempts: {e}")
                return None, None

    soup = BeautifulSoup(resp.text, "html.parser")
    container = soup.find(id="DocContainer")
    topic_body_container = _extract_topic_body_container_from_next_data(soup)
    if topic_body_container and (not container or len(topic_body_container.get_text(" ", strip=True)) >= len(container.get_text(" ", strip=True))):
        container = topic_body_container
    if not container:
        container = soup.find("main") or soup.find("article") or soup.body

    if container and container.get_text(strip=True):
        return container, soup

    # Fallback: Playwright for client-rendered content
    try:
        html = render_html_with_playwright(url, session=session)
        if not html:
            raise RuntimeError("Playwright did not return HTML.")
        soup_pw = BeautifulSoup(html, "html.parser")
        container_pw = soup_pw.find(id="DocContainer") or soup_pw.body
        return container_pw, soup_pw
    except Exception as e:
        _log(f"  [warn] Playwright fallback failed: {e}")
        return container, soup


def fetch_prod_html(url: str) -> Optional[Tag]:
    """Fetch prod page and return just the content container."""
    container, _ = fetch_page(url)
    return container


